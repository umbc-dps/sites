 <h3>Admissions: International Students</h3>
 
 <p>The M.P.S.: Biotechnology program is available for full-time enrollment.  The program is designed to be completed in five consecutive academic semesters/session (2 years).  International students on F-1 visas are required to maintain a full-time course load (9 credits per semester) during the fall and spring academic semesters. The summer session and a student's last semester are exempt from this full-time course load requirement. International students will be required to take one additional course to maintain the full-time course load for the regular academic semesters.   International students can enroll in courses outside of the program to fulfill the additional elective required.</p>
 
  <h4>Application Dates</h4>
 <p>The application deadline for International Students is <strong>January 1</strong>.  <br>
   Applications received after this deadline may still be reviewed and processed if application documentation is complete.</p>
 
 <h4> Minimum requirements:</h4>
 <ul>
   <li>A bachelor's degree in biology, chemistry/biochemistry, chemical/biochemical engineering, or biotechnology*</li>
   <li>Minimum undergraduate GPA of 3.0 on 4.0 scale</li>
   <li>Two semesters of general chemistry and two semesters of organic chemistry</li>
   <li>GRE scores are required unless your undergraduate degree was earned at an accredited U.S. university. The combined score of the Verbal Reasoning and Quantitative components must be at least 306, with the Verbal Reasoning score being at least 153. Also, the Analytical Writing score should be at least 4.5.</li>
   <li>Test of English as a Foreign Language (TOEFL) Scores: Minimum scores 597 (Written), 247 (computerized), 99 (Internet Based test-iBT).  </li>
   		<ul>
        	<li>Scores must be less than 2 years old.</li>
			<li>Applicants not meeting the program TOEFL (or IELTS) requirements may be eligible for conditional admission.</li>
        </ul>
 </ul>
 <p>*If you have a three-year degree versus a four-year degree, your transcript will be reviewed to determine whether you have met the necessary prerequisites for the program. Eligibility for admission can only be determined upon receipt of a complete application.</p>
 
 
 <h4>Application Process</h4>
 <p>All international applicants are encouraged to apply as early as possible to allow sufficient time to receive all the necessary documents and to process and assess the application.</p>
 <p>International applications are accepted for the Fall  semester only. We encourage all international applicants to send in their documents at least six months prior to the start of the semester applying to.</p>

 <ul>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_new">Apply online</a>
     <ul>
            <li>Select proposed program Biotechnology  &gt;&gt; Biotechnology (PRST MPS)</li>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo; section</li>
       <li>Insert names of references into &ldquo;References&rdquo; section</li>
       <li>Complete residency information to be reviewed for in-state tuition rates</li>
     </ul>
   </li>
   <li style="list-style-type:decimal">Request official transcripts from all post-secondary institutions to be sent to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or emailed to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
  <li style="list-style-type:decimal;">Request for test  scores to be sent by ETS to UMBC (code 5835)</li>
 </ul>
 
 <p><strong>All documents should be mailed to:</strong></p>
 <p>UMBC Graduate School, M.P.S Biotechnology<br>
   1000 Hilltop Circle<br>
   Baltimore, MD 21250 U.S.A.</p>

 <p><strong>Your application packet should include the following documents:</strong></p>
 <ul>
   <li>Completed degree-seeking application (including residency form). Use program code <strong>PRST</strong> and track code <strong>BT</strong>.</li>
   <li>Three letters of recommendation</li>
   <li>Personal statement outlining the student's goals and expectations of the program.  Statement should include applicant’s background, qualifications, career experience, and  any additional pertinent information.</li>
   <li>Official transcript(s) from each college &amp; university attended</li>
   <li>TOEFL scores forwarded to UMBC by ETS - UMBC’s institutional code is 5835</li>
   <li>Application fee ($50 if apply online / $70 if apply by mail)</li>
   <li>If paying by check, the check must be drawn on a bank with a U.S. location. The check should be payable to UMBC.</li>
 </ul>
 
 <p><strong>Please note:</strong> All international students are required to submit TOEFL scores unless you attended a university in an English-speaking country or hold an undergraduate degree from a U.S. institution. IELTS score are accepted in substitution for TOEFL scores.</p>
 
 <h4>Obtaining Immigration Documents</h4>
 <p>If you intend to apply for an F-1 visa and require an I-20 from UMBC, please include the following additional documents with your application.</p>
 <ul>
   <li>Copy of valid passport</li>
   <li>Certified bank statement or bank verification letter from financial sponsor showing a minimum of $29,000 (USD) in available funds.</li>
   <li>Letter from financial sponsor stating they will be responsible for all living expenses and school-related expenses for the duration of your stay at UMBC.</li>
 </ul>
 <p>Send these documents after receipt admission acceptance.  Note: I-20 is processed after formal acceptance into the program.</p>
 <p>All immigration related questions should be addressed to International Education Services by phone at (410) 455-2624 or by e-mail, <a href="mailto:ies@umbc.edu">ies@umbc.edu</a>.</p>
 
 <p>If you require further assistance, please contact <a href="mailto:nancyc@umbc.edu">Nancy Clements</a> or call 410-455-5536. </p>
