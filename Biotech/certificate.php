<h3>Admissions: Post-Baccalaureate Certificate </h3>
<h4>Application Dates</h4>
<p>The application deadline for Spring 2017 is <strong>December 1</strong><br>
  Spring 2017 Classes Start: <strong>January 30, 2017</strong></p>
  
  <p>The application deadline for Fall 2017 is <strong>August 1</strong><br>
  Fall 2017 Classes Start: <strong>August 30, 2017</strong></p>

   <p>
   Students may begin the Biotechnology Management certificate in the fall, spring, and summer. Students may begin the Biochemical Regulatory Engineering certificate in the fall and spring.  Any student interested in applying after the posted deadlines should contact <a href="mailto:nancyc@umbc.edu" target="_blank">Nancy Clements</a>.</p>
 <h4>Admissions Requirements </h4>

 <h5>Post-Baccalaureate Certificate in Professional Studies:  Biotechnology Management</h5>
       <ul>
         <li>A bachelor&rsquo;s degree in any field</li>
         <li>Minimum undergraduate GPA of 3.0 on a 4.0 scale</li>
       </ul>
       
     <h5>Post-Baccalaureate Certificate in Biochemical Regulatory Engineering</h5>

       <ul>
         <li>A bachelor&rsquo;s degree in science or relevant field</li>
         <li>Minimum undergraduate GPA of 3.0 on a 4.0 scale</li>
       </ul>

 
 <h4>Application Process</h4>
 <p> To apply for the Biotechnology Post-Baccalaureate Certificates, complete a UMBC Graduate Application for Post-Baccalaureate Certificate Programs:</p>
 <ul>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_new">Apply online</a>
     <ul>
       <li>Select &ldquo;Application for Post-Baccalaureate Certificate Program&rdquo;       </li>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo;</li>
       <li>Complete residency information to be reviewed for tuition classification</li>
     </ul>
   </li>
   <li style="list-style-type:decimal">Submit unofficial transcripts from all post-secondary institutions to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or email to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
 </ul>
 <p><strong>The following information is required to process your application:</strong></p>
 <ul>
   <li>Completed application including residency form (determines in-state tuition   eligibility)
     <ul>
       <li>Use Program code <strong>PSBT</strong> for Certificate in Professional Studies: Biotechnology Management</li>
       <li>Use Program code <strong>CENR</strong> for Post-Baccalaureate Certificate in Biochemical Regulatory Engineering</li>
     </ul>
   </li>  
   <li>Unoffical transcript(s) from each college university attended</li>
   <li>Application fee ($50 online / $70 paper)</li>
 </ul>
 
 <h4>Mailing Instructions </h4>
 <p>All application documents should be mailed to: </p>
UMBC Graduate School, [Include Certificate Name]<br>
   1000 Hilltop Circle<br>
   Baltimore, MD 21250</p>
   
 <p>If you require further assistance, please contact <a href="mailto:nancyc@umbc.edu">Nancy Clements</a> or call 410-455-5536. </p>
 
