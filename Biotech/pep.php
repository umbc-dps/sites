 <h3>Professional Experience Program in Biotechnology</h3>
 <p><strong><em>Effective Fall 2011</em></strong></p>
 <h4>Program Description</h4>
 <p>The MPS in Biotechnology Program (BTEC) has implemented Professional Experience Program in Biotechnology (PEP) that will be available to all MPS graduate students beginning in the Fall of 2011. The goal of the program is to provide students with the opportunity to have a professional training experience in the biotechnology industry. A student entering the graduate program who is not otherwise engaged or who might opt for a different experience, will have the opportunity to be considered for a position with an organization that will give them industry relevant experiences. The experiences cover the full range of sub disciplines within the biotechnology industry including bench research, manufacturing, regulatory affairs, legal and patenting, business development and marketing and sales.</p>
 <p>PEP is intended to assist students in securing positions with local biotechnology and related industry companies. Positions are not guaranteed. The engagement can be short‐term lasting one or two semesters in length or may be a longer term commitment lasting for the duration of the student&rsquo;s two year participation in the BTEC program. These opportunities, often referred to as internships, will be identified and student placements managed by the BTEC program. However, each student has the choice to pursue alternative experiences on their own but should seek guidance from the program administrators when doing so. Each student will determine which option is best suited to their career aspirations.</p>
 <p>These experiences should give the student the opportunity to gain valuable experience in a biotechnology related area. The experience does not have to be in a research environment but, it should be relevant to the biotechnology industry and flexibility in defining what constitutes a suitable experience will be evaluated on a case by case basis and in consideration to the student&rsquo;s career objectives. The project should engage the student for no less than one semester and up the two years in duration with the expectation that the student would work between 15 and 30 hours per week depending on the circumstances. There are some projects which may be suitable that do not require a full semester to complete. These will be evaluated by the program staff accordingly. Most positions will, but may not always, include compensation. Whether the position is a paid one or not and how much compensation is received will be determined by the sponsoring organization.</p>
 <h4>Program Management</h4>
 <p>The Associate Director or designated Instructor will provide oversight to the experience to ensure that the student has an appropriate professional or practical training experience. Students interested in the program need to apply to participate in the program by submitting a resume and personal statement to the Associate Director. The Associate Director will then determine which opportunity best suits the student and will assist the student in identify an appropriate sponsoring company. Companies participating in the program are not obligated to accept a student candidate, so it is incumbent on the student to present themselves professionally.</p>
 <h4>Duties and Expectations of the Student</h4>
 <p>BTEC students must be in good academic standing with the UMBC Graduate School in order to participate in PEP. To be considered, students must follow the application instructions described in the next section. Prior work experience in the biotechnology field is not a prerequisite for consideration.</p>
 <p>Once a student has been offered a position with an organization, they will be expected to conduct themselves with a high degree of professionalism. Each student should expect to work 15‐30 hours per week, or more, depending the scope of work and sponsoring organization. The scope and schedule of work will be established by the sponsoring organization. The sponsoring organization will be submitting to the Associate Director interim and final evaluations of the student&rsquo;s performance. Each student will be expected to achieve and maintain a satisfactory or higher rating from their supervisor. Failure to maintain a satisfactory rating will result in the student being dropped from the program.</p>
 <p>Students will be expected to submit a summary report describing the work they performed and their accomplishments. This report will be presented as an oral presentation to selected instructor committee no more than one month upon completing the internship and within three months of graduating. A written report will be submitted in conjunction with the oral report. In addition, where appropriate for a given project and when a suitable venue is available, students will present the results of their projects at a poster session. In situations where the student is working under an agreement of confidentiality with a sponsoring organization, written permission should be obtained from the sponsor that the material presented can be disclosed. If disclosure is not possible, alternative arrangements must be made to verify that the student&rsquo;s experience was in keeping with the spirit of the program.</p>
 <p>Academic credit for the professional experience is pending approval from the Graduate School. Students wishing to have the experience appear on their transcript can apply for PRAC98/99 credit through The Shriver Center on campus. This is a zero (0) credit, non tuition bearing course that is offered and administered through The Shriver Center. Students desiring this option should contact The Shriver Center which will provide administrative support to ensure that the requisite documentation (sponsor letter and contract), student performance and academic credit are addressed correctly.</p>
 <h4>Procedure for Application</h4>
 <p>BTEC graduate students interested in being considered for a PEP position should submit an electronic version of a biographical sketch and resume to Dr. Sheldon E. Broedel, Jr. (<a href="mailto:Broedel@umbc.edu">Broedel@umbc.edu</a>). The biographical should be no more than one page in length (3/4&rdquo;margins with 11 pt type). This should describe any professional, academic, workplace, leadership and volunteer experiences you have had. In addition, you should articulate your career aspirations and near‐term plan for achieving those goals and should include a description of the type of professional experience you are seeking. Prepare your resume using the format below.</p>
