 <h3>Post-Baccalaureate Certificate in Biotechnology Management</h3>
 <p>This four-course certificate provides a foundation in management and business processes needed within the biotechnology field.</p>
 
 <h4>Program Highlights:</h4>
 <ul>
   <li>Simplified admissions process </li>
   <li>Four graduate courses (12 credit hours) </li>
   <li>Management and bioregulatory content </li>
   <li>All credits transfer to the M.P.S.:Biotechnology </li>
   <li>Evening classes </li>
 </ul>
 
  <h4>Learning Outcomes</h4>
 <p>By the end of this program, you will be able to:</p>
 <ul>
   <li>Explain the regulatory affairs process for bringing biotechnology products to market </li>
   <li>Practice and apply interpersonal skills in team building and project leadership </li>
   <li>Use financial tools to monitor and manage biotechnology projects </li>
   <li>Discuss legal and ethical issues in the life sciences <br>
   </li>
 </ul>
 

 <h4>Certificate Curriculum</h4>
 <p>This certificate requires the following four courses:</p>
 <ul>
   <li>BTEC 665: <a href="courses.php#665">Management, Leadership and Communication</a> [3] </li>
   <li>BTEC 670: <a href="courses.php#670">Legal &amp; Ethical Issues in the Science Professions</a> [3]</li>
   <li>BTEC 680: <a href="courses.php#680">Financial Management for Science Professionals</a> [3]</li>
   <li>BTEC 685: <a href="courses.php#685">Project Management Fundamentals</a> [3]</li>
 </ul>
   
 
