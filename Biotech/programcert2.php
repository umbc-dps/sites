 <h3>Post-Baccalaureate Certificate in Biochemical Regulatory Engineering</h3>
 <p>Taking a new biotechnology product through a lengthy and rigorous FDA approval process can be a daunting experience. But knowing the internal framework of the FDA review and marketing authorization process can be a significant advantage. UMBC's post baccalaureate program in biochemical regulatory engineering guides students through the ins and outs of the FDA process and explores the numerous considerations that can help ensure product approval and sustainable regulatory compliance status. Professionals from leading bioscience companies and government agency organizations participate in this post-baccalaureate certificate program. Guest lecturers also from private entities and public organizations, with direct hands-on experience on the various topics, are featured frequently in all the courses of the program.</p>
 
 <h4>Program Highlights:</h4>
 <ul>
   <li>Simplified admission process </li>
   <li>Four graduate courses (12 credit hours) </li>
   <li>Evening classes offered on-campus </li>
   <li>Frequent guest lectures by experts from the private and public sectors </li>
   <li>Up-to-date information on regulatory compliance topics </li>
 </ul>
 
  <h4>Learning Outcomes</h4>
 <p>By the end of this program, you will be able to:</p>
 <ul>
   <li>Understand the key steps for obtaining FDA approval of a biotechnology product</li>
   <li>Achieve a working knowledge of CGMP compliance, quality control, validation, and design of GMP biotech facilities</li>
   <li>Prepare key CGMP documents, such as batch records, SOPs, validation protocols</li>
   <li>Develop a QC package for key classes of biotech products
   </li>
 </ul>
 

 <h4>Certificate Curriculum</h4>
 <p>This certificate requires four courses. The courses may be taken in any sequence. However, it is recommended that students take ENCH 660: Regulatory Issues in Biotechnogy first.</p>
 <ul>
   <li>BTEC/ENCH 660: <a href="http://www.umbc.edu/biotech/courses.html#660">Regulatory Issues in Biotechnology</a> [3]</li>
   <li>BTEC/ENCH 662: <a href="http://www.umbc.edu/biotech/courses.html#662">Good Manufacturing Practices for Bioprocesses</a> [3]</li>
   <li>ENCH 664: <a href="http://www.umbc.edu/engm/courses.html#bi">Quality Control and Quality Assurance for Biotechnology Products</a> [3]</li>
   <li>ENCH 666: <a href="http://www.umbc.edu/courses.html#bi">Design, Construction, and Validation of GMP Biotechnology Facilities</a> [3]</li>
 </ul>
   
 
