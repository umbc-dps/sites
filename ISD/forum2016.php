<p>There is so much to learn about learning. Where does one begin to understand how to put it all together? The challenge is that there are so many pitfalls, and so many ways to waste time and harm learning. There are also so many potent learning factors; brilliant methods to inspire and engage.</p>
<p>In this stimulating session, Dr. Thalheimer will challenge us in a rapid-fire quiz show, uncovering the many pitfalls around learning. He will present using humor, energy, and a provocative edge.</p>

<h4>How the heck can one know what is true? </h4>
  <p>Join us for the ISD Now Forum and hear from Dr. Will Thalheimer, a noted research-to-practice expert.</p>
  
  
<h4>Join us for this free event to learn about learning as it relates to:</h4>

<ul>
  <li>Neuroscience</li>
  <li>Remembering</li>
  <li>Learner-centric design</li>
  <li>On-the-job performance</li>
  <li>Powerful learning approaches</li>
</ul>

<p>After this research-based presentation, Dr. Thalheimer will share work from his recently published book, Performance-Focused Smile Sheets: A Radical Rethinking of a Dangerous Art Form—the book Dr. Karl Kapp called, “Quite simply, the BEST book on smile sheet creation and utilization, Period!” If you thought smile sheets would always be inadequate, join us in seeing how they can be radically improved to help you and your organization get much more meaningful feedback.</p>




