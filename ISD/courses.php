          <h3>Course Descriptions</h3>


          <p><strong>Note:</strong> Not all courses are offered every semester, and new courses may be added at any time. Check the <a href="schedule">schedule of classes</a>, for the latest offerings. </p>



 <p>+ If EDUC 602 is listed as a prerequisite, it must be an ISD section of EDUC 602.</p>
 
 <div class="accordionheader">
            <h4><a href="#EDUC602" id="EDUC602"></a><strong>EDUC 602: Instructional Systems Development I</strong> [3] Online   (Fall, Spring and Summer)<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>
   This course includes the elements of   analysis, design, development, implementation and evaluation. An emphasis is   placed on micro-level design issues including analysis, design and evaluation.   Learners work through the ISD process to assemble a training or education   project that is ready for implementation. A design plan and lesson plan is   constructed to allow learners real-world experience in the ISD process.  <EM>The online section of this course is taught using an asynchronous delivery   format.</EM> </p>
 <p><STRONG>Prerequisite:</STRONG> Taken in first semester and consent of department</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC603" id="EDUC603"></a><strong>EDUC 603: Instructional Systems Development II</strong> [3]</STRONG> Online (Fall, Spring, Summer) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course focuses on the assessment, design and evaluation stages of the ADDIE model of instructional systems design.  It promotes a sequential process through which instructional designers define performance problems, analyze performance gaps, identify root causes, propose training solutions, design appropriate learning events and craft evaluation strategies to measure the efficacy of their recommended approaches. Students will prepare multiple project documents throughout this real-world, real-time applied learning event, culminating in a final project portfolio of professional-grade ISD work. <EM>The online section of this course is taught using an asynchronous delivery   format.</EM></p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 (ISD Section) and consent of   department</p>
 </div>


<div class="accordionheader">
            <h4><a href="#EDUC605" id="EDUC605"></a><strong>EDUC 605: Adult Learner</strong> [3] Online (Fall, Spring and Summer) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course covers adult learning theories, instructional   strategies and other aspects of adult learning and human performance   improvement, with a special emphasis on the implications for design, delivery,   evaluation and the application of learning. Other selected topics include   alternative methods to traditional learning/training, and the characteristics,   motivation and learning preferences of adult learners.  <EM>The online section   of this course is taught using an asynchronous delivery   format.</EM> <BR>
   <BR>
   <STRONG>Prerequisite: </STRONG>EDUC 602 and consent   of department</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC641" id="EDUC641"></a><strong>EDUC 641: The Online Classroom</strong> [3] Online (Summer only) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>In this course, various aspects of computer-mediated   communication and instruction are examined. A broad range of distance education   issues and applications are explored from a theoretical and practical   standpoint.  <EM>This course is taught using an asynchronous delivery   format.</EM></p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 recommended and consent   of department.</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC643" id="EDUC643"></a><strong>EDUC 643: Principles of Distance Education</strong> [3]</STRONG> Online (Spring only)<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course provides students with a foundation in history,   theory, organization, technologies and instructional procedures used in distance   education. Students gain experience with several distance-education delivery   systems.  <EM>This course is taught using an asynchronous delivery   format.</EM></p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 recommended and consent   of department.</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC647" id="EDUC647"></a><strong>EDUC 647: Corporate Distance Training</strong> [3]</STRONG> Online (Fall only) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course identifies how to maximize utilization of   organizational technology to deliver distance training. Students will examine   case studies to explore current practices and future trends in business   application of distance learning.  <EM>This course is taught using an   asynchronous delivery format.  There will be some optional synchronous sessions   that will be recorded for later viewing.</EM> </p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 and consent of department</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC648" id="EDUC648"></a><strong>EDUC 648: Issues in Consulting for Training and   Development</strong> [3]</STRONG> Online starting spring 2016(Spring only)<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>Students examine the various roles, functions, skills and   knowledge needed of internal and external consultants to help solve human   performance problems.</p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 and consent of department.</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC649" id="EDUC649"></a><strong>EDUC 649: Best Practices in Training, Development   and Performance</strong> [3]</STRONG> Online (Currently Not Offered)<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course provides participants with awareness of strategies,   practices, and ideas concerning training and development issues. It is designed   for students to explore &ldquo;real world&rdquo; applications for programs, services and   operations in a training environment.  Professionals in the training field will   be featured as guest speakers. This course is   offered in the summer of odd numbered years (e.g. 2011) <EM>This course is   taught using a synchronous online delivery format.</EM></p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 and consent of department.</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC671" id="EDUC671"></a><strong>EDUC 671: Principles of Training and Development</strong> [3] Online (Fall and Spring) and On-Campus (Summer only) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course examines key principles relevant to training and   development. They include: the role of training in an organization, adult   learning theory, needs assessment, training methodology, organizational support,   resources and constraints, evaluation of training, and managing the training   function. Issues that influence training implementation, such as ethics and   interpretation, are also addressed.  <EM>The online section of this course is   taught using an asynchronous delivery format.</EM></p>
 <p><STRONG>Prerequisite: </STRONG> EDUC 602 and consent of   department</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC681" id="EDUC681"></a><strong>EDUC  681: Survey of Instructional Technology Applications</strong> [3] Online (Fall,  Spring and Summer) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>First in a series of three Instructional Technology courses,   this course exposes students to a range of multi-media tools used to design   interactive instruction including computer-based training (CBT) and web-based   training (WBT) products. Through analysis and hands-on experience with each   tool, the students identify the properties of each tool, describe the strengths   and limitations and evaluate their application for different learning events.   This course formerly was EDUC 640. <em><strong>This course is designed for PC and Windows based software. It does not support MAC users. </strong>The online section of this course   is taught using a synchronous delivery format.</EM></p>
 <p>This course requires the purchase of Captivate software. The current semester's requirement may be found on the <a href="textbooks">Textbooks and Materials</a> page.</p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 and consent of   department</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC682" id="EDUC682"></a><strong>EDUC 682: Instructional Technology Design and   Development</strong> [3]</STRONG> Online (Fall and Spring)<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>Second in the series of Instructional Technology courses, this   course continues a student's development process as he/she applies ISD   principles to the development and delivery of instruction using a computer-based   training model. This course formerly was EDUC 610. <em><strong>This course is designed for PC and Windows based software. It does not support MAC users. </strong>This course is   taught using a synchronous online delivery format.</EM></p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 640 or 681 required and   consent of department</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC683" id="EDUC683"></a><strong>EDUC 683: Multi-Media Project   Management</strong> [3]</STRONG> Online (Fall and Spring)<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>The last of three Instructional Technology courses, this   lab-based course allows students to apply project development and multi-media   design skills by completing an industry-based design project. The course is   divided into four or more discrete modules that teach specific multi-media   development applications required to execute and complete the design project. By   the conclusion of the course, each student possesses comprehensive knowledge of   the applications and how to apply them to a multi-media design project.   This course formerly was EDUC 620. <em><strong>This course is designed for PC and Windows based software. It does not support MAC users. </strong>This course is taught using an   asynchronous delivery format.  There will be some synchronous sessions that will   be recorded for later viewing.</EM></p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 640 or 681 required and   consent of department<BR>
 </p></div>


<div class="accordionheader">
<h4><a href="#EDUC689" id="EDUC689"></a></a><strong>EDUC 689: Special Topics: Connecting Instructional Systems Development (ISD) and Human Performance Improvement (HPI)</strong> [3] Online (Fall Only) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course will address the role that Human Performance Improvement (HPI) plays in the Instructional Systems Development (ISD) world. The course is designed for students to explore the field of HPI and to identify the skills and knowledge inherent in HPI profession that may also benefit students in ISD or other professions. Professionals from the field of HPI will be guest speakers for the synchronous lectures of this course offering. </p>
 <p><strong>Prerequisite:</strong> EDUC 602 and consent of department</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC689-1" id="EDUC689-1"></a><strong>EDUC 689 Special Topics: ISD for Informal Learning</strong> [3] Online (Currently Not Offered)<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course considers multiple formats for providing extended learning opportunities beyond the classroom or training center.  Throughout the semester, students will examine multiple informal learning tools and determine how best to identify, foster, and sustain a professional learning network.  Students will also discuss and debate the relevance of social learning as a medium for ongoing professional development, as well as a strategic learning partner for business. <EM>This course is taught using an asynchronous delivery   format.</EM> </p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 and consent of department</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC689-2" id="EDUC689-2"></a><strong>EDUC 689 Special Topics: Managing Training and Performance Function  </strong>[3]</STRONG> Online  (Currently Not Offered) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course will address the management of training, workplace learning and performance. The course is designed with multiple real world applications and skills required in management positions. Emphasis will be on applied projects and practical approaches used successfully by professionals from the field of training and performance.  This course is   offered in the summer of even numbered years (e.g. 2012) <EM>This course is   taught using a synchronous online delivery format.</EM></p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 and consent of   department</p></div>

<div class="accordionheader">
            <h4><a href="#EDUC689-3" id="EDUC689-3"></a><strong>EDUC 689 Special Topics: Managing Your Career</strong> [3] Online (Winter session only) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course will provide students with tools, strategies and  techniques to manage their career successfully.    The main objective of the course is to  prepare students to advance their career goals.   These goals may include getting a job, a promotion, or seeking projects/opportunities  that enhance your career.  Additionally,  students will assess their interests, knowledge, skills, abilities and  competencies to for specific job opportunities in the field of workplace  learning.  Students will receive  individual career counseling and coaching in their job search process<em>.</em></p>
 <p>This course will also cover: Managing career paths and transitions; career challenges faced by workplace learning professionals; working with recruiters, HR and hiring managers; marketing yourself to employers and clients; forecasting future jobs and competencies; resume and cover letter development; interviewing strategies and practice; and performance appraisal strategies.</p>
 <p><strong>Prerequisite</strong>: EDUC 602 and consent of department</p>
</div>

<div class="accordionheader">
            <h4><a href="#EDUC689-4" id="EDUC689-4"></a><strong>EDUC 689 Special Topics: Portfolio Development &amp; Talent Management</strong> [3]</strong> Online (Fall and Summer) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course will cover the design and development of career portfolios for professionals in training, instructional design, instructional technology, eLearning and performance improvement fields. It will also address how to use a portfolio as career  development and evaluation tool for individuals in managing their own careers, and how organizations can use portfolios in the talent management arena to recruit, asses, develop and evaluate current employees and potential employees.</p>
 <p>Students will create a professional portfolio and conduct an actual job search incorporating their portfolios. <em><strong>This course is taught using an asynchronous delivery format.</strong></em></p>
 <p><strong>Prerequisite:</strong> EDUC 602 and consent of department </p></div>

<div class="accordionheader">
            <h4><a href="#EDUC689-5" id="EDUC689-5"></a><strong>EDUC 689: Special Topics: Project Management</strong> [3] Hybrid On-Campus (Currently Not Offered)<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>In this course students examine the fundamental project   management principles within the context of the instructional systems   development model. Students apply the project management principles to solve   training and development case studies and simulations. <em><strong>This course is taught using a blended/ hybrid method of delivery that uses in-person class meetings and some online asynchronous sessions.</strong></em></p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 602 and consent of department</p></div>
 
 
 <div class="accordionheader">
<h4><a href="#EDUC689-6" id="EDUC689-6"></a></a><strong>EDUC 689: Special Topics: Design for How People Learn</strong> [3] Online (Fall Only) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course explores how people learn and the ways that Instructional Designers may integrate that knowledge into the practice of design. This is an inquiry-based course that focuses on three primary questions. Participants emerge from the course having explored, and deeply reflected on, available research from which they will derive meaningful and sustainable personal learning that is fully incorporated into their instructional design practice.</p>
 <p><strong>Prerequisite:</strong> EDUC 602 and consent of department</p></div>



<div class="accordionheader">
            <h4><a href="#EDUC771" id="EDUC771"></a><strong>EDUC 771: Research Designs in Education</strong> [3]</STRONG> Online (Fall and Spring)<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>
   This applied   research course is designed for Master&rsquo;s students in the ISD Training Systems   graduate program. The course focuses on the process of applying research design   models and methodologies to the analysis of performance problems or   opportunities for organizations, teams or individual workers. Students will   develop and apply a variety of systematic measurement and evaluation tools,   including extant (existing) data research, surveys, benchmarking, and focus   groups while conducting performance and root cause analysis in the context of   needs assessments or front end analysis. A statistical analysis software package   will be used to analyze and report research data.  <EM>This course is taught   using an asynchronous delivery format.</EM></p>
 <p><STRONG>Prerequisites: </STRONG>EDUC 602, 603, 605 and/or   consent of department.</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC792" id="EDUC792"></a><strong>EDUC 792T: ISD Internship</strong> [3] Online (Fall, Spring and Summer) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>A field-oriented experience in which the student designs and   implements a system of instruction, an analysis technique or evaluation design   in a setting consistent with the student's professional preparation.  <EM>This   course is taught using an asynchronous delivery format.</EM></p>
 <p><STRONG>Prerequisite:</STRONG> Full MA program admission status, completion   of 10 out of 12 program courses and consent of department.
 </p></div>


<div class="accordionheader">
            <h4><a href="#EDUC794" id="EDUC794"></a><strong>EDUC 794: ISD Project Seminar</strong> [3] Online (Fall, Spring and Summer) <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course provides the advanced graduate student in the ISD   program the opportunity to analyze an educational or training problem, and apply   the complete instructional systems development process to the design and   development of a comprehensive instructional program to meet the needs   determined by the analysis. The student is expected to design a project plan and   follow the plan as he or she designs and develops all the instructional material   necessary to support delivery of the comprehensive instructional system. The   instructional system is expected to include an evaluation component and reflect   the proper application of ISD principles in the overall design.  <EM>This course   is taught using an asynchronous delivery format.</EM></p>
 <p><STRONG>Prerequisite: </STRONG>EDUC 792T, completion of 10 out of 12 program   courses and consent of department.</p></div>


<div class="accordionheader">
            <h4><a href="#EDUC796" id="EDUC796"></a><strong>EDUC 796: Human Performance Technology</strong> [3]</STRONG> On-Campus (Currently Not Offered)<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course focuses on a synergistic examination of the current   issues related to the design, development, delivery and evaluation of training   systems for employee training in industry and business. Corporate organization,   financial, social and political factors are analyzed in terms of their effect   upon the efficacy and efficiency of such training programs. The students are   expected to research such factors and their synergistic effect upon   corporations' internal efforts to respond to training needs.</p>
 <p><STRONG>Prerequisite:</STRONG> EDUC 602 and consent of   department. </p>
</div>
