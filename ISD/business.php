 <h3>Business Services</h3>
 <p><strong>Training and Consulting Services</strong></p>
 <p>UMBC&rsquo;s Instructional Systems Development &amp; Training Systems program provides a variety of professional services to help organizations become more productive, efficient and competitive. The Instructional Systems Development (ISD) program has been in existence for nearly 40 years and has developed a reputation of quality. We design, develop and deliver training programs and provide consulting services to organizations in business, government, education, non-profit sector, in the following areas:</p>
 <ul>
   <li>eLearning design and development</li>
   <li>Instructional design</li>
   <li>Instructional Technology</li>
   <li>Training, including train the trainer</li>
   <li>Performance improvement</li>
 </ul>
 <p><strong>Job Postings</strong></p>
 <p>If your organization would like to get the word out on current job postings in the field of training, instructional design, instructional technology, eLearning and other workplace learning employment opportunities, we would be pleased to post them on our site. In addition to listing your announcement on our site, we also email job postings to our listserv subscribers. Employers that want to post jobs on our website and listserv should contact Greg Williams at <a href="mailto:gregw@umbc.edu">gregw@umbc.edu</a>.</p>
 <p><strong>Internships</strong></p>
 <p>As part of UMBC's ISD Master's program, students are required to complete an internship. The internship course is taken once students have completed 21 credit hours and is a field-oriented experience in which the student designs and implements a system of instruction, an analysis technique or evaluation design in a setting consistent with the student's professional preparation. If your organization is interested in participating in our internship process, please contact us to discuss further.</p>
 <p><strong>ISD Now Forum</strong></p>
 <p>The ISD Now Forum is an event, free to the community, that provides an opportunity for individuals working or interested in training, education, business, government and the non-profit sector to share ideas and dialogue on topics of importance. Part of UMBC's mission is to provide service. We take pride in our partnerships with employers and other organizations in the community, and are able to give back with events like the Training Forum. In many instances, we have panels of experts discussing topics related to business, organizational productivity and workplace learning and performance. Other topics center around issues related to human performance improvement, training and employee development, improving individual and organizational effectiveness and other related subjects. We encourage members of the business community to take part in this great networking opportunity. For more information on the Training Forums, please <a href="forum">click here</a>.</p>
 <p>For more information on any of these opportunities, please contact Dr. Greg Williams, ISD Program Director, at <a href="mailto:gregw@umbc.edu">gregw@umbc.edu</a> or call him at 443-543-5447.</p>
