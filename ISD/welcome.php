 <p>Welcome to UMBC. The Instructional Systems Development (ISD) Program is happy to have you as part of our community. Below are links that will familiarize you with UMBC and ISD.</p>
 <h4>About myUMBC</h4>
 <p>myUMBC is UMBC&rsquo;s electronic student resource site for a number of services you will use as a UMBC student. Services including paying your bill, checking your student account, accessing your e-mail, managing your e-mail account and logging into Blackboard may be found.</p>
 <ul>
   <li><a href="https://webadmin.umbc.edu/admin/User/Create">Creating your myUMBC account</a></li>
 </ul>
 <p>Once you have created your myUMBC account, you can log into myUMBC anywhere, anytime.  All new students should become familiar with the many services and resources available on the site.</p>
 <h4>Registration Information</h4>
 <p>When a student is admitted the department will e-mail the advising and registration instructions. If you have misplaced your e-mail please contact Renee Eisenhuth, program coordinator, at <a href="mailto:reisen@umbc.edu">reisen@umbc.edu</a>.</p>
 <p>After registering online, be sure to review the <a href="textbooks">Textbooks and Materials</a> page for your course(s). (More than one semester may be listed at a time. Please make sure you are viewing the correct semester.)</p>
 <h4>Academic Integrity Web-Based Tutorial</h4>
 <p>New graduate students admitted into a Master&rsquo;s or certificate program must complete the <a href="http://catalog.umbc.edu/content.php?catoid=14&navoid=718#graduate-school-academic-integrity-tutorial" target="_blank">Academic Integrity Web-Based Tutorial</a>.  The Tutorial must be completed by the second week of the semester you were admitted. Save your completion page at the end.</p>
 <h4>Student Business Services</h4>
 <p><a href="http://sbs.umbc.edu">Student Business Services</a> (SBS) is the office that bills students, collects tuition and fees and issues student refunds. They process student payments, tuition remission, and post payments to student accounts from outside agencies.</p>
 <p>Review their website for helpful information including E-billing information, how to make online payments, and tuition information including the drop and withdraw refund schedule.</p>
 <h4>Registrar&rsquo;s Office</h4>
 <p>The <a href="http://registrar.umbc.edu">Registrar&rsquo;s Office</a> provides services related to academic programs, registration and records. They will post the university academic calendar for each semester. You may also order official transcripts and enrollment verifications through links on their website.</p>
 <h4>Remind</h4>
 <p>The ISD program introduced a new way for students to stay in touch. Remind allows the ISD program to have text messages sent directly to a student&rsquo;s cell phone. We will send brief messages about deadlines, class schedules, graduation and other student services.It is not required to sign up for Remind.</p>
 <p>To subscribe:  Send the text message &ldquo;@umbc1&rdquo; to 443-577-4993, to receive messages via text.</p>
 <p>You may opt-out of messages at any time by replying &ldquo;unsubscribe @umbc1&rdquo;.</p>


