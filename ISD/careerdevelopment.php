 <h3>Career Development Services</h3>

<p>The ISD-Training Systems graduate program offers one-on-one career development services to our current and prospective students.</p>
<h4>Services include:</h4>
<ul>
  <li>Personal career counseling sessions</li>
  <li>Information on current trends related to jobs in the ISD field</li>
  <li>Opportunities to connect previous experience with new education</li>
  <li>Resume reviews and edits</li>
  <li>Job search strategies and guidance</li>
  <li>Job interview and salary negotiation preparation</li>
  <li>Confidence in career transitions <br>
  </li>
</ul>
<h4>Appointments</h4>
<p>To accommodate busy working professionals, these services can be provided in-person, over the phone or via E-mail. To make an appointment with Sarah Gardenghi, our career counselor in residence, E-mail: <a href="mailto:gardengh@umbc.edu">gardengh@umbc.edu</a> or call 410-455-6839. Career development is a free service provided by UMBC to help successfully connect your education to future careers in ISD.</p>
<h4>Eligibility for Services</h4>
<p>Current students in the ISD–Training Systems graduate program and program alumni (Master&rsquo;s or certificate) are eligible to receive services. Prospective students may also be eligible with the approval of the Program Director, Dr. Greg Williams. Contact him at <a href="mailto:gregw@umbc.edu">gregw@umbc.edu</a> or 443-543-5447.</p>

<h4>Career Counselor</h4>
<p><img src="images/SarahGardenghi.PNG" align="left" style="padding-right:10px">Sarah Gardenghi can provide services in-person,
over the phone or via email.</p>

<p><strong>Phone:</strong> 410-455-6839  <br>
  <strong>Email:</strong> <a href="mailto:gardengh@umbc.edu">gardengh@umbc.edu</a></p>
  
<p>Sarah brings ten  years of experience  presenting professional  training seminars on  career development  and communication  skills. She counsels  individuals who are  career changing,  displaced, re-entering  or advancing in their  current field. Sarah  delivers seminars and  provides online career  content specifically for ISD students covering  resume/portfolio writing, job searching, Internet  use and interviewing strategies.</p>

<p>She currently works for UMBC Division of  Professional Studies advising and counseling  students, and meets regularly with ISD students  for personal career counseling. She earned  her undergraduate degree in Psychology and  Communication from Concordia College, and  her Master&rsquo;s degree in Communications from  Colorado State University. 
