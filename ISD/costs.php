      <h4>Application Fee</h4>


      <p>$50 - online; $70 - paper</p>


      <h4>Matriculation Fee</h4>


      <p>A one-time matriculation fee will be paid during the first two semesters of enrollment for Master's degree-seeking students, and the fee will be posted to student accounts automatically: $100 will be charged the first semester; $100 will be charged the second semester. Certificate and non-degree students will not be charged a matriculation fee or orientation fee. The certificate diploma fee will continue to be charged when the application for certificate diploma is submitted to the Graduate School. For more information on the matriculation fee, please <a href="http://sbs.umbc.edu/tuition-info/mandatory-fees/" target="_blank">click here</a>.</p>
      
      <h4>Graduate Tuition and Fees</h4>
      <p>Graduate students are charged a per credit hour rate for tuition and other fees. UMBC tuition and fees may be paid by check, credit card, cash or employer-provided benefits. Some students may qualify for student loans, graduate assistantships or Veteran's benefits. For more information on graduate tuition and fees, <a href="http://gradschool.umbc.edu/funding/tuition/" target="_blank">click here</a>.     </p>
      <h4>Financial Aid, Loans, Fellowships, and Veterans Affairs</h4>
           <p>If you are applying for Financial Aid, to qualify, you MUST apply and be admitted as a Certificate or Master's candidate. If you miss the deadline for either program, you must wait until the next deadline to apply.</p>
           
      <p>The Office of Financial Aid maintains and coordinates a variety of scholarships, grants, loans, student employment opportunities and veteran's benefits. Students wishing to be considered for financial aid resources must complete and submit a "Free Application for Federal Student Aid" (FAFSA) to the U.S. Department of Education (listing UMBC's Federal School Code: 002105). The FAFSA may be completed on the web at <a href="http://www.fafsa.ed.gov" target="_blank">http://www.fafsa.ed.gov/</a>.</p>
      <p>Information about federally subsidized and unsubsidized loans, the federal work-study program and other forms of need-based assistance is available from the <a href="http://www.umbc.edu/financialaid" target="_blank">UMBC Office of Financial Aid</a>.</p>
      
      
       <h4>Graduate Assistantships</h4>
      <p>The Graduate School has graduate assistantships available to qualified, full-time, degree-seeking students. The financial support - stipend, tuition remission, and benefits - is part of the University's commitment to the success of our graduate students. </p>
      <p>Graduate assistantships or scholarships are not available through the ISD program.</p>
      <p>Detailed information about assistantships and their responsibilities, benefits and stipend levels is available in the <a href="http://gradschool.umbc.edu/funding/assistantships/handbook">Graduate Assistant Handbook</a>.</p>
      
      
