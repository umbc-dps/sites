 <h3>Admissions: Master's Degree </h3>
 <h4>Application Dates</h4>
 <p>For best consideration, the priority deadline for Fall is <strong>June 1</strong>.  </p>
 <p>For best consideration, the priority deadline for Spring is <strong>November 1</strong>.</p>
 <p>For best consideration, the priority deadline for Summer is <strong>March 1</strong>.</p>
   <p>Non-Degree application deadlines are one week prior to the start of the semester.</p>
 <h4>Admissions Requirements:</h4>
  <UL>
   <li>Bachelor's degree: Earned from a regionally accredited college or university or an equivalent degree from a comparative foreign institution.</li>
   <LI>Minimum undergraduate GPA of 3.0 on a 4.0 scale.
   <LI>GRE scores are not required</LI>
   <LI>3 letters of recommendation</LI>
   <li>International applicants should refer to the <a href="international">International Admission Page</a> for special admission information</li>
  </UL>
  

  <h4>Optional Interview:</h4>
  <p>An in-person or a phone interview is not required, but prospective students may have questions concerning the program. The purpose of the interview is to address any questions or concerns that you may have. It also is an opportunity to discuss your career plans, and to see how the program can help you reach your goals. Dr. Greg Williams, Graduate Program Director, is available to talk with prospective students. Dr. Greg Williams can be reached at <a href="mailto:gregw@umbc.edu" target="_blank">gregw@umbc.edu</a>.</p>
  
  
  <h4>Application Process:</h4>
 <p>Students who miss the <strong>Master&rsquo;s</strong> application deadline may still take courses as a Certificate or non-degree student and apply for formal admission the following semester. Students who miss the <strong>Certificate</strong> application deadline may still take courses as a non-degree student and apply for formal admission the following semester.</p>


