 <p>
   <strong>J. Marvin Cook, Ph.D., Associate Professor Emeritus,</strong> was the first Director/Coordinator of the ISD-Training Systems Graduate Program at the University of Maryland, Baltimore County.</p>
   
   <p>   Dr. Cook joined the faculty of the Department of Education at UMBC in 1969 and retired in 1992. In 1993, he was designated Associate Professor Emeritus in recognition of his extensive contributions to the Department&rsquo;s popular Graduate Program in Instructional Systems. Upon his retirement, the Graduate Program in Instructional Systems Development (ISD) was the largest graduate program at UMBC.</p>
   
  <p>
   Dr. Cook worked diligently to assure that the graduates of the ISD- Training Systems Graduate Program were in high demand by business, industry, higher education, government and health care organizations. To assure that the graduates were highly qualified, he utilized his research and consulting experience to continually update the content of the ISD Graduate Program.</p>
   
   <p>
   Dr. Cook authored 10 staff development books and manuals for trainers, educators and managers plus numerous articles in the field of education and training. Internationally recognized for his work in ISD, he was in high demand as a national conference presenter of the ISD process and its application in a wide range of professional fields. He utilized each opportunity to describe how UMBC&rsquo;s ISD- Training Systems Graduate Program was designed and successfully implemented.</p>
