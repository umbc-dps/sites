   <h4><a id="exam"></a>Comprehensive Exam (Required for Master's students admitted prior to fall 2012)</h4>
   </strong>The purpose of the comprehensive examination in the master&rsquo;s program is to show a mastery of knowledge within the field of training and development. You will be asked to problem solve, analyze and evaluate a case in a training and development context, and synthesize for the reader your understanding through recommendations about the case. Part of the task is to select an appropriate (theoretical or research based) framework from which to write an analysis and evaluation of the case. An additional focus needs to be on support for your judgments and what you say in response to the case by citing the research or theoretical models found in the literature in the training and development field. The response needs to go beyond the foundations you have learned in your course work to demonstrate your knowledge of a broader, more comprehensive scope of reading and subsequent reflection by you in this field.<br>
   <br>
   This is a take-home, open book exam that must be completed over a weekend period (9 a.m. Friday - 6p.m. Monday). Your response to the question(s) is limited to ten (10) double-spaced typed (printed) pages, including your bibliography. APA style is required. <br>
   <br>
   <h4>Exam Dates</h4>
Normally, the fall exam occurs on a weekend either late October/ early November; the spring exam occurs on a weekend either March/ early April; and the summer exam occurs on a weekend in late June. <br>
