<div class="accordionbody"><a href="" id=""></a>The Final Project Seminar course (EDUC 794) is a capstone   course where students complete a comprehensive project that ties together things   that they have learned throughout the program. The internship course (EDUC 792T)   is an opportunity for students to gain either initial or additional experience   with a live client organization. This is done by completing a   project that will be delivered to the client at the end of the course. The   project or assignment(s) for the internship do not have to be as comprehensive   in scope as the Final Project Seminar course. For further details, check the   syllabus for each course.&nbsp; </p>
