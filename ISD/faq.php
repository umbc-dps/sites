<div class="accordionheader">
          <h4><a href="" id=""></a>How do I proceed through a UMBC online course?<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>Once you are accepted to the program, you will receive a UMBC e-mail account. Near the beginning of the semester, most instructors send their students a &ldquo;Welcome&rdquo; e-mail message, notifying them that the course has been activated on Blackboard.  You can then go to Blackboard, select your course and read the syllabus thoroughly. From that point, you log on regularly (several times a week) to check announcements from the instructor, post to the Discussion Board, respond to feedback from your classmates and the instructor, and submit assignments.<br>
 </p>
 <p>You can communicate with your instructor through the discussion board, e-mail or by phone. Some instructors also have online office hours.</p>
 <p>Also, most assignments are turned in by posting them to the &ldquo;Discussion Board&rdquo; or by emailing them to the instructor. In many classes, assignments are always due the same day and time every week (example: Wednesday by 11:59 p.m.).</p>
 </div>
 
 <div class="accordionheader">
   <h4><a href="" id="a2"></a>Are Online Classes Easier than Face-to-Face? <i class="fa fa-angle-down"></i></h4>
 </div>
 <div class="accordionbody1">
   <p>No. Online classes take as much or more time as a class you might   take on campus. Online courses are never out of session. Students are   expected to log on and contribute to discussion several times a week.   That also means reading messages every week from the instructor and   other students.</p>
   <p>There are no conclusive studies comparing the two methods, but   initial studies indicate that online students perform as well or better   than their in-class counterparts. </p>
   <p>Early single-institution studies and anecdotal evidence suggest that   online learning at least holds its own with the face-to-face classroom.   For some, in fact, online learning may result in better comprehension. </p>
   <p>For any course (including in-person), learning ultimately depends on   the quality of the instructor, course material and participation of the   students. The method, too, is important. Courses that encourage online   discussion and interaction between students, their peers and the   instructor typically demonstrate higher levels of participation than   traditional courses.</p>
 </div>


