<div class="accordionheader"><h4>I see that the Master's program has a required internship.&nbsp; How   does that work?<i class="fa fa-angle-down"></i></h4>
</div>



<div class="accordionbody"><a href="" id=""></a>This Master's program emphasizes applied   learning.&nbsp;It is important for students to be able to apply what they have   learned in a professional setting.&nbsp;Given that, the internship can be completed   in two ways.&nbsp;The first is for the student to complete a traditional internship   where they report to and work at the sponsoring organization's workplace.&nbsp;The   second way to complete the internship is to choose an internship project that   can be completed using more of an independent consulting approach.&nbsp;Using this   approach, students would meet with clients to map out the goals and specific   deliverables for the internship.&nbsp;Students may have periodic meetings with the   client organization, but would not be required to perform the work   on-site.&nbsp;Students will be required to draft an internship proposal which   describes what they will do, the project deliverables and method of   evaluation.&nbsp;The internship proposal is subject to approval by the client   organization and Dr. Williams. </p>
