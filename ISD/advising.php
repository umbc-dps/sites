      <h3>Advising</h3>

 <p>Every semester, each student is required to have academic advising.  Advising is designed to make sure each student is completing the program in a timely and efficient manner.  It is also required before a student is given authorization to register for the next semester.</p>
 <p>Dr. Chuck Hodell is the Academic Advisor for all students enrolled in the ISD Graduate Programs.  He can be contacted at <a href="mailto:hodell@umbc.edu">hodell@umbc.edu</a>.  Students who are unsure which courses to take should utilize the advising options to discuss some of the following:</p>
 <ul>
   <li>Course choice and course sequence</li>
   <li>Academic performance issues and student progress</li>
   <li>Career development plans</li>
   <li>Comprehensive exam planning and preparation</li>
 </ul>
 <p><strong>Once students know which course(s) they want to take they should use the Academic Advising Form to submit to Dr. Hodell. It may take 7 - 10 days to review and approve an advising form during times of high volume.  (Advising authorization does not reserve a seat in a course for a student. Please check your registration dates).</strong></p>
 <p><a href="documents/advisingform.pdf">Click here</a> for the Academic Advising PDF Form.  To view the advising form as a word document click <a href="documents/advisingform.doc">here</a>.</p>
 <p>Below are instructions on completing the form.</p>
 <p>The Academic Advising form should contain the following: </p>
 <ul>
   <li>Complete the General Information section completely</li>
   <li>If you have completed any course, enter the term, (Fall, Spring or Summer) and year that a course was completed under the column marked &ldquo;Semester Completed.&rdquo;    For example, if a course is completed in the Fall semester of 2009, simply put &ldquo;Fall 2009&rdquo; in that column. </li>
   <li>If you are currently enrolled in a course, enter the current term by the course under the column, &ldquo;Semester Enrolled/Anticipated&rdquo;</li>
   <li>Please indicate your projected courses for the next semester in the "Semester Enrolled/Anticipated" section. (Please review the <a href="schedule">current program schedule</a> to ensure that the courses are offered.)</li>
 </ul>
 <p>Completed form needs to be sent to Dr. Chuck Hodell at <a href="mailto:hodell@umbc.edu">hodell@umbc.edu</a>.</p>
 <p>Once the form is completed and returned, it is the responsibility of the student to contact Dr. Hodell by e-mail to check for approval and determine if additional advising is necessary.</p>
 <p>If you have any questions, contact Renee Eisenhuth, Program Coordinator at <a href="mailto:reisen@umbc.edu">reisen@umbc.edu </a>for clarification.</p>
 
