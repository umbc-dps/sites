 <h3>ISD Now  Forum</h3>
 
 
 <h4>What is ISD Now Forum?</h4>
 
 <p>The ISD Now Forum is an event, free to the community, that provides an opportunity for individuals working or interested in training, education, business, government and the non-profit sector to share ideas and dialogue on topics of importance. Part of UMBC's mission is to provide service. We take pride in our partnerships with employers and other organizations in the community, and are able to give back with events like the Training Forum. In many instances, we have panels of experts discussing topics related to business, organizational productivity and workplace learning and performance. Other topics center around issues related to human performance improvement, training and employee development, improving individual and organizational effectiveness and other related subjects.</p> 
 
 
