<h4>Portfolio Structure</h4>
  All portfolios  will be submitted via a file sharing site <strong><u>or</u></strong> a website of the student&rsquo;s creation. &nbsp;<strong><u>Either  is acceptable</u></strong>.  Students are  responsible for the creation and maintenance of their portfolio and may use a  web building tool of their own choice.  </p>
