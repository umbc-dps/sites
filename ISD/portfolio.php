  <p>The design of  each portfolio will be unique to the student. The portfolio should be well  organized and carefully planned so that all visual, audio, and text are clear  and flow intuitively for the viewer. &nbsp;All files and links must operate  flawlessly.</p>
  <h4>Preparation  for the Portfolio</h4>
  <p>The program  offers the following resources in order to prepare for the portfolio:</p>
<ul>
  <li>Full-credit  online course (EDUC &nbsp;689  Portfolio Development in Fall &amp; Summer)</li>
  <li>Optional  online portfolio tutorials</li>
  <li>Sample  portfolios from alumni and advanced students</li>
  <li>Interim  check-ins during the routine advising process</li>
  <li><a href="documents/portfoliochecklist.pdf">Portfolio Checklist</a></li>
</ul>
<h4>Initial Review of the Portfolio</h4>
  <p>Students  will submit their portfolios for <strong><u>initial</u></strong> review after their 6th course and no later than their 8th course.  The purpose of this review is to provide  students with feedback so they can be prepared when they submit their portfolio  for the <u>final</u> review.</p>
  <p>When you are ready to share your portfolio please contact Dr. Williams at <a href="mailto:gregw@umbc.edu">gregw@umbc.edu</a>.</p>
<h4>Final Review of the Portfolio</h4>
<p>Each portfolio must  be submitted for <strong><u>final</u></strong> review  in mid- semester during the semester when students expect to graduate (April  1st for Spring, July 1st for Summer, November 1st for Fall graduation). &nbsp;A  feedback committee consisting of faculty, ISD board members, selected alumni  and employers will conduct a preliminary review of each portfolio and offer  suggestions as necessary.  Final grading (pass  or not pass) of the portfolios will be made by vote of the committee.  Feedback will be made in a timely fashion, so  students can meet graduation timeline requirements.  The committee, at its option, may allow for  additional work to be completed to meet requirements.  Possible outcomes may include:<br>
  1. Pass – pass  without any conditions<br>
  2. Pass With  Conditions – passing is conditional on making minor changes<br>
  3. Not Pass –  student must make changes changes/revision based on committee feedback and then  must resubmit their portfolio</p>
<h4>Final Portfolio Submission Deadlines:</h4>
 
<p>The semester that you apply for graduation is when your portfolio is due.</p>
<ul>
  <li>Fall Semester: November 1st</li>
  <li>Spring Semester: April 1st</li>
  <li>Summer session: July 1st</li>
</ul>
<p>When you are ready to share your portfolio please contact Dr. Williams at <a href="mailto:gregw@umbc.edu">gregw@umbc.edu</a>.</p>
<h4>Timetable </h4>
  <p>The  following time table is provided as a planning tool to guide students</p>
<p><strong>First Semester</strong> – select file sharing  tool, post from courses after each semester<br>

  <strong>After 6 courses</strong> – student must post  portfolio for initial cursory review by evaluation committee<br>
  <strong>Final Semester</strong> – submit final  portfolio mid-semester of semester that student intends to graduate (April 1st for Spring, July 1st for  Summer, November 1st for Fall graduation). <br>
  <strong>After Every Semester</strong> – post work samples  from course(s) taken that semester<br>
  <strong>Ongoing</strong> – student request  feedback from fellow students, co-workers, alumni &amp; professionals</p>
<h4>Portfolio Ownership</h4>
<p>All submitted  portfolios are the property of the student but may be used as examples by UMBC  unless otherwise specified by the student.</p>

  <h4>Note to Students</h4>
  These  requirements are subject to change.  Since  this is a new requirement and process for students, the ISD program may need to  flexible with the administration of the requirements.</p>

