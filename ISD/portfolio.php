 <h3>UMBC ISD Portfolio Requirements</h3>
 <p>Beginning with the Fall 2012 semester, students admitted to the Master&rsquo;s degree in Instructional Systems Development – Training Systems, will be required to complete a professional portfolio. This requirement replaces the <a href="graduation#exam">comprehensive exam requirement</a>.</p>
 <p> The purpose of this requirement is to provide students an opportunity to demonstrate what they have learned in their coursework and the program. It also provides students, employers, clients, colleagues, etc. with tangible evidence of our graduates&rsquo; knowledge, skill, abilities and competencies. Portfolios can be a wonderful career tool that may assist graduates in getting jobs, promotions, new clients, and other professional opportunities.</p>
 <p> Students have the opportunity in nearly every class to create basic artifacts. With some polishing and improvements, these artifacts may be used to satisfy the portfolio requirement.</p>
 <p>In order to graduate each ISD Master’s student must provide a professional portfolio of course-related work samples that reflects their accomplishments throughout their tenure in the program. Portfolios must be approved by the portfolio committee for the student to graduate.</p>
 <p>The portfolio will contain a <strong><u>minimum of 8</u></strong> and <strong><u>maximum of 10</u></strong> work samples.</p>

 <ul>
   <li>Two  work samples must be from the 4 core courses (EDUC 602, 603, 605, and 671)</li>
   <li>At  least one sample must be included from either EDUC 771, 792 or 794</li>
   <li>Additional  samples may include work from other ISD program elective courses</li>
   <li>Students  may only use work samples that were created as course requirements for the ISD  program.   Samples created before they  entered the program or samples that are not related to ISD program requirements  will <u>not</u> be allowed.</li>
 </ul>
 
 <p>Up to two co-created or group work samples will be accepted with proper documentation, and consent from other contributors and a description of individual contribution to the work product. </p>
 
 <h4>Portfolio Content</h4>
 <p>In addition to the work samples, students will be required to include the following items, which may be documents, video, audio or other formats selected by the student:</p>
 <ul type="disc">
   <li>Overarching  piece reflecting the rationale for the selected work samples and portfolio  structure (250 words max)</li>
   <li>Student&rsquo;s  career goals and highlights of their learning in the program (250 words max)</li>
   <li>Resume(2  page max)</li>
   <li>Cover  sheet explaining each work sample, indicating competencies demonstrated and  significant learning points.  Please  format your work sample using the CCAR &nbsp; (Competency, Context, Action and  Result) method. &nbsp;This video shows you how to do that at <a href="http://www.youtube.com/watch?v=tJEgScNghNI#t=27m36s" target="_blank" title="Formatting Your Portfolio">http://www.youtube.com/watch?v=tJEgScNghNI#t=27m36s</a></li>
 </ul>
 <h4>Cover Page Format for Each Work Sample</h4>
 <p>Each work sample must have a cover page to introduce the reader to the sample.  It must have the following format:</p>

<table id="costs">
  <tr>
 <td>Competency:</td>
 <td>Identify the competency the sample demonstrates</td>
  </tr>
  <tr>
 <td>Context:</td>
 <td>Describe situation where you created the sample</td>
  </tr>
  <tr>
 <td>Action: </td>
 <td>Describe what you did &amp; why</td>
  </tr>
  <tr>
 <td>Results: </td>
 <td>Describe outcomes, use measurable results if possible</td>
  </tr>
  <tr></tr>
</table>

<h4>Examples of Work Samples</h4>    
<p>Students  should aim to present a varied portfolio that demonstrates a solid grasp of  core competencies and the competencies in their area(s) of interest or  specialty. &nbsp;&nbsp;It is not necessary to demonstrate all of the  competencies in any model.  Examples of the  types of work samples that may be included in the student&rsquo;s portfolio are: </p>
<ul type="disc">
       
      <li>Case  studies </li>
         <li>Design  plans or documents</li>
         <li>Instructor  manuals</li>
         <li>Evaluation  strategy, assessment instruments &amp; tools</li>
         <li>Job  aids or performance support tools</li>
         <li>Lesson  plans</li>
         <li>Multi-media  productions</li>
         <li>Online  learning projects</li>
         <li>Project  management plans or documents</li>
         <li>Research  or &ldquo;white&rdquo; papers</li>
         <li>Storyboards</li>
         <li>Training  materials and leader guides</li>
         <li>Video  or audio recordings</li>
         <li>Web  presentations, wikis and blogs</li>
         <li>Writing  sample </li>
    </ul>
<p>Students are responsible for honoring all copyrights and ensuring that appropriate credit is given for all materials used in the portfolio.</p>

