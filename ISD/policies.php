 <h3>Policies</h3>
 <p>These policies are only a partial listing of policies and any questions concerning program policies can be answered by the Program Director or his designee.</p>
 <h4>Registration Approval is Provisional</h4>
 <p>Since grades from a previous term are not official until after most registrations have been approved, all registrations are provisional based on a student meeting all grade and GPA requirements for registration. If a student fails to meet grade and GPA requirements, all registration approvals will be removed.</p>
 
 <h4>Unprofessional Behavior</h4>
 <p>The program reserves the right to remove any student from a course if it is determined that a student has acted in an unprofessional manor. Removals will be at the discression of the Program Director in consultation with faculty.</p>
 
 <h4>Non-Degree Student Registration</h4>
 <p>Students with a non-degree status will be allowed to take one course, EDUC602. No other courses or additional course registrations will be permitted under any circumstances. This course can be applied toward the MA or a certificate in the program with a grade of “B” or better.</p>
 
 <h4>Provisional Program Admittance</h4>
 <p>All students entering either the MA or a certificate program  will be provisionally admitted based on the following criteria:</p>
 <ol>
   <li>The first two courses, which must include EDUC602  must result in the student earning a &ldquo;B&rdquo; or better for both courses.</li>
   <li>Any grade of &ldquo;incomplete&rdquo; issued for the first  two courses will block any further registration until the incomplete grades are  addressed by finishing the course(s) with a &ldquo;B&rdquo; or better.</li>
 </ol>
 
 <h4>Transfer Credits</h4>
 <p>Requests for transfer credits must meet both the graduate school requirements and the following program requirements:</p>
 <ol>
   <li>No transfer credits or course substitutions will  be accepted for a certificate program. </li>
   <li>Credits must be on official transcripts no more  than five years old at the time of review.</li>
   <li>Credits must be a &ldquo;B&rdquo; or better for all courses.</li>
   <li>Credits must be from a regionally accredited  school and be from the graduate level or higher.</li>
   <li>No credits can be accepted as a substitute for a  required core course in the MA.</li>
   <li>All credits must be for a course that is, or could  be reasonably offered in the ISD graduate program.</li>
   <li>No more than six credits will be considered for  transfer.</li>
   <li>All transfer credit reviews and decisions will  be made by the Program Director or his appointed reviewer, followed by the  Graduate School.</li>
 </ol>

 
 
 
 <h4>Unofficial Grades</h4>
 <p>The Registrar of UMBC is the official source of grades for all students. No unofficial or provisional grades can or will be issues by the program in accordance with the provisions of FERPA. Any student needing an official transcript for any reason, including but not limited to tuition reimbursement must contact the Registrar. No unofficial, provisional or any other grade report can or will be issued by the program or any faculty member.</p>


