       <h4><a href="#4" id="4"></a>Post-Baccalaureate Certificate in Instructional Design for E-Learning (12 credits)<i class="fa fa-angle-down"></i> </h4></div>

    <div class="accordionbody">

       <p>This joint certificate is being offered through UMBC and University of Maryland University College (UMUC). Courses combined from the two institutions lead to the Instructional Design for E-Learning certificate.</p>
       <p>This certificate provides knowledge and skills in the field of course design, development and delivery at a distance, with a focus on E-Learning. Students will take four courses covering topics in the areas of training at a distance, the use of technology in distance education, and instructional design.       </p>
       <p><strong>Beginning with the fall 2015 semester, UMUC is discontinuing all certificates in their Master of Distance Education program, including the Instructional Design for E-Learning certificate. Students who are currently enrolled in the joint certificate may complete their work, but no new students will be accepted for the certificate. UMBC will continue to offer their two courses. Students who previously enrolled in the joint certificate should contact UMUC for information on their courses.</strong></p>
