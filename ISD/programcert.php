         <p>Students must apply to either UMBC or UMUC, for admission to this certificate program. Once admitted to their home institution, students would take courses at the other institution as a visiting student. Courses from the other institution will be treated as transfer credit at their home institution, leading to the joint certificate. The home institution will issue the certificate diploma.</p>

