<h4>Comprehensive Exam</h4>
<p>All M.A. students, admitted prior to fall 2012, are required to complete a comprehensive exam at the conclusion of their program of study. The purpose of the comprehensive exam is to evaluate a student's ability to analyze, synthesize and problem-solve a case study within the context of training and development. <a href="graduation#exam">See Comprehensive Exam Requirements</a></p>
       
       
<h4>Portfolio</h4>
<p>All M.A. students, admitted fall 2012 and on are required to submit a professional portfolio of course-related work samples that reflects their accomplishments throughout their tenure in the program. <a href="portfolio">UMBC ISD Portfolio Requirements</a></p>
<h4>Internship and Final Project Seminar</h4>
<p>Students are required to complete an internship course and final project seminar course.  Since most students are working adults the internship provides flexible options to complete this requirement. </p>


       <h4>Academic Standards</h4>
    <p>Students must earn a grade of &ldquo;B&rdquo; or higher in their first six credits.  Students must maintain a 3.0 grade point average (&ldquo;B&rdquo;) to graduate.  If students do not maintain a 3.0 GPA their matriculation in the program may be discontinued.  </p>


<p>For more details on policies and procedures visit the <a href="http://gradschool.umbc.edu/students/policies">UMBC Graduate School website</a>.</p>

