 <h3>Master of Arts in Instructional Systems Development</h3>
 <p>The Master of Arts requires a minimum of 36 credits hours. Students must complete seven required core courses (totaling 21 credits), five elective courses (totaling 15 credits) and take a final comprehensive examination (students admitted prior to fall 2012) or complete a professional portfolio (students admitted fall 2012 and on).</p>
 
 <h4>Benefits of Program</h4>
 <ul>
   <li>Combines research and practical design experience</li>
   <li>Provides a strong foundation in adult learning, instructional systems development and training principles</li>
   <li>Emphasis on implementation of technology within instruction</li>
   <li>Practical application of learned skills through internships and project-based course work</li>
   <li>Accomplished program faculty as scholars, instructional designers, developers and training managers</li>
   <li>Flexible learning options; online and evening classes</li>
 </ul>
 
 <h4>Twelve (12) Credits in Core ISD Courses</h4>
 <p>Core ISD courses must be taken in the first 18 credits.</p>
 <ul type="disc">
 <li>
EDUC 602: Instructional Systems Development I * (3)</li>
 <li>EDUC 603: Instructional Systems Development II
   (3)</li>
 <li>EDUC 605: Adult Learner
   (3)</li>
 <li>EDUC 671: Principles of Training and Development
   (3) </li>
 </ul>
 <p>(*)  Must be an ISD section of the course.</p>
 <h4>Nine (9) Credits of Advanced ISD Courses</h4>
 <p>To be taken after 21 credits have been completed.</p>
   <ul type="disc">
     <li>EDUC 771: Research Design in Education* (3)</li>     
