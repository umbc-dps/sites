      <h4>Application Fee</h4>


      <p>$50 - online; $70 - paper</p>


      <h4>Matriculation Fee</h4>


      <p>A one-time matriculation fee will be paid during the first two semesters of enrollment for Master's degree-seeking students, and the fee will be posted to student accounts automatically: $100 will be charged the first semester; $100 will be charged the second semester. Certificate and non-degree students will not be charged a matriculation fee or orientation fee. The certificate diploma fee will continue to be charged when the application for certificate diploma is submitted to the Graduate School. For more information on the matriculation fee, please <a href="http://sbs.umbc.edu/tuition-info/mandatory-fees/" target="_blank">click here</a>.</p>



      <h4>Financial Aid</h4>


