          </div>


          <div class="accordionbody">
 <p>This course emphasizes the many partitioning alternatives that  can be employed when developing physical systems architectures, including  hierarchical partitioning, federated partitioning, scalable architectures, high  availability architectures, and collaborative systems. &nbsp;The course also  deals with methods for architecting successful systems, such as achieving data  integrity, managing system workflow, and constructing representation models.</p>
 <p><strong>Prerequisites:</strong> It is assumed that the student  is familiar with the systems engineering process and the various development  methodologies. ENEE 660: Systems Engineering Principles and ENEE 661: Systems  Architecture and Design</p>
</div>

 <div class="accordionheader">
