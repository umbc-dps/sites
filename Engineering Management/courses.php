          <div class="accordionbody">
 <p>This course addresses simulation architectures. Topics addressed include cost and risk analysis;experimental design; simulation control and interfaces; requirements and architecture definition; simulation design and implementation; verification, validation, and accreditation; estimating, planning and controlling simulation efforts. <strong>Prerequisites: </strong>B.S. degree in EE or related field and a working knowledge of C/C++ or a similar programming language. ENEE 660 and ENEE 661 or consent of instructor.</p>
</div>

 <div class="accordionheader">
