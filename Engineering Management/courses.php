<div class="accordionheader">
            <h4><a href="#ENMG650" id="ENMG650"></a>ENMG 650: Project Management Fundamentals <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>Students learn the fundamentals of managing projects in a systematic way. These fundamentals can be applied within any industry and work environment and will serve as the foundation for more specialized project management study. Principles and techniques are further reinforced through practical case studies and team projects in which students simulate project management processes and techniques.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG652" id="ENMG652"></a>ENMG 652: Management, Leadership, and Communication <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>Students learn effective management and communication skills through case study-analysis, reading, class discussion and role-playing. The course covers topics such as effective listening, setting expectations, delegation, coaching, performance, evaluations, conflict management, negotiation with senior management and managing with integrity.
 </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG654" id="ENMG654"></a>ENMG 654: Leading Teams and Organizations <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>Students analyze leadership case studies across a wide range of industries and environments to identify effective leadership principles that may be applied in their own organizations. Students learn how to influence people throughout their organization, lead effective teams, create an inclusive workplace, use the Six Sigma process, implement and manage change and develop a leadership style.</p>
<p><strong>Prerequisite:</strong> ENMG 652: Management, Leadership and Communication </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG656" id="ENMG656"></a>ENMG 656: Engineering Law and Ethics <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course provides a comprehensive overview of important legal principles affecting engineers, engineering sciences and corporate management, with a focus on the intersection of these legal principles with business ethics. The student learns how to think through and process legal problems consistent with ethical norms, and how to analyze business risks in light of operative legal constructs, taking into consideration ethical issues, to arrive at a range of correct business decisions.                     Throughout the course, the student will learn substantive legal principles including an overview of constitutional, contract, tort, corporate and regulatory law. Students will work in groups during certain exercises, role play in real and hypothetical case studies, and make a final presentation of a comprehensive legal and ethical engineering problem.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG658" id="ENMG658"></a>ENMG 658: Financial Management<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course will cover the fundamentals of setting up, reading and analyzing financial statements and reports in a business setting.&nbsp; Course topics will include: project budgeting, profit planning, return on investment and basic corporate finance.  Students will analyze case studies from specific industries. </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG659" id="ENMG659"></a>ENMG 659: Strategic Management <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course is intended to integrate the learning from the previous management courses and to focus it on the perspective and problems of the Chief Executive Officer and other organizational strategic managers. The theme of the course is that any organization improves its chances of sustained success when its managers formulate an action-oriented strategic business plan based on the strategic management process. Case studies are included to illustrate the concepts and their applications.</p>
 <p><strong>Prerequisite:</strong> Minimum of three engineering management courses </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG660" id="ENMG660"></a>ENMG 660: Systems Engineering Principles <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course provides the foundational framework to understand the system engineering (SE) process, selection of specialized SE tools and the execution of SE under differing design or acquisition philosophies. the courses addresses: (1)SE principles (2)SE processes and metholodogies (3) integration of technical disciplines and (4) SE management. </p>
 <p>This course can be counted as either a management course or an engineering course for the M.S. in Engineering Management. </p>
 </div>
 
<div class="accordionheader">
            <h4><a href="#ENMG661" id="ENMG660"></a>ENMG 661: Leading Virtual Global Teams<i class="fa fa-angle-down"></i></h4>
</div>

          <div class="accordionbody">
            <p>This course is designed to help the student apply managerial concepts and skills to managing and leading virtual and/or global work teams. Geographically dispersed work teams have great challenges: tone is difficult to convey electronically, time zones limit audio communication opportunities, work oversight requires more reposting, and team building is exceedingly difficult using technological - rather than in-person - tools. Language and culture differences in multinational teams compound these challenges. Students will learn to empower others, build credibility, communicate appropriately and adapt quickly across cultures and technologies.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG663" id="ENMG663"></a>ENMG 663:&nbsp; Advanced Project Management Applications<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This advanced course in project management builds on the beginner level project management courses to expand the hands-on applications, with a focus on critical evaluation of project performance and ultimately creating an environment for maximizing one's own project management performance. With a strong emphasis on the importance of learning through application, the course will bridge academia with the professional business environment to provide opportunities for students to interact with industry professionals as the students execute their course work. Students will also confront the real challenges facing project managers associated with the growing global and virtual workforce through the use of on-line learning tools and methods of collaboration. At the successful completion of the course, students will have the requisite skills and experiences necessary to function effectively, and artfully, as skilled project managers.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG664" id="ENMG664"></a>ENMG 664: Quality Engineering &amp; Management<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course provides an overview of the basic principles and tools of quality and their applications from an engineering perspective. The primary quality schools of thought or methodologies, including Total Quality Management, Six Sigma and Lean Six Sigma, and quality approaches from key figures in the development and application of quality as a business practice, including W. Edwards Deming and Joseph M. Juran will be analyzed. Some of the key mathematical tools used in quality systems will be discussed, including Pareto charts, measurement systems analysis, design of experiments, response surface methodology, and statistical process control. Students will apply these techniques to solve engineering problems using the R software. Reading assignments, homework, exams, and the project will emphasize quality approaches, techniques, and problem solving.</p>
 <p>This course can be counted as either a management course or an engineering course for the M.S. in Engineering Management.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG668" id="ENMG668"></a>ENMG 668: Project and Systems Engineering Management<i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course will cover fundamental project control and systems engineering management concepts, including how to plan, set up cost accounts, bid, staff and execute a project from a project control perspective.&nbsp; It provides an understanding of the critical relations and interconnections between project management and systems engineering management.&nbsp; It is designed to address how systems engineering management supports traditional program management activities to break down complex programs into manageable and assignable tasks. </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG672" id="ENMG672"></a>ENMG 672: Decision and Risk Analysis <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course provides an overview of  decision and risk analysis techniques. It focuses on how to make rational  decisions in the presence of uncertainty and conflicting objectives. This  course covers rational decision-making principles and processes; competing  objectives, multi-attribute analysis and utility theory; modeling uncertainty  and decision problems using decision trees and influence diagrams; solving decision  trees and influence diagrams; uses of Bayes&rsquo; Theorem; defining and calculating  the value of information; regression analysis; incorporating risk attitudes  into decision analyses; and conducting sensitivity analyses. A significant  portion of the course is devoted to the use of various applications of  analytic, empirical, and subjective probability theory to the modeling of uncertain  events. As such, students will find it useful to have some experience with  basic probability.</p>
 <p>This course can be counted as either  a management course or an engineering course for the M.S. in Engineering  Management.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG690" id="ENMG690"></a>ENMG 690: Innovation and Technology Entrepreneurship <i class="fa fa-angle-down"></i></h4>
</div>


          <div class="accordionbody">
            <p>This course offers an overview of innovation and its role in entrepreneurial ventures, both in new companies and within existing corporations. The basics of entrepreneurship with specific emphasis on technology-based business start-up are investigated. For the purposes of this course, technologies include IT, engineering and biotech. The course covers where to find innovative ideas and how to determine if a business idea is feasible along with an overview of the critical success factors in a new venture start-up.</p>
</div>
            
<div class="accordionheader">
            <h4><a href="#ENMG692" id="ENMG692"></a>ENMG 692: Principles of Organization Learning<i class="fa fa-angle-down"></i></h4>
</div>
            
            <div class="accordionbody">
            <p>
            Corporations  are applying radically new management techniques to remain competitive. Today,  information forms the basis for competitive advantage as companies are competing  as much on their ability to create and manage new information, as they do on marketing  and selling their physical products and associated services. This course  studies how organizations create and use knowledge to support their operations  and strategic planning. A &ldquo;knowledge-creating&rdquo; company is said to be one that  consistently creates new knowledge, disseminates it widely throughout the  organization, and quickly embodies it in new technologies and products, and  whose sole business is continuous innovation. Actions are investigated which  corporate executives and managers can take to improve their management,  translation, and utilization of knowledge, to increase their organization&rsquo;s absorptive  capacities and ability to learn quickly, to posture themselves for innovative  responses to changing market conditions, to handle disruptive technology  cycles, to implement the effective use of data analytics, and to develop  sustainable business models and improve organizational performance.</p>
            
 <p>Businesses  collected more customer information in 2010, than in all prior years combined.  The amount of corporate data being collected is said to be doubling every 6  months. The intellectual property of these companies will take a second seat and  their ability to compete will depend on their current absorptive capacity, and  their capacity to learn as an organization faster than their competitors.  This course prepares students for future  market environments where innovative businesses will compete based on their  ability to process information and learn, and learn quickly. This course has  selected the most relevant research papers in the fields of knowledge  management, organizational learning, and strategic planning. After this course,  you will be familiar with the most significant research dedicated to optimizing  business and project processes that has been released in the last decade. Each  research paper has been summarized in 2 to 3 page reports to help students  manage the significant amounts of information and to track the key points being  made in each paper. The future may already be here, so why not be prepared for  it!</p>
