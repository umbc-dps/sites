<div class="accordionheader">
            <h4><a href="#ENEE660" id="ENEE660"></a>ENEE 660: Systems Engineering Principles [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
 <p>This course provides the foundational framework to understand the system engineering (SE) process, selection of specialized SE tools and the execution of SE under differing design or acquisition philosophies. The course addresses: (1) SE principles (2) SE processes and methodologies (3) integration of technical disciplines and (4) SE management. <strong>Prerequisite:</strong> B.S. degree in EE or related field.</p>
</div>

 <div class="accordionheader">
            <h4><a href="#ENEE661" id="ENEE661"></a>ENEE 661: System Architecture and Design [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
 <p>This course covers the development of a system architecture and hardware/software system design within the overall systems engineering(SE) process. Major topics include development of an operational concept, functional decomposition, requirements allocation and partitioning, interface definition, inclusion of integrity, reliability, and maintainability within the design concept, validation and verification, technical performance budgeting, quality function deployment techniques, and statistical and linear models. <strong>Prerequisite: </strong>B.S. degree in EE or related field. ENEE 660 (SE Principles) may be taken concurrently.</p>
</div>

 <div class="accordionheader">
