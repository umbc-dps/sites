          <div class="accordionbody">
 <p>This course provides a survey of artificial intelligence concepts, technologies, applications, techniques, methodologies and issues. The first half of the course will focus on expert systems and the knowledge engineering life cycle. The second half of the course will highlight various knowledge technologies, including case-based reasoning, genetic algorithms, fuzzy logic, neural networks, hybrid intelligent systems, data mining and knowledge management. The course also will discuss management implications of use, non-use and misuse of AI technologies. <strong>Prerequisite: </strong>Graduate student standing and permission of the instructor.</p>
