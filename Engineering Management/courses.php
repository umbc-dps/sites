          <div class="accordionbody">
 <p>This course covers the translation of design specifications into product elements, integration of these elments into a system, and the verification that the resulting system performs as intended in it operational environment. <strong>Prerequisites: </strong>ENEE 660 and ENEE 661 or consent of instructor.</p>
 <p>** A student who completes the above four Systems Engineering courses plus ENEE 670 - Systems Engineering Project will earn the Post-Baccalaureate Certificate in Systems Engineering, as described at <a href="http://www.umbc.edu/se/certificate">Systems Engineering - Post-Baccalaureate Certificate</a>.</p>
</div>

 <div class="accordionheader">
