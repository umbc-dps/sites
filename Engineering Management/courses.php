 <p>IS 607 will give you a hands-on introduction to the major basic technologies used in the field of Information Systems. These technologies are: Networking and data communications, programming, databases, HTML, JavaScript and server-side processing. This course will require you to develop web pages and sites. This course uses only client-side technology that requires a web browser. You will also have to FTP files up to servers at UMBC. All details of this process are included in the course materials. Some lectures include movies that require QuickTime or an open source equivalent.</p>
 <p>As of Spring 2009, IS 607 is the equivalent to IS 605 and IS 606.</p>
</div>

 <div class="accordionheader">
