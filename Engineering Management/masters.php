<h3>Admissions: Master's Degree</h3>
<h4>Application Dates</h4>
<p>The application deadline for Fall 2016 is <strong>August 1</strong><br>
Spring 2016 Classes Start: <strong>August 31, 2016</strong></p>

 <h4>Admissions Requirements </h4>
 <p>A bachelor’s degree in Engineering, Computer Science or Information Systems.</p>
 <ul>
   <li>Minimum undergraduate GPA of 3.0 on a 4.0 scale </li>
   <li>GREs and letters of recommendation are <strong>not</strong> required.</li>
   <li>International applicants should refer to the <a href="international.php" target="_blank">International Admission page</a> for special admission information.</li>
 </ul>
 
<h4>Application Process</h4>
 <p>Applications for the Master's program are accepted for the Fall &amp; Spring semesters: </p>
 <ul>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_blank">Apply online</a>
     <ul>
       <li>Select proposed program Engineering Management  &gt;&gt;   Engineering Management ENGM</li>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo; section</li>
       <li>Complete residency information to be reviewed for in-state tuition rates</li>
     </ul>
   </li>
   <li style="list-style-type:decimal">Request official transcripts from all post-secondary institutions to be sent to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or emailed to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
   <li style="list-style-type:decimal">Submit resume to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a> or <a href="mailto:kedmonds@umbc.edu" target="_blank">kedmonds@umbc.edu</a>.</li>
 </ul>


 <p>Please use one of the following designations to indicate your intended engineering track:</p>



 <p><strong>Engineering Tracks</strong>:</p>
 <ul>
   <li><a href="courses.php#bi" target="_blank">Chemical/Biochemical Regulatory Engineering</a> - BI</li>
   <li><a href="courses.php#ch" target="_blank">Chemical Engineering</a> - CH</li>
   <li><a href="courses.php#ce" target="_blank">Computer Engineering</a>  - CE</li>
   <li><a href="courses.php#cs" target="_blank">Computer Science</a> - CS</li>
   <li><a href="courses.php#cy" target="_blank">Cybersecurity</a> - CY</li>
   <li><a href="courses.php#ee" target="_blank">Electrical Engineering</a> - EE</li>
   <li><a href="courses.php#en" target="_blank">Environmental Engineering</a> - EN</li>
   <li><a href="courses.php#hc" target="_blank">Human Centered Computing</a> - HC</li>
   <li><a href="courses.php#is" target="_blank">Information Systems</a> - IS</li>
   <li><a href="courses.php#me" target="_blank">Mechanical Engineering</a> - ME</li>
   <li><a href="courses.php#sy" target="_blank">Systems Engineering</a> - SY</li>
 </ul>
 
 
 <p><strong>The following information is required to process your application:</strong></p>
 <ul>
   <li>Completed application including residency form (determines in-state tuition eligibility)</li>
   <li>Statement of academic goals</li>
   <li>Transcript(s) from each college university attended</li>
   <li>Application fee ($50 online / $70 paper)</li>
   <li>Current resume: Submit as additional document during application process or email to <a href="mailto:awaters@umbc.edu" target="_blank">kedmonds@umbc.edu</a>) </li>
</ul>
 
