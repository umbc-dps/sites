 <h3>Accelerated BS/MS Degree Option</h3>
<p>UMBC Engineering, Computer Science, and Information Systems majors also considering a Master of Science degree in Engineering Management may apply for admission to an accelerated course of study leading to the Bachelor of Science in their major, and a Master of Science in Engineering Management. This program is open to full-time and part-time students.</p>

<p>The BS/MS program has two phases. In Phase I, undergraduate students may take up to nine (9) credits of graduate level course work, subject to approval of their undergraduate advisor and the graduate program director, that may be applied towards the requirements of both degrees. Students typically apply for Phase I of the program in the second semester of their junior year. Phase II corresponds to admission into the MS program.</p>

<p>Admission to Phase I is based on the student's academic record, an evaluation of the student's Statement of Purpose, and an interview. Applicants to Phase I are expected to have an overall GPA of 3.0 or higher.</p>

<p>Once admitted to Phase I, the student may take up to nine (9) credits of graduate level Engineering Management, or graduate level courses in Engineering, Computer Science, or Information Systems that can be applied towards the requirements for both the Bachelors and MS degrees. Both the student's undergraduate faculty advisor, and the Engineering Management Graduate program director must approve the courses. The students must also meet the prerequisites for the graduate level courses. In order for the courses to be counted for graduate credit, the student must receive a grade of B or higher.</p>

<p>Admission to Phase II requires an application for Graduate School. Admission to Phase II is based on the same criteria as admission into the regular MS degree program: academic record, goal statement, resume, and (optional) letters of recommendation. In accordance with Graduate School policy, applicants to Phase II (the MS degree) must have an overall GPA of 3.0 or higher, and maintain a GPA of 3.0 or higher during the course of their graduate studies. <u>Note: Admission into Phase I does not constitute a promise to admit the student to Phase II.</u></p>

<p>Contact Kim Edmonds (<a href="mailto:kedmonds@umbc.edu">kedmonds@umbc.edu</a>) or visit <a href="http://www.umbc.edu/engm" target="_blank">www.umbc.edu/engm</a> for application information or admissions questions.</p>

