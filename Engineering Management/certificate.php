 <h3>Admissions: Post-Baccalaureate Certificate</h3>
 <h4>Application Dates</h4>
<p>The application deadline for Spring 2017 is <strong>December 1</strong><br>
  Spring 2017 Classes Start: <strong>January 30, 2017</strong></p>
  
  <p>The application deadline for Fall 2017 is <strong>August 1</strong><br>
  Fall 2017 Classes Start: <strong>August 30, 2017</strong></p>

 <h4>Admissions Requirements:</h4>
 <p>A bachelor’s degree in Engineering, Computer Science or Information Systems.</p>
 <ul>
   <li>GREs and letters of recommendation are <strong>not</strong> required.</li>                
   <li>Applicants should have an overall grade point average of 3.0 or higher on a 4-point grading scale in their B.S. course work and any previous graduate courses. (Applicants with an appropriate level of knowledge and professional experience in the IT or Engineering Management fields are strongly encouraged to apply.)</li>                                                          
   <li>International applicants should refer to the <a href="international.php">International Admission Page</a> for special admission information </li>                 
   </ul>
 <h4>Application Process:</h4>
 <p>Applications for the Master's program&nbsp;are accepted for the Fall and Spring semesters. To apply, complete a UMBC Graduate Application for Post-Baccalaureate Certificate Programs use program name: Engineering Management (EMAN)</p>

 <ul>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_blank">Apply online</a>
     <ul>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo; section</li>
       <li>Complete residency information to be reviewed for in-state tuition rates</li>
     </ul>
   </li>
   <li style="list-style-type:decimal">Request official transcripts from all post-secondary institutions to be sent to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or emailed to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
   <li style="list-style-type:decimal">Submit resume to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a> or <a href="mailto:kedmonds@umbc.edu" target="_blank">kedmonds@umbc.edu</a><a href="mailto:kedmonds@umbc.edu"></a>.</li>
 </ul>
 
 
 <p>Your application should include the following   documents: <em>(Incomplete applications will not be reviewed.)</em></p>
 <ul>
   <li>Completed application including residency form (Residency form determines in-state tuition eligibility)</li>
   <li>Statement of academic goals</li>
   <li>Official transcript(s) from each college &amp; university attended</li>
   <li>Non-refundable application fee ($50 online) </li>
   <li>Current resume</li>
 </ul>
 <h4>Mailing Instructions </h4>
 <p><strong>All application documents should be mailed to:</strong> <br>
UMBC Graduate School, MPS: Engineering Management <br>
   1000 Hilltop Circle<br>
   Baltimore, MD 21250</p>
 <p>If you require further assistance, please contact <a href="mailto:kedmonds@umbc.edu">Kim Edmonds</a>  or call 410-455-3445. </p>



