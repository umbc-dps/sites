<h4><strong><a id="academics"></a>Frequently asked questions about ACADEMICS:</strong></h4>
  <div class="accordionheader">
    <h4>1. What are  some of the strengths of the program?<i class="fa fa-angle-down"></i></h4>
  </div>

    <div class="accordionbody">
      
      <ul>
        <li><em><strong>Full-time  Faculty:</strong></em>&nbsp;Members of  the Program faculty belong to the Departments of Education and Modern  Languages/Linguistics. They are international in make-up with expertise in  applied linguistics, sociolinguistics, instructional systems development,  psychology, research, ESL and EFL program development, methodology, curriculum  development, and testing and evaluation. They have had considerable experience  with K-12, adult, and university programs, both ESOL and bilingual, throughout  the U.S. and abroad.</li>
        <li><em><strong>Internships  in the U.S. and Abroad:</strong></em>&nbsp;The UMBC  program has a strong internship component. With program connections to local  school systems and a variety of international situations, students are provided  with opportunities for practicum and internship experiences throughout Maryland  and in such countries as Korea, Ecuador, Mexico, Bolivia, and El Salvador.</li>
        <li><em><strong>Resource  Center for Language and Culture:</strong></em>&nbsp;The program has its own Resource Center at  the University. This unique center provides a wealth of materials in a place  where students can meet and study. The Center's very large collection consists  of cross-cultural, foreign language, and ESOL journals, texts, videos, and  other materials.</li>
        <li><em><strong>Diverse  Student Body:</strong></em>&nbsp;The  students in the program come from various parts of the United States and from  around the world. They bring a rich variety of academic and professional  experience to the program. Many have been ESL/EFL learners themselves.</li>
        <li><em><strong>Program and  Post-program Employment:</strong></em>&nbsp;Program  students have many opportunities to tutor, teach, and work in ESL related  settings. Opportunities exist in local K-12 ESOL programs,  colleges/universities, English language institutes, special language programs,  and ESOL/bilingual support centers. Salaries range from $25 to $65 per hour.  Prospective employers seek out program students and graduates because of the  program's reputation and theory to practice emphasis.</li>
        <li><em><strong>Broad-based  Preparation:</strong></em>&nbsp;Besides  providing a strong foundation in applied linguistics and ESL/EFL methodology,  the program also places a strong emphasis on instructional systems development  (ISD) and cross-cultural training. The ISD core of the program prepares  students to analyze, design, develop, operate, and evaluate instructional  programs. Because of this training, many graduates are hired for administrative  jobs with instructional and/or training programs.</li>
        <li><em><strong>Accessible Faculty and Staff: </strong></em><em>The program faculty and staff pride themselves on maintaining open lines  of communication with applicants, current students, and alumni.</em></li>
        <li><em><strong>Collaborative Environment:</strong></em><em> Our learning  community is based on collaboration rather than competition. </em></li>
      </ul>
      <p>&nbsp;</p>    
    </div>
 <div class="accordionheader">
    <h4>2. Does the program prepare teachers for all levels of ESL and EFL?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Yes.  Graduates of the program are prepared to teach both ESL and EFL at elementary,  secondary, adult, and college levels. All courses are designed to incorporate  both ESL and EFL at elementary, secondary, adult, and college levels. All  courses are designed to incorporate both ESL and EFL theory, methodology, and  practice. Students can choose to focus on any area in their outside  assignments, special projects, and papers. The majority of the students prepare  for teaching both ESL and EFL. Some focus on K-12; others focus on teaching  adults.</p>
    </div> 
    <div class="accordionheader">
    <h4><strong>3. How long  will it take me to finish the MA TESOL program?</strong><i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>The basic  program requires 12 courses to complete. Full-time students can complete the  program by taking 3 courses each fall and spring and 1 or 2 courses in the  summer. This would enable them to finish in a year and a half. Part-time  students taking 2 courses per semester would need 2 and a half years to  complete the program. Students interested in taking the certification option  should expect to take an additional semester to complete the program. The internship  experience requires a full academic year, and course loads in addition to the  internship should not be too demanding. Students choosing the Thesis option  often also require an additional semester to complete the program.</p>
    </div> 
    <div class="accordionheader">
    <h4>4. How large  is the program?<i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>The  program has approximately 65 MA students and 25 certificate students.  Approximately one half of these students are part-time (1-2 classes/semester). The program also offers cohort programs to current  Maryland public school teachers. </p>
    </div> 
    <div class="accordionheader">
    <h4>5. What is the  difference between the non-thesis and thesis options?<i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>Students  who choose the non-thesis option attend a project seminar class, undertake an  internship, and take a written comprehensive exam at the end of the program.  Thesis option students develop a research proposal and write a thesis under the  guidance of an advisor and other faculty members. The thesis preparation takes  the place of the project seminar, internship and comprehensive examination. The  thesis option requires 3 more credits than the non-thesis option.</p>
    </div> 
    <div class="accordionheader">
    <h4>6. How  difficult is it to be admitted into the program?<i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>The program has high standards for admission. Applicants  first must meet the University of Maryland Graduate School Baltimore admission  requirements (see graduate catalog). All applications are screened by the  program's admissions committee. Final decisions are based on the applicants'  past academic experience, GRE scores, recommendations, and letters of intent.  We actively recruit students from diverse backgrounds and academic preparations.  At present, approximately 10-20 new students are accepted each semester.</p>
    </div> 
    <div class="accordionheader">
    <h4>7. What is  K-12 ESOL Certification? How could it be useful to me?<i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>Obtaining  K-12 ESOL Certification will enable you to be hired as an ESOL teacher in a  Maryland public school system. ESOL teachers in Maryland teach English language  learners in a variety of ways depending on their job placement. Opportunities  are available at both elementary and secondary levels. ESOL teachers teach  self-contained classes, pull-out classes and in inclusion situations depending  on the system and placement they receive. Maryland ESOL Certification is  recognized in many other states and in American schools overseas. Each state  has its own requirements for certification and may require additional training,  but Maryland certification standards are similar to and more rigorous than  those of most other states.</p>
    </div> 
    <div class="accordionheader">
    <h4>8. How much  more course work does it take to get the M.A. plus K-12 ESOL Certification?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>The  non-certification option requires 36 credits including a 3-credit internship.  The certification option requires 42 total credits including 6 additional  credits of internship.</p>
    </div> 
    <div class="accordionheader">
    <h4>9. Do I need  to decide now if I want to pursue K-12 ESOL Certification in addition to the  M.A.?<i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>No. You  can decide to pursue certification after you have started taking courses at  UMBC. Once on campus, you can talk more extensively with your academic advisor  and with students who are already studying for certification so that you can  make a more informed decision.</p>
    </div> 
    <div class="accordionheader">
    <h4>10. Can I get  credit toward the MA for courses that I have already taken?<<i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>Students  can transfer up to 6 credit hours (2 courses) toward the master's degree. These  courses must be of a similar nature to courses that are required for the degree  and they must be at the graduate level. The courses can be  from any accredited university or college, but they must not have been used  toward any other degree. Students who have used such courses toward other  degrees can request to have the program requirements for those waived. Students  who have courses waived must still complete a minimum of 36 credits in the  program. </p>
      <p>Prospective  students can take one or two courses in our program as non-degree  students with the permission of program advisors. These courses do not assure  acceptance into the program, but they can be counted toward the degree if the  student is admitted.</p>
    </div>
    <h4><strong><a name="cert" id="cert"></a>Frequently asked questions about THE CERTIFICATE   PROGRAM:</strong>
    </h4>
    <div class="accordionheader">
      <h4>1. Can I use the UMBC ESOL Certificate for State Certification?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>State ESOL  Certification and the UMBC ESOL Certificate are 2 different things. The  Certificate does not mean state certification. The state has a list of  requirements for ESOL Certification which are addressed by the Program's MA  Program. However, for individuals who are currently certified by the State in  another field, ESOL Certification may be added by passing the Praxis II  Examination in ESOL. UMBC's Certificate program will help students pass Praxis  II and prepare them to teach in ESOL classes in Maryland's public schools.      
</div>
  <div class="accordionheader">
    <h4>2. Can courses I take for the certificate be applied to the MA degree?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Yes! All  Certificate courses can be transferred to the MA if a student is admitted to  the MA program.</p>
    </div>
  <div class="accordionheader">
    <h4>3. Are all Certificate courses offered online?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Yes, All  certificate courses are offered both face-to-face and online. Students can choose  to take all face-to-face classes, or all online courses or a combination.  Students who take only online courses get a reduction in school fees.</p>
</div>
 <h4><strong><a name="app" id="app"></a>Frequently asked questions about THE  APPLICATION:</strong>
    </h4>
    <div class="accordionheader">
      <h4>1. The application says that I need ETS codes for the  universities I have previously attended. Do I need to fill these in?<i class="fa fa-angle-down"></i></h4>
</div>
    <div class="accordionbody">
      <p>No. You do not need to list  ETS codes for any of the universities you have attended outside of the United  States.&nbsp;</p>
</div><div class="accordionheader">
      <h4>2. What are the &quot;Program Code&quot; and &quot;Track  Code&quot; that I need to fill in on the application forms?<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>The code  for MA applicants is TESL ( face-to-face) or TESLO (online).&nbsp;<br>
      The code for Certificate applicants is CESL (face-to-face) or CESLO (online).</p>
</div><div class="accordionheader">
      <h4>3. Do I need to fill out the graduate school residency  form?<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>The  residency form is only for students who have lived in Maryland for an extended  period and pay taxes to the state. If you have not lived in Maryland, you do  not need to fill out this form.&nbsp;</p>
</div><div class="accordionheader">
      <h4>4. What is the Golden ID Program? The application asks if I  would like to apply under it.<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>The Golden  ID Program offers free tuition to Maryland residents who are over the age of  60 and not employed full-time. If this applies to you, check &quot;yes&quot;.  If you are not in this situation, check the box on the application form for  &quot;no.&quot;&nbsp;</p>
</div>
<div class="accordionheader">
      <h4>5. Should I send all of my application materials to UMBC  together? If I send them separately, I am worried that some materials might  become lost.<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>Your  application materials will not get lost at UMBC if you carefully follow the  directions for submitting application materials. These directions are contained  in the Graduate School Application. In the case of GRE and TOEFL test scores,  these should be sent directly from the testing authority to the university.&nbsp;</p>
</div>
<div class="accordionheader">
      <h4>6. What is the  preferred method of having references submit their recommendations?<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>MA applicants can submit their references&rsquo; contact  information with their online applications. Certificate applicants can request  that their references send recommendations to the ESOL program by mail or to <a href="mailto:esol@umbc.edu">esol@umbc.edu</a>.</p>
</div>
<div class="accordionheader">
      <h4>7. What about my TOEFL scores? Will they arrive at the  right place, too?<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>If you  have listed the correct ETS institution code for UMBC (5835), your scores will  arrive at UMBC's Graduate School. The Graduate School will retain the original  score sheet and send a copy to the TESOL program.&nbsp;</p>
</div>
<div class="accordionheader">
      <h4>8. What is the minimum TOEFL score required by the program?<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>The  minimum TOEFL score required for admissions consideration is 213 on the  computer-based test. For the internet-based test, the minimum is 80.&nbsp;</p>
      <p>We also accept the IELTS. The minimum score required for that test is  6.5</p>
    </div>
<div class="accordionheader">
      <h4>9. If I send UMBC a photocopied TOEFL or GRE score report,  do I still need to have my official scores sent to UMBC from  (ETS)?<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>Yes. You  may send a photocopied score report to the TESOL program to speed up the  application review process; however, you must ensure that an official score  report is sent to UMBC directly from ETS.&nbsp;</p>
</div>
<div class="accordionheader">
      <h4>10. Which institution code should I fill in on the GRE?<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>The ETS  institution code for UMBC on the GRE is 5835. (This is the same institution  code that you fill in on the TOEFL).&nbsp;</p>
</div>
<div class="accordionheader">
      <h4>11. The application says that I need to submit documents  for certifying financial support. Do I submit these documents with my  application?<i class="fa fa-angle-down"></i></h4></div>
    <div class="accordionbody">
      <p>No. The  International Education Services Office at UMBC prefers that you send these  documents after being notified of your acceptance to the university because the  documents need to be as recent as possible. Please directly contact the  International Education Services office (ies@ umbc.edu) if you have additional  questions about certifying financial support.&nbsp;</p>
</div>
 <h4><strong><a name="adm" id="adm"></a>Frequently asked questions about ADMISSIONS:</strong>
    </h4>
    <div class="accordionheader">
      <h4>1. I have already submitted some of my application  materials. Can you tell me early whether or not I have been admitted?<i class="fa fa-angle-down"></i></h4></div>
 <div class="accordionbody">
      <p>No. We are  unable to make final admissions decisions until all the required application  materials have been submitted. Because admissions decisions are made on a  rolling basis, however (meaning that the admissions committee meets on several  occasions to make admissions decisions), it is to your advantage to send us  your application materials as early as possible.&nbsp;</p>
</div>  
 <div class="accordionheader">
      <h4>2. If I am admitted to the program, do I have to come to  UMBC the following semester, or can I come at a later date?<i class="fa fa-angle-down"></i></h4></div>
 <div class="accordionbody">
      <p>Once you  receive an official letter of acceptance to UMBC, you can request to defer your  enrollment for up to one year from the time it is offered.</p>
</div>
 <h4><strong><a name="housing" id="housing"></a>Frequently asked questions about HOUSING:</strong>
    </h4>
    <div class="accordionheader">
      <h4>1. What will  housing be like at UMBC?<i class="fa fa-angle-down"></i></h4></div>
 <div class="accordionbody">
      <p>UMBC has  on-campus housing for graduate students. You can also find accommodations off  campus. Graduate students usually rent rooms or apartments near UMBC with costs  varying from $250 to $600 per month. It is common for students to share  apartments. There is an office on campus that maintains a list of off-campus  housing possibilities near UMBC, including those on campus shuttle-bus routes.  (The campus shuttle bus provides free transportation for UMBC students to and  from the university). Most international students choose to live in apartment  complexes that are located on the shuttle-bus route. </p>
 </div> 
 <div class="accordionheader">
      <h4>2. Can I arrange for housing before I arrive on campus?<i class="fa fa-angle-down"></i></h4></div>
 <div class="accordionbody">
      <p>It is  never a good idea to commit to a housing contract without actually seeing the  accommodations in person. Therefore, it is best to arrange permanent housing  AFTER you arrive in Baltimore.&nbsp;</p>
</div>  
 <div class="accordionheader">
      <h4>3. If I  don't have permanent housing when if first arrive on campus, where can I stay?<i class="fa fa-angle-down"></i></h4></div>
 <div class="accordionbody">
      <p>The  university offers temporary, relatively inexpensive on-campus housing for  several weeks in August and January. This option provides you with a place to  stay for a short period while you look for a permanent residence. The  International Education Services Office will forward you more information  regarding temporary on-campus housing once you have been notified of your  acceptance by the university.</p>
</div> <h4><strong><a name="fin" id="fin"></a>Frequently asked questions about FINANCIAL  ASSISTANCE:</strong>
    </h4>
    <div class="accordionheader">
      <h4>1. If I am an International student, can UMBC offer me any  scholarships or financial aid?<i class="fa fa-angle-down"></i></h4></div>
 <div class="accordionbody">
      <p>UMBC  has some support available for graduate students but nothing specifically for  international students. It is very  possible to find on-campus work. Once you arrive at UMBC, you can begin  searching for on-campus jobs. The positions are advertised when they become  available, and you will need to contact the individual campus offices  sponsoring the jobs in order to apply. The Career Development and Placement  Office on campus maintains a list of current on-campus job opportunities. Be  mindful, however, that the income from on-campus jobs provides a small  supplementary income for living expenses; it is probably not enough to support  oneself.&nbsp; </p>
</div> 
 <div class="accordionheader">
      <h4>2. What about graduate assistantships? These are mentioned  on the graduate school application.<i class="fa fa-angle-down"></i></h4></div>
 <div class="accordionbody">
      <p>The Education Department has several graduate assistant  positions available each year, including one assistantship position with the  TESOL program. These positions last the entire ten-month academic year. The  graduate assistant positions are usually held by students who have already been  enrolled in the M.A. program for at least one semester; however, prospective  students may apply. The positions are advertised each March, and the details of  the jobs and application procedures are available at that time.</p>
    </div>

