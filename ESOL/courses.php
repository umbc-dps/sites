  <h3>Internships and/or K-12 Student   Teaching</h3>
  <p>The non-certification internship is a field-oriented   experience in a setting consistent with the student's professional preparation   and career goals. It provides the student with the opportunity to put into   practice the skills and knowledge acquired in the program's courses. The internships   lasts from 3 to 6 months and can be done in the United States or abroad. The   program has internship agreements with institutions in Ecuador, Korea, Mexico,   Bolivia and El Salvador. Some internships include paid round trip transportation   and room and board. Internships can also be arranged individually by the   student.<BR>
    <BR>
    Students who add the K-12 ESOL Certification option to their   program are required to do 2 semesters of student observation/teaching in K-12   ESOL public school programs in the Baltimore/Washington area. This internship is   experienced at both elementary and secondary schools. Students who are employed   as ESOL teachers can use their employment site to satisfy this   requirement.</p>
  <h3>Thesis Research</h3>
  <p>Thesis option students   develop a research proposal and write a thesis under the guidance of an advisor   and several faculty members. Thesis students can do their thesis research in the   United States or abroad. Many of our students who have gone on to doctoral   programs have found their thesis experience helpful.</p>
