<h3>Program Overview</h3>
<p>UMBC offers an <strong>MA in  TESOL</strong> and a graduate <strong>ESOL Certificate</strong>.  Established  in 1979, our TESOL program is designed to train both prospective and  experienced teachers of English as a Second Language (ESL) and English as a  Foreign Language (EFL). It provides students with a strong theoretical and  practical foundation in applied linguistics, language acquisition, ESL/EFL  methodology, testing, cross-cultural studies and curriculum development.&nbsp;  Both programs can be taken completely in face-to-face classes on campus,  completely online, or with a combination of face-to-face and online classes.
<h4>MA in TESOL  Options<br>
</h4>
<ul>
  <li><a href="#option1">Option 1: Master of Arts in TESOL - General</a></li>
  <li><a href="#option2">Option 2: Master of Arts in TESOL with Thesis</a></li>
  <li><a href="#option3">Option 3: Master of Arts in TESOL with K-12  Certification</a></li>
  <li><a href="#option4">Option 4: Master of Arts in TESOL with Thesis  and K-12 Certification</a></li>
</ul>
<p>Students may complete the programs either online  or face-to-face. Our programs remain flexible so that students can study  at their own pace. </p>
<p><strong><a id="option1"></a>OPTION 1 MASTER OF ARTS IN TESOL – GENERAL (36 CREDITS)</strong></p>
<p>The general TESOL M.A.  program consists of 12 three-credit courses.<br>
  8 courses (24 credits)  are required:</p>
<ul>
  <li>EDUC 601: Human Learning  and Cognition</li>
  <li>EDUC 602: Instructional  Systems Development I</li>
  <li>EDUC 625: Teaching  Reading &amp; Writing to ESOL/Bilingual Students I</li>
  <li>EDUC 644: Linguistics for  ESOL/Bilingual Educators</li>
  <li>EDUC 688: Methods and  Techniques in Teaching ESOL</li>
  <li>EDUC  666: Cross-Cultural Communication for ESOL</li>
  <li>EDUC 794: ISD Project  Seminar in ESOL </li>
  <li>EDUC 792S: ESOL  Internship </li>
</ul>
<p>4 elective courses (12  credits) can be chosen from among the following:</p>
<ul>
  <li>EDUC 636: ESOL Testing  and Evaluation</li>
  <li>EDUC 655: Teaching  Reading and Writing to ESOL/ Bilingual Students II</li>
  <li>EDUC 667: The Grammar of  American English</li>
  <li>EDUC 672: Bilingualism</li>
  <li>EDUC 689: Instructional  Technology for ESOL Teachers</li>
  <li>EDUC 670: Theories of Language  Learning in the ESOL Classroom/MLL 670: Second Language Acquisition &amp; Learning: From Theory to Practice</li>
  </ul>
<p>Students who do not complete a thesis are required to take a comprehensive examination. </p>
<p><strong><a id="option2"></a>OPTION 2 MASTER OF ARTS IN TESOL WITH THESIS (39 CREDITS)</strong></p>
<p>Thesis students develop a  research proposal and write a thesis under the guidance of an advisor and two  or more committee members. Students may do thesis research in the United States  or abroad. Thesis students take the  same required and elective courses as in Option 1, less the Project Seminar  (EDUC 794) and Internship (EDUC 792S). In addition, thesis students must  complete at least two research courses and 6 credits of thesis research.</p>
<ul>
  <li>EDUC 645: Qualitative  Research Methods (Elective)</li>
  <li>EDUC 684: Introduction to  Field Research Methods in School or Community (Elective)</li>
  <li>EDUC 771: Research  Designs in Education (Required)</li>
  <li>EDUC 799: Master&rsquo;s Thesis  Research (Required - 6 credits)</li>
  </ul>
<p>Students who do not complete a thesis are required to take a comprehensive examination. </p>
<p><strong><a id="option3"></a>OPTION 3 MASTER OF ARTS IN TESOL WITH K-12  CERTIFICATION (42 CREDITS)</strong></p>
<p>Because of Maryland  State Department of Education requirements, students who choose the Certification  option take the required courses in Option 1 and the following courses, which  are also required.</p>
<ul>
  <li>EDUC 636: ESOL Testing  and Evaluation</li>
  <li>EDUC 655: Teaching  Reading and Writing to ESOL/ Bilingual Students II</li>
  <li>EDUC 667: The Grammar of  American English</li>
  <li>EDUC  670: Theories of Language Learning in the ESOL Classroom/MLL 670: Second Language Acquisition &amp;  Learning: From Theory to Practice</li>
  <li>EDUC 791S: ESOL  Internship Phase I </li>
  <li>EDUC 792L: ESOL  Internship Phase II (6 credits)<br>
  </li>
</ul>
<p>The K-12 Certification option includes 100  days of internship experience in public elementary and secondary schools over  two semesters. EDUC 791S and EDUC 792L replace EDUC 792S in Option 1.</p>
<p><strong><a id="option4"></a>OPTION 4 MASTER OF ARTS IN TESOL WITH THESIS AND  K-12 CERTIFICATION (51 CREDITS)</strong></p>
<p>Combining Certification with a Thesis requires fulfilling the  requirements for both options. </p>
<h4>ESOL Certificate</h4>
<p><br>
  The Graduate ESOL Certificate  is a 4-course program.<br>
  Two courses (6  credits) are required: </p>
<ul>
  <li>EDUC 688: Methods and  Techniques in Teaching ESOL</li>
  <li>EDUC  666: Cross-Cultural Communication for ESOL/MLL 625: Intercultural and Cross-Cultural Communication<br>
  </li>
</ul>
<p>For the two elective courses (6 credits), students can choose from the  following list:</p>
<ul>
  <li>EDUC 625: Teaching  Reading &amp; Writing to ESOL/Bilingual Students I</li>
  <li>EDUC 636: ESOL Testing  and Evaluation</li>
  <li>EDUC 644: Linguistics for  ESOL/Bilingual Educators</li>
  <li>EDUC 655: Teaching  Reading and Writing to ESOL/ Bilingual Students II</li>
  <li>EDUC 667: The Grammar of  American English</li>
  <li>EDUC 670: Theories of Language Learning in the ESOL Classroom/MLL 670:   Second Language Acquisition &amp; Learning: From Theory to Practice</li>
  <li>EDUC 672: Bilingualism</li>
  <li>EDUC 689: Instructional Technology  for ESOL Teachers</li>
</ul>
<p>All courses taken for the certificate can be applied to the MA in TESOL  upon admission into the MA Program if the student wishes to continue in the  field of study.</p>
