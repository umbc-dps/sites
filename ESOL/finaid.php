<h3>Financial Aid</h3>
<p>UMBC's  Department of Education has both assistantships and scholarships available for  graduate students.<br>
Current  students can apply for one of several departmental Graduate Assistantship  positions, which require 20 hours of work per week and provide tuition  remuneration, health insurance and a stipend. Graduate assistantships are  also available through the English Language Institute and other university  organizations and programs.  
<p>Department  scholarship funds are widely divided among requesting students. Consequently  many students receive scholarship support but in relatively small amounts of  money. </p>
<p>Other  sources for financial support are available. ESOL is considered a high needs  area. Federal, state and local tuition support money is available. Applicants  seeking financial support are encouraged to consult with the university's  financial support office.</p>
<p>The TESOL  program also has paid librarian positions in our Resource Center for Language  and Culture.</p>
