<h3>Course Descriptions</h3>


  <div class="accordionheader">
    <h4><strong><a id="EDUC601"></a>EDUC 601</strong><strong><br>
        <strong>Human Learning and  Cognition [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
</div>

    <div class="accordionbody">This course emphasizes major principles of human learning and  cognition viewed from empiricist, nativist and constructivist perspectives.  Topics such as conditioning, memory, information processing, motivation,  problem-solving and metacognition will be investigated systematically.  Principles of learning as applied to special populations (the gifted and  talented, the handicapped, the adult learner) and models of teaching (e.g.  discovery, inquiry, cooperative) also are emphasized.    </div>
 <div class="accordionheader">
    <h4><strong><a name="EDUC602" id="EDUC"></a>EDUC 602</strong><strong><br>
        <strong>Instructional Systems  Development I [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
 </div>

    <div class="accordionbody">
      <p>This course includes the elements of analysis, design,  development, implementation and evaluation. An emphasis is placed on  micro-level design issues, including analysis, design and evaluation. Learners  work through the ISD process to assemble a training or education project that  is ready for implementation. A design plan and lesson plan is constructed to  allow learners real-world experience in the ISD process. </p>
    </div> 
    <div class="accordionheader">
    <h4><strong><a name="EDUC625" id="EDUC2"></a>EDUC 625</strong><strong><br>
        <strong>Teaching Reading and  Writing to ESOL/Bilingual Students: Part I [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>This course presents theories, methods, and techniques for teaching reading and writing to English language learners, with a special focus on beginning literacy in children and adults. Students will demonstrate knowledge of theories of reading and writing instruction, brain research and language acquisition, as well as components of a balanced literacy program. Students will also demonstrate knowledge of early and emergent literacy including phonics and word analysis, components of a comprehensive literacy program, and assessment, as well as an awareness of the relationships between oral and written language, and ways to integrate reading, writing, speaking, and listening.</p>
    </div> 
    <div class="accordionheader">
    <h4><strong><a name="EDUC636" id="EDUC3"></a>EDUC 636</strong><strong><br>
        <strong>ESOL Testing and Evaluation  [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>This course is concerned with the theory and methodology  appropriate for ESOL and EFL testing. Course content includes an investigation  of literature containing theoretical foundations of and research for  second-language testing. Students will be expected to use research findings in  the practical application of test construction, administration and evaluation.</p>
    </div> 
    <div class="accordionheader">
    <h4><strong><a name="EDUC644" id="EDUC4"></a>EDUC 644</strong><strong><br>
        <strong>Linguistics for  ESOL/Bilingual Educators [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>This course is an introduction to the study of languages and linguistics for educators and educational researchers who deal with language learning, literacy development and communicative behavior in school settings. The purpose of the course is to provide students with enough familiarity with the major aspects of language structure and function (phonetics, phonology, morphology, syntax, semantics and pragmatics) to see how they figure in the analyses of texts and language learning, as well as to explore issues related to bilingualism and bilingual education. Throughout the course there will be opportunities to analyze language data and discuss language-related issues in various contexts. </p>
    </div> 
    <div class="accordionheader">
    <h4><strong><a name="EDUC645" id="EDUC5"></a>EDUC 645</strong><strong><br>
        <strong>Quantitative Research  Methods I [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>This is a course in the application of basic statistics in a  variety of educational research settings. Emphasis is placed on the use of  descriptive statistics, the interpretation and construction of data collection  instruments and the application of basic research paradigms.</p>
    </div> 
    <div class="accordionheader">
    <h4><strong><a name="EDUC655" id="EDUC6"></a>EDUC 655</strong><strong><br>
        <strong>Teaching Reading and  Writing to ESOL/Bilingual Students: Part II [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>This course presents theories, methods, and techniques for teaching and assessing ESL and EFL reading and writing with a special focus on developing academic literacy (reading and writing) for elementary through tertiary-level students, including content-based language instruction and the role of technology.  Activities include reviewing reading and discussing theories of academic reading and writing, reviewing reading and writing textbooks, developing and presenting academic reading and writing instruction and assessment activities, and developing an integrated unit plan (with reading, writing, and technology).  In addition to reading a series of assigned texts each week, you will also write a reflective journal entry each week which focuses on issues, concerns, and techniques in the teaching of reading and writing to English language learners and/or prepare an activity, writing prompt, lesson or unit plan, or assessment instrument for use with ESL/EFL learners.</p>
    </div> 
    <div class="accordionheader">
    <h4><strong><a name="EDUC666" id="EDUC7"></a>EDUC 666<br>
Cross-Cultural Communication for ESOL [3]</strong><i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>The purpose of this  online course is to study communication within the context of the cultural  setting. The three main goals are: 1) to provide the students with materials,  both cognitive and experimental, with which they can develop an awareness of  their own cultural identity; 2) to increase their knowledge of the special  communication problems to be expected in a cross-cultural situation; and 3) to  offer students the opportunity to apply new insights to cross-cultural  encounters. The course is an online version of MLL 625 - Intercultural and  Cross-Cultural Communication [3].</p>
    </div> 
    <div class="accordionheader">
    <h4><strong><a name="EDUC667" id="EDUC8"></a>EDUC 667<br>
The Grammar of American English for ESOL Teachers [3]</strong><i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>The course examines  the syntactic, phonological and morphological systems of modern American  English, with particular attention to areas most relevant to teachers of  English as a second or foreign language. The course focuses on English features  that are particularly difficult for English language learners. Simplified ways  of describing their features and techniques for teaching them are presented.</p>
    </div> 
    <div class="accordionheader">
    <h4><strong><a name="EDUC670" id="EDUC9"></a>EDUC 670</strong><strong><br>
        <strong>Theories of Language  Learning in the ESOL Classroom [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>This course will cover current theories of how second language  is learned, providing participants a greater understanding of the processes  involved in language acquisition and how English language learners' (ELL's)  personal characteristics influence this process. This understanding will allow  participants to effectively differentiate instruction for each ELL as well as  plan instruction and strategically utilize TESOL methodologies and techniques.</p>
    </div>
    <div class="accordionbody"></div>
    <div class="accordionheader">
      <h4><strong><a name="EDUC672" id="EDUC10"></a>EDUC 672</strong><strong><br>
          <strong>Bilingualism [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
    </div>

    <div class="accordionbody">
      <p>This course is an introduction to the social and educational  aspects of bilingualism. It offers an overview of the broad range of  sociolinguistic and political issues surrounding bilingualism, examines the  language mixing behavior of bilingual speakers, and explores the use of two or  more languages in popular music, advertising, and online social spaces. The  course covers such key topics as language maintenance and shift, attitudes  toward bilingualism, bilingual identity, multilingual educational models and  policies, and bilingual parenting.          </div>
  <div class="accordionheader">
    <h4><strong><a name="EDUC684" id="EDUC11"></a>EDUC 684</strong><strong><br>
        <strong>Qualitative Research  Methods in School and Community [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
  </div>

    <div class="accordionbody">
      <p>This course focuses on the application of selected field  research methods to problems of educational practice. Students will study  issues pertaining to the role and responsibility of the field investigator  working in schools and in other community groups. Students will plan and  conduct a field study using qualitative field techniques.</p>
    </div>
  <div class="accordionheader">
    <h4><strong><a name="EDUC688" id="EDUC12"></a>EDUC 688</strong><strong><br>
        <strong>Methodology of Teaching  English as a Second or Foreign Language [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
  </div>

    <div class="accordionbody">
      <p>The course investigates traditional and modern approaches and  techniques for teaching English as a second or foreign language; theories of  second-language acquisition/learning; curriculum and materials design of  ESOL/EFL for academic, social/survival and professional purposes.</p>
</div>
  <div class="accordionheader">
    <h4><strong><a name="EDUC689" id="EDUC12"></a>EDUC 689</strong><strong><br>
        <strong>Educational Technology for ESOL Teachers (elective) </strong></strong><i class="fa fa-angle-down"></i></h4>
  </div>

    <div class="accordionbody">
      <p>This course focuses on developing our skills in employing technology to enhance teaching and learning of English as a second or foreign language. The course surveys best practices in designing, implementing, and assessing technology-enhanced learning experiences to engage students and improve learning outcomes. The course will emphasize the NETS*T standards as written by the International Society of Technology in Education: Facilitate and Inspire Student Learning and Creativity; Design and Develop Digital-Age Learning Experiences and Assessments; Model Digital-Age Work and Learning; Promote and Model Digital Citizenship and Responsibility; and Engage in Professional Growth and Leadership. Participants will collaborate on a variety of educational technology projects, including the development of an ePortfolio, the completion comprehensive website and software analyses, the development of technology-rich thematic units, and the creation of a digital project as the capstone for the course.</p>
</div>

    <div class="accordionheader">
      <h4><strong><a name="EDUC771" id="EDUC13"></a>EDUC 771</strong><strong><br>
          <strong>Analyzing Educational  Research [3]</strong></strong><i class="fa fa-angle-down"></i></h4>
</div>
    <div class="accordionbody">
      <p>This course provides an overview of designs used in educational  research. Topics include, but are not limited to, experimental,  quasi-experimental, historical, ethnographic and phenomenological modes of  inquiry. Emphases are on the assumptions, applications, tools and procedures  associated with each of the varied designs. For example, study of experimental  and quasi-experimental design will attend to issues such as validity,  randomization and multivariate statistics.</p>
</div>
<div class="accordionheader">
      <h4><strong><a name="EDUC791s" id="EDUC14"></a>EDUC 791S<br>
ESOL Practicum in Schools [3]</strong><i class="fa fa-angle-down"></i></h4>
</div>
    <div class="accordionbody">
      <p>This course  constitutes part of Phase I of a two-phase student teaching internship for  those seeking K-12 ESOL certification. The primary purpose of this practicum is  to provide those planning to teach ESOL in the public schools with an  opportunity to observe and interact with ESOL teachers and students in the  classroom and to gain an understanding of the real world of school; of the  challenges confronting teachers, administrators and students; and of the  resources available to deal with these. In addition, students will observe how  the knowledge and skills developed in the M.A. TESOL Program can inform and facilitate teacher decision-making and practice and be  able to take a closer look at themselves as future ESOL teachers. Through a  series of readings, structured observations, interviews and seminar discussion,  students will have an opportunity to build on their current understanding of  the teaching-learning process and the roles ESOL teachers play. They also will  be able to integrate the knowledge obtained in other classes and contexts with  the practical world of teaching. In addition, through opportunities to tutor,  co-teach or present portions of lessons, the student will develop skills in  ESOL teaching.</p>
</div><div class="accordionheader">
      <h4><strong><a name="EDUC792l" id="EDUC15"></a>EDUC 792L<br>
ESOL Practicum in Schools II [6]</strong><i class="fa fa-angle-down"></i></h4>
</div>
    <div class="accordionbody">
      <p>This   course constitutes Phase II of a two-phase student teaching internship   for those seeking K-12 ESOL certification.  The course consists of an   80-day internship in Maryland public schools (40 days in an elementary   school and 40 days in a secondary school) with an accompanying weekly   seminar in which teacher candidates discuss issues related to their   student teaching experiences and prepare their teaching folios.</p>
</div><div class="accordionheader">
      <h4><strong><a name="EDUC792s" id="EDUC16"></a>EDUC 792S<br>
ESOL Internship [3]</strong><i class="fa fa-angle-down"></i></h4>
</div>
    <div class="accordionbody">
      <p>This internship is designed for students who are not pursuing K-12 ESOL certification.Students work a minimum of 40 hours in an ESOL instructional program with program supervision. A reflection paper based on this experience is the culminating project.</p>
</div>
<div class="accordionheader">
      <h4><strong><a name="EDUC794" id="EDUC17"></a>EDUC 794<br>
ISD Project Seminar [3]</strong><i class="fa fa-angle-down"></i></h4>
</div>
    <div class="accordionbody">
      <p>In this course, students analyze an educational or training problem and design a comprehensive instructional program using instructional design principles. </p>
</div>
<div class="accordionheader">
      <h4><strong><a name="EDUC799" id="EDUC18"></a>EDUC 799</strong><strong><br>
          <strong>Master&rsquo;s Thesis Research  [2-9]</strong></strong><i class="fa fa-angle-down"></i></h4>
</div>
    <div class="accordionbody">
      <p>Note: Six credit hours are required for the master&rsquo;s (with  thesis) degree program. Prerequisites: Consent of the student&rsquo;s advisor,  prerequisite courses per program map of student&rsquo;s selected concentration and  permission of the department.</p>
