<h3>Peace Corps</h3>
<p>The&nbsp;<strong>Master's  International Program</strong>,&nbsp;<strong>MI</strong>, offers MA coursework in TESOL with  an extensive teaching internship through Peace Corps experience. Students complete  18 hours of coursework at UMBC before starting a 27-month Peace Corps service.  Through completing this initial coursework, students become better prepared to  teach English in the Peace Corps. After their service, students return to UMBC  to finish their remaining MA coursework.
<p>
<p> The  master&rsquo;s program requires a minimum of 36 hours of coursework. Six of those  credits can be based on reflections from Peace Corps experience. Returning MI  students who choose the K-12 certification option are not required to write  these reflections. <br>
  <p>  
  <p>The  Peace Corps requests that you complete an academic year (18 credits) in our  program before applying to the Peace Corps. <br>
    <p>    
    <p>The UMBC MA TESOL Program,  the oldest in Maryland and one of the oldest in the United States, attracts a  diverse group of domestic and international students. UMBC also is the home to  the Shriver Peaceworker Program which brings returned Peace Corps Volunteers to  campus for a two-year service learning program integrating community service,  graduate study, and ethical reflection. The Peaceworker Program is coordinated  by the campus-based Shriver Center, established in honor of Eunice Kennedy  Shriver and Sargent Shriver and their lifelong commitment to service.              
