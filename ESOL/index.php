<p>UMBC  offers a Master's Degree program in&nbsp;<strong>Teaching English to Speakers of Other  Languages</strong>. The&nbsp;<strong>TESOL MA</strong>&nbsp;program is offered through the  Department of Education with cooperation from the Department of Modern Languages  and Linguistics.<br>
  <br>
  The  program is designed to train both prospective and experienced teachers of  English as a Second Language (ESL) and English as a Foreign Language (EFL). It  provides students with a strong theoretical and practical foundation in applied  linguistics, language acquisition, ESL/EFL methods, testing, cross-cultural  studies and curriculum development. Students in the MA program can choose to  add K-12 ESOL certification to the ESOL concentration.<br>
  <br>
  The&nbsp;<strong>Masters  International (MI)</strong>&nbsp;program  for ESOL is also administered through UMBC's TESOL MA program. This program  enables students to combine Peace Corps experience with a graduate education.<br>
  UMBC also  offers a&nbsp;<strong>Post Baccalaureate Certificate in ESOL</strong>.  The&nbsp;<strong>ESOL Certificate</strong>&nbsp;is a 4-course program.</p>
<p>Courses  for the&nbsp;<strong>TESOL MA</strong>&nbsp;and the&nbsp;<strong>ESOL  Certificate</strong>&nbsp;can  be taken in both&nbsp;<strong>face-to-face</strong>&nbsp;and&nbsp;<strong>online</strong>&nbsp;courses.</p>

    
