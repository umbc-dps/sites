<h3>International Students</h3>
<h4>Studying  at UMBC</h4>
<p>Because we  encourage international students to apply to our TESOL M.A. program, we offer  some special services and activities for international students who are accepted  into the program.</p>
<h4>Length of  the program at UMBC:</h4>
<p>The  basic program requires 12 courses to complete. Full-time students can complete  the program by taking 3 courses each fall and spring and 1 or 2 courses in the  summer. This would enable them to finish in a year and a half. Part-time  students taking 2 courses per semester would need 2 and a half years to  complete the program. Students interested in taking the certification option  should expect to take at least an additional semester to complete the program.  The internship experience requires a full academic year, and course loads in  addition to the internship should not be too demanding. Students choosing the  thesis option should plan on taking an additional semester to complete the  program.</p>
<h4>Special Assistance:</h4>
<p>To help international  students adjust to the new academic environment, an experienced graduate  student will act as an<strong>International  Student Liason</strong>&nbsp;between  international students and the TESOL M.A. to assist students with concerns they  might have, including:</p>
<ul>
  <li>logistical concerns: housing, transportation,  communication with professors, weekend events, etc.&nbsp;</li>
  <li>academic  concerns: special study sessions for various courses, individual study and  review sessions, an American English pronunciation course and other academic  support as deemed appropriate.</li>
</ul>
<h4>Housing:</h4>
<p>Although  housing is ultimately the responsibility of each individual student, the TESOL  Office will work with and assist international students in finding roommates  and housing both before they leave their home countries and after they arrive  at UMBC.</p>
<h4>Cross-Cultural Information about UMBC of interest to  International Students.</h4>
<ul>
  <li>UMBC is  approximately 40 minutes by bus from Baltimore and 1 hour by car from  Washington, D.C.</li>
  <li>UMBC's  Student Events Board and various departments on campus sponsor special movies,  lectures, discussions, trips to local and East Coast sites of interest, musical  events and drama throughout the academic year. Many of these events are free or  are specially discounted for UMBC students.</li>
  <li>The  English Language Center, the Department of Modern Languages and Linguistics,  and various international student groups sponsor special cross-cultural events  on campus throughout the year.</li>
  <li>Many of  the TESOL M.A. students meet in the Resource Center for Language and Culture  for social and study purposes. International students will be invited to join  these groups.</li>
  <li>International Students are encouraged to  participate in the ESOL club, which has activities specifically for graduate  TESOL students throughout the academic year.</li>
</ul>
<h4>Applying  to UMBC</h4>
<p>This  webpage contains information for international students applying to the MA  TESOL program at the University of Maryland, Baltimore County (UMBC).</p>
<p>Please be  sure to read and follow the <a href="http://www.umbc.edu/gradschool/admissions/apply.php" target="_blank">application directions</a>.</p>
<h4>Application  Deadline Dates:</h4>
<p>Applications  for fall admission are accepted until February 28. Applications for spring  admission are accepted until October 31.</p>
<h4>TOEFL/IELTS</h4>
<p>We urge you to take the TOEFL as soon as possible. Admissions  decisions cannot be made until the UMBC Graduate School has received the  official TOEFL score from Educational Testing Service (ETS).<br>
  <br>
  <em>NOTE:</em>&nbsp;Official TOEFL examination score  reports must be sent directly to the UMBC Graduate School from Educational  Testing Services (ETS). Photocopied score reports are not acceptable.</p>
<p>The ETS code number for UMBC is 5835.<br>
  <br>
  TOEFL:  A minimum of 213 on the computer-based test or  80 on the internet-based test is required.<br>
  IELTS: A minimum of 6.5 is  required. <br>
  <br>
  Graduate  Record Exam (GRE)<br>
  An acceptable  GRE score is required. Slightly less rigid requirements are set for  international applicants for whom more consideration is given for TOEFL scores.</p>
<p>The ETS code  number for UMBC is 5835.<br>
</p>
<h4>Oral  Proficiency for Maryland State ESOL Certification</h4>
<p>For non-native  speakers of English who are interested in working toward Maryland state ESOL  certification, near-native oral proficiency in English is required. A specific  application for the certification internship must be submitted.Â </p>
<h4>Financial  Resources</h4>
<p>Each  international student must certify that he/she has sufficient funds (exclusive  of travel expenses) for university tuition, fees, books, supplies, and living  expenses. For more information see the <a href="http://ies.umbc.edu/international-students-f-1/prospective-students/financing-your-studies/" target="_blank">International Education Services webpage</a>.</p>
<h4>Official  Transcripts</h4>
<p>An  official transcript from each college/university you have attended must be sent  directly to the UMBC Graduate School.</p>
