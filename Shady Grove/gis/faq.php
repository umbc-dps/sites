<h3>Frequently Asked Questions</h3>

  <div class="accordionheader">
    <h4>Is there a way to get admitted if my undergraduate GPA is less than the required 3.0?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Yes. If a student has a GPA slightly lower than a 3.0 but has considerable work experience, outstanding letters or recommendation, and a very strong personal statement then, that student may be considered for admission.</p>
    </div>

  <div class="accordionheader">
    <h4>Is there anything I can do if I did not get admitted but wish to reapply to the program? <i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Yes. If a student is denied admission into the program it is not necessarily a reflection on the individual student but generally has to do with our concern over that student&rsquo;s readiness to take on a graduate-level course load. One way to address that concern is for the student to take some courses through our program as a non-degree-seeking student (for more information, <a href="http://gradschool.umbc.edu/admissions/programs" target="_blank">click here</a> and scroll down to the to the link for and information on Non-degree students (special advanced student)). If the student then shows a high level of success in those courses he she may consider reapplying to the program. It would also be helpful for the student to earn la letter of recommendation from one of those instructors that would provide compelling evidence that the student is indeed ready to take on a graduate-level course load.</p>
      <p>Sometimes a student is denied admission based on extremely poor writing skills (please make sure to proof read your personal statements). If the student suspects that this was a possible cause for denial, the student may want to email the Program Director for any additional recommendations.</p>
      <p>While none of this guarantees admission into the program, these are steps that would help provide evidence for the student&rsquo;s commitment and maturity. Additionally, if the student does reapply and is admitted, up to 6 of those credits taken as a non-degree-seeking student may be applied to the graduate degree.</p>
    </div>
  <div class="accordionheader">
    <h4>What does it mean to be a non-degree-seeking student? <i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>&ldquo;Non-degree-seeking status is for students who enroll in graduate level courses but do not intend to pursue a full degree program. A minimum 3.0 Undergraduate GPA (or permission from an academic department) is required for admission.</p>
      <p>Non-degree-seeking status continues for five years from the semester of initial enrollment. You may enroll for classes during this five-year period without re-applying for admission. To re-enroll after an interruption in attendance, contact the Graduate School. If you have previously enrolled in graduate courses at UMBC in the past five years you do not have to pay an additional application fee.<br>
      </p>
      <p>Note: Non-degree-seeking students who wish to apply for a full graduate degree program should be aware that there is no guarantee that credits earned during non-degree status will apply toward a degree program. A maximum of six credit hours can transfer if approved.&rdquo; You can also learn more about different degree-seeking options <a href="http://gradschool.umbc.edu/admissions/programs" target="_blank">here</a>.</p>
    </div>
  <div class="accordionheader">
    <h4>When are the classes offered? <i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Most classes are delivered in the evening. Most run once a week for 2.5 hours during the time period between 5:00pm – 10:00pm. Some semesters we will also offer Saturday classes, which may run every week for 2.5 hours or every other week for 5 hours. To view a typical schedule, please visit our <a href="courses.php" target="_blank">schedule of classes</a>, and click on the link for the Graduate program.</p>    
      </div>
      
  <div class="accordionheader">
    <h4>Can I transfer graduate credits from another institution into the UMBC Geographic Information Systems Program?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>You may be able to transfer <strong>up to six</strong> graduate credit hours earned from another institution into a UMBC cybersecurity program. Contact the Graduate Program Director, <a href="mailto:villiger@umbc.edu">villiger@umbc.edu</a>, to discuss your situation and be sure to include a complete course description. A 3.0 GPA is required for each course transferred. Once you are admitted and an enrolled student in UMBC's program, you must complete and submit a <a href="http://gradschool.umbc.edu/students/forms">Transfer of Credit Request Form</a>.</p>    </div>
 
      
  <div class="accordionheader">
    <h4>What type of career/job search support does the program or UMBC provide upon graduation? <i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
    <p>To assist us in placing students in internships and jobs, we will have support from <a href="http://www.shrivercenter.org/" target="_blank">UMBC's Shriver Center</a>. Our students will also have access to <a href="http://www.careers.umbc.edu/" target="_blank">UMBC's Career Services</a> department and the <a href="http://www.shadygrove.umd.edu/student-services/career-and-internship-center" target="_blank">USG's Career Services</a> group. Additionally, we will have access to our Advisory Board members who may also have job opportunities through their organizations and/or may know of opportunities in related areas.</p>
    </div>
      
      
  <div class="accordionheader">
    <h4>What opportunities/careers are available with this degree? <i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
    <p>This is an applied degree, is intended for early to mid-career professionals interested in an understanding of GIS that goes beyond “GIS as a tool for analysis”. This forward-looking program is developing the next generation of GIS professionals: those that have a fundamental understanding of the importance of geography as well as an advanced knowledge of the technologies involved in the end-to-end development of advanced Geographic Information Systems.</p></div>
      



