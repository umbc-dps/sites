<div class="accordionheader">
<h4><a name="Abdullah" id="Abdullah"></a>Qassim A. Abdullah, Ph.D., PLS, CP | Adjunct Instructor <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
<p><img src="images/faculty/umbc-faculty-Qassim-Abdullah.jpg" alt="Qassim A. Abdullah" align="left" width="200"  />Dr. Qassim  Abdullah, chief scientist at Fugro EarthData, Inc., is an accomplished  scientist with more than 30 years of combined industrial, research and  development, and academic experience in analytical photogrammetry, digital  remote sensing, and civil and surveying engineering. Over the course of his career, Dr. Abdullah  has contributed significantly toward the advancement of digital aerial imagery  and LiDAR acquisition, and production processes. Among his accomplishments, Dr. Abdullah  developed proprietary software applications for use in digital orthophotography  and DEM production; he developed and integrated a metric digital aerial camera  for rapid image acquisition; he integrated airborne GPS into conventional  aerotriangulation adjustments; he refined the integration of Inertial  Navigation Systems (INS) and GPS technology for sensor positioning and  orientation; and he participated in the development of the first high altitude  topographic LiDAR system. </p>
  
  <p>A civil engineer,  ASPRS certified photogrammetrist, and professional surveyor in four states, Dr.  Abdullah currently serves as Fugro EarthData&rsquo;s chief scientist responsible for  designing and managing strategic programs to develop and implement new remote  sensing technologies that allow Fugro EarthData to meet the evolving needs of  geospatial users. He was instrumental in streamlining Fugro EarthData&rsquo;s  photogrammetric mapping processes, and most recently, has been leading the  technology transfer and process integration of Fugro EarthData&rsquo;s new panoramic  mapping and Aerial Thermal Infrared Sensing systems.</p>
  
  <p> Dr. Abdullah  obtained a bachelor&rsquo;s degree in civil engineering and master&rsquo;s degree and  doctorate degrees in Photogrammetry from the civil engineering department at  the University of Washington in Seattle.  He is affiliated with a number of national and international  professional societies, is a published author of over 50 technical papers and  reports, and is a sought-after professional speaker and educator. Besides publishing the monthly column  &ldquo;Mapping Matters&rdquo;, which appears in the ASPRS journal PE&amp;RS, he is involved  in several national committees, and participates in discussions regarding the  industry and technology forecast, future technologies, process improvement of  LiDAR and digital photogrammetry, and accuracy standards. </p>

<p>You can view Dr. Abdullah&rsquo;s public LinkedIn profile <a href="http://www.linkedin.com/in/themapsdoctor" target="_blank">here</a>.</p></div>


<div class="accordionheader">
<h4><a name="Bermudez" id="Bermudez"></a>Luis Bermudez, Ph.D. | Adjunct Instructor <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p><img src="images/faculty/umbc-faculty-Luis-Bermudez.jpg" alt="Luis Bermudez" align="left" width="200"  />Dr.  Bermudez is the Director of the Interoperability Certification at the Open  Geospatial Consortium (OGC). In this position he leads the planning and  execution of Compliance and Testing Program activities  (opengeospatial.org/compliance), serves as system architect in interoperability  program initiatives, and gives talks, tutorials and workshops worldwide about  OGC technologies, geospatial interoperability, sensor networks and geo-semantics.</p>
  
  <p>Before  joining OGC, he was the Coastal Research Technical Manager at Southeastern  Universities Research Association (SURA) where he directed projects that  advanced information technologies helping to understand the Earth phenomena,  such as climate change, inundation, coastal erosion, and harmful algae blooms.  He coordinated the technology development of the Coastal Ocean Observing and  Prediction (scoop.sura.org/) program to chain and merge information from real  time data and distributed numerical models to provide faster and more accurate  storm surge information in hurricane season.</p>
  
  <p>Before  his position at SURA, he was the Technical Lead of the Marine Metadata  Interoperability Initiative, where he led multi-institutional projects related  to coastal informatics, developed open source software and managed a Content  Management System to advance collaboration activities with more than 300  members (marinemetadata.org).</p>
  
  <p>Dr. Bermudez has a Ph.D.  and M.S. in Hydroinformatics from Drexel University and an M.S. in Industrial  Engineering from the Andes University in Bogota, Colombia. He has 15 years of  experience in software development, GIS, knowledge representation, and  international project management.</p>
  
