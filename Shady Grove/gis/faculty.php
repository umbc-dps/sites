<h4><a name="Villiger" id="Villiger"></a>Erwin Villiger, Ph.D. | Program Director <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
<p><img src="images/faculty/umbc-faculty-Erwin-Villiger.jpg" alt="Erwin Villiger" align="left" width="200"  />Dr. Erwin Villiger founded the MPS in GIS program in 2008 and has served as its Program Director since its launch. He began his career in geospatial technology with the Central Intelligence Agency (CIA) as an Imagery Analyst and Remote Sensing Scientist. During his time with the CIA, he worked to introduce GIS to the analytic workforce. In 1997, he resigned from the CIA to pursue a Ph.D. in Environmental Science at George Mason University (GMU) where he managed the GIS and Remote Sensing computer labs for the Department of Geography and Earth System Science. While at GMU, he founded Global View Inc. to provide consulting services in the geospatial arena. In 1999, he accepted a 2-year position as a Senior Scientist with Earth Satellite Corporation in Rockville, MD where he managed the Center for National Security and Policy Support. Dr. Villiger currently serves as a Research Scientist with ISciences, LLC., a sustainability science, human security, and Earth information products company.</p>
    
  <p>Dr. Villiger has taught geography and GIS courses for Johns Hopkins University and George Mason University. In addition he has developed a number of GIS education programs for professionals and workforce development. Dr. Villiger earned his BA in Geography, MS in Geographic and Cartographic Sciences with specialties in remote sensing and geographic information systems, and his PhD in Environmental Science and Public Policy.</p>
  
