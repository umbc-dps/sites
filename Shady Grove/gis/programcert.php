 <h3>Post-Baccalaureate Certificates in Geographic Information Systems</h3>
 
 <p>Consisting of five courses (15 credits), the Professional Graduate Certificate in GIS is designed to provide students with a set of core technical and analytical tools which reflect the technical interdependence of the GIS career field. Students may earn this certificate first and then choose to transfer all of the credits into the M.P.S. program.</p>

