        <h3>Course Descriptions</h3>


        <p><strong>Note:</strong> Not all courses are offered every semester, and new courses may be added at any time. Check the <a href="schedule">schedule of classes</a>, for the latest offerings. </p>


        <div class="accordionheader" style="background-color:#E0C586">
          <h4><a href="#requirements" id="requirements"></a>Required Core Courses (18 credits):</h4>
        </div>   
        
<div class="accordionbody">     
</div>



        <div class="accordionheader">
          <h4><a href="#GES670" id="GES670"></a>GES 670: Advanced Seminar in GIS <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course provides a geographic foundation vital to effective spatial systems development and an introduction to common geospatial tools including ESRI&rsquo;s ArcGIS and Google Earth. A range of topics are introduced and discussed through student projects, presentations, and guest lecturers.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#GES671" id="GES671"></a>GES 671: Spatial Database and System Design <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course provides the fundamentals of relational databases, including data modeling, database design,  database implementation and database security. It focuses on managing and working with geospatial data using the Structure Query Language (SQL). Students will also learn how data in databases can be expose on the Web. Students will have hands-on experience with different tools: Microsoft Access, ArcSDE, PostgreSQL, PostGIS, QGIS and GeoServer. Students are introduced to challenging labs to solve complex problems and improve their geospatial thinking, database and tools skills.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#GES673" id="GES673"></a>GES 673: Geoprocessing and Spatial Analysis <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course covers the manipulation and analysis of geospatial data, and focuses on automated approaches to geographic feature overlay, feature selection and analysis, topology processing, raster processing, and data conversion. This course also addresses the role of geoprocessing and spatial analysis in the definition, management, and analysis of information used to form decisions.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#GES675" id="GES675"></a>GES 675: GIS Application Development I<i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>It uses ArcGIS runtime for Java as the tool to help students gain the basic GIS desktop application development skills. It starts with Java basics and shows what an object oriented programming language is. Java as an open source and platform-independent programming language is widely used to develop powerful enterprise-wide applications. ArcGIS runtime for Java is built on top of Java SWING, a Java GUI widget toolkit. In this class, students will learn how to use Java to develop desktop GUI application. They will also learn Eclipse, the IDE tool that developers use to develop, debug, compile and deploy Java application. They will develop map applications, add layers and change layer renders. They will also develop application to do geoprocessing and perform network analysis. The course also covers geometry and projection on the fly. ArcGIS server and ArcGIS online will also be included. In the end of the class, students can develop a GIS application for other people to use.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#GES678" id="GES678"></a>GES 678: Project Management of the Enterprise GIS <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This capstone course involves advanced study and application of structured analysis and design methods throughout the GIS life cycle. The course stresses common approaches forgathering requirements, modeling, analyzing and designing geographic information systems. The course employs the case method of instruction.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#GES679" id="GES679"></a>GES 679: Professional Seminar on the Geospatial Technologies <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>These professional seminars expose students to the diversity of issues, applications, and developments in the industry. Seminars focus on a specific topic or issue of import to the geospatial industry or professional practice. Each student enrolls in three Professional Seminars over the course of their program.</p>
        </div>

        <div class="accordionheader" style="background-color:#E0C586">
          <h4><a href="#requirements" id="requirements"></a>Elective Geographic Information Systems Courses  (15 credits), Choose 4:</h4>
        </div>   
        
<div class="accordionbody">     
</div>

        <div class="accordionheader">
          <h4><a href="#GES770" id="GES770"></a>GES 770: Special Topics in Enterprise GIS <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course is to introduce the concept of geoprocessing and how to use Python to automate the process. Students will learn the basic syntax of Python, available system tools in ArcToolbox, model and ModelBuilder. They will also learn how to build a model and how to create a model tool. The core of the class is ArcPy site package. Students will work with the embedded ArcGIS Python window to write and execute scripts. They will also use the more advanced IDE tool Eclipse with PyDev plug-in. Students will write scripts using tools from ArcPy to perform various tasks, such as managing maps and layers, performing data editing, executing geoprocessing tools and creating custom geoprocessing tools. They will develop Python scripts in Eclipse. Once the Python scripts are developed, the student will learn how to convert it to a script tool so it will be available for other Geoprocessing models. In the end of the class, the students will be able to implement a geoprocessing solution for a complex geospatial problem.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#GES771" id="GES771"></a>GES 771: Advanced Spatial Data Management <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course provides advanced data management techniques that go beyond the traditional relational database management systems. The course topics include: publishing data on the web, big data, Not Only SQL (NoSQL) data, cloud computing, map tiles, and fusion tables. </p>
          
          <p><strong>Prerequisite:</strong> Spatial Database and System Design (GES 671).</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#GES772" id="GES772"></a>GES 772: Geospatial Web Services and Interoperability <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course covers the key web technologies used for publishing and orchestrating web services for geospatial data. The course provides a formal methodology for designing geospatial distributed information systems. Topics include distributed computing, OGC Web Services (WMS, WFS, WCS, CSW, SOS, WPS), workflows, clients, and visualization. Students will configure and develop tools to publish data on the web and clients for visualization of the data, using open source (e.g GeoServer, OpenLayers, Leaflet, Open Street Map, Google Maps API) and commercial (ArcGIS) software.</p>
        </div>


   

        <div class="accordionheader">
          <h4><a href="#GES773" id="GES773"></a>GES 773: GIS Modeling Techniques <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course addresses the concepts, tools, and techniques of GIS modeling, and presents modeling concepts and theory as well as provides opportunities for hands-on model design, construction, and application. The focus is given to model calibration and validation.</p>
        </div>
        
        
       <div class="accordionheader">
          <h4><a href="#GES774" id="GES774"></a>GES 774: Spatial Statistics <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course investigates statistical techniques for exploring and characterizing spatial phenomena. The course covers local/global cluster analysis, spatial autocorrelation, interpolation, kriging, as well as exposure to prominent GIS statistical packages. An emphasis is placed on exploratory spatial data analysis (ESDA) to develop spatial cognition and analytical skills with practical applications to modeling spatial phenomena in computer environments.</p>
        </div>
        
        

        <div class="accordionheader">
          <h4><a href="#GES775" id="GES775"></a>GES 775: Advanced GIS Application Development <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course is designed for students who wish to pursue advanced application development skills. Its main focus is web based GIS application development. Therefore, students need to have basic programming skills and also need to understand HTML and internet. This course have three focuses: ArcGIS Online as convenient GIS map/data services in the Cloud, GIS web application development using ArcGIS Javascript API and ArcGIS Server. The students will explore the services provided by <a href="http://www.arcgis.com" target="_blank">ArcGIS Online</a>, create a map using their own data and set up different sharing privileges. They will use ArcGIS Javascript API to develop a GIS web application. They will learn Javascript as a client side programming language, using AJAX to communicate to server, the structure of JSON and the popular dojo kit. They will use ArcGIS server to set up their own service and then use Javascript API to develop a web application to consume the service. In the end, the students will be able to build a robust enterprise GIS web application using the skills learned in the class.</p>
        </div>
        
        
        
        <div class="accordionheader">
          <h4><a href="#GES776" id="GES776"></a>GES 776: GIS Data Sources, Tasking, and Acquisition <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>In this course, the myriad sources of spatially-referenced and non-spatially-referenced data are examined. Raster, vector, and socio-economic data sources are explored including sensor networks, aerial and satellite-based collection systems, GPS, and data conversion planning. Particular attention is paid to the development of a framework within which students may judge the value of third-party geospatial data to the enterprise GIS.</p>
        </div>
        
   
        
        <div class="accordionheader">
          <h4><a href="#GES777" id="GES777"></a>GES 777: Advanced Data Modeling and Editing Techniques <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course provides students with the concepts and skills required to automate and maintain GIS feature data. It focuses on the automation of data workflow, editing processes, coordinate system reconciliation, the maintenance of topology, and the creation and maintenance of metadata. Advanced data modeling is investigated including feature-based and rule-base topology, and custom object development.</p>
        </div>
        
        
        
        <div class="accordionheader">
          <h4><a href="#GES778" id="GES778"></a>GES 778: Advanced Visualization and Presentation <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>Web technologies are providing increasingly sophisticated environments for visualization of spatial data. This course explores advanced techniques for visualizing multivariate and multidimensional data. Topics include advanced cartographic techniques, 3D, dynamic data update, and temporal modeling. Students will learn to create geospatial data-driven Web apps with modern technologies and open source software, including HTML5, JavaScript, and D3. Project-based learning will allow students to advance through the course at a pace that's tailored to their backgrounds. Although the course requires no advanced knowledge of Web technologies, students with previous programming experience will have a wider range of project options.</p>
        </div>
               
