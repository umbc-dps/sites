 <h3>Admissions: Master's Degree </h3>
 <h4>Application Dates</h4> <p>The application deadline for Fall 2016 is <strong>August 1</strong>.  <br>
   Fall semester begins: August 31, 2016</p>
 <h4>Admissions Requirements</h4>
 <ul>
   <li>B.A. or B.S. in Geography, Computer Science or Information Systems or a bachelor&rsquo;s degree in a related field with GIS-related work experience.</li>
   <li>Working knowledge of Geographic Information Systems.</li>
   <li>Minimum undergraduate GPA of 3.0 on a 4.0 scale (Students with a GPA less than 3.0  may be admitted to the MPS program provisionally or to the Graduate Certificate program.)</li>
   <li>GRE is not required. </li>
<li>International applicants should refer to the <a href="international.php">International Admission Page</a> for special admission information. </li>                 
   </ul>
 <h4>Application Process</h4>
 <p>Applications for the Master's program&nbsp;are accepted for the Fall and Spring semesters. To apply, complete a Degree-Seeking UMBC Graduate School   Application and use program name: GEOGRAPHIC INFORMATION SYSTEMS (MPS)</p>

 <ol>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_blank">Apply online</a>
     <ul>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo; section</li>
       <li>Insert names of three references into &ldquo;References&rdquo; section</li>
       <li>Submit <a href="documents/experienceform.doc">Experience Form</a> as an attachment in &ldquo;Additional Admissions Documents&rdquo; section</li>
       <li>Complete residency information to be reviewed for in-state tuition rates</li>
       </ul>
   </li>
   <li style="list-style-type:decimal">Request official transcripts from all post-secondary institutions to be sent to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or emailed to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
   </ol>
 <p>Your application should include the following   documents:  <em>(Incomplete applications will not be reviewed.)</em></p>
 <ul>
   <li>Completed application including residency form (residency form determines in-state tuition eligibility)</li>
   <li>Three letters of recommendation (if you choose to forego the online option, please see the paper form <a href="http://gradschool.umbc.edu/students/forms">here</a>)</li>
   <li>Statement of academic goals</li>
   <li><a href="documents/experienceform.doc">Experience Form</a></li>                          
   <li>Official transcript(s) from each college &amp; university attended</li>                
   <li>Non-refundable application fee ($50 online) </li>
 </ul>
 <h4>Mailing Instructions </h4>
 <p><strong>All application documents should be mailed to:</strong> <br>
UMBC Graduate School, MPS: Geographic Information Systems <br>
   1000 Hilltop Circle<br>
   Baltimore, MD 21250</p>
 <p>If you require further assistance, please contact <a href="mailto:rcjones@umbc.edu">Rickeysha Jones</a>  or call 301-738-6285 . </p>


