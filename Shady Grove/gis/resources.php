        <div class="accordionheader">
          <h4><a href="#8" id="8"></a>Where is GIS going? <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>The Department of Labor, Bureau of Labor Statistics has identified a number of high-growth industries that will place significant demands on the workforce in the near future. One of these areas is Geospatial Technologies. <br>
          </p>
          <p>Geospatial Technologies encompass a number of technologies that tie data and information to geographic locations. Satellite imagery, digital maps, the Global Positioning System (GPS), Location Based Services, spatially enabled databases, and Internet services are elements of this industry. Advances in computer technology over recent decades have significantly improved our ability to capture, compare, and visualize these data.</p>
          <p>Geographic Information Systems are increasingly integrated with more traditional information systems and with the IT infrastructure of corporations and government entities. The technologies supporting these developments are highly functional and increasingly conforming to open standards.</p>
          <p>For a look at where ESRI sees their technology going visit this site: <a href="http://www.esri.com/products/technology-topics" target="_blank">Technology Trends in GIS</a> </p>
        </div>



        <div class="accordionheader">
          <h4><a href="#9" id="9"></a>What makes this program unique? <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This Masters in Professional Studies in Geographic Information Systems offered by UMBC is unique in a number of ways.</p>
          <ul>
            <li>Explicitly developed courses</li>
            <li>Designed to meet the current and future needs of the geospatial industry</li>
            <li>Different from traditional GIS and technical Geography degrees</li>
            <li>Focused on IT integration of GIS:  &ldquo;Enterprise GIS&rdquo;</li>
          </ul>
          
          <h5>Explicit Development</h5>
          <p>This master program has been explicitly developed to conform to the <a href="http://www.aag.org/galleries/publications-files/GIST_Body_of_knowledge.pdf" target="_blank">Geographic Information Science &amp; Technology: Body of Knowledge</a> developed by the <a href="http://www.aag.org/" target="_blank">AAG</a> and <a href="http://www.ucgis.org/" target="_blank">UCGIS</a>. The GIS&amp;T BOK is the result of a tacit recognition that current avenues for learning GIS and related technologies are not serving the needs of the workforce. The BOK provides a collection of the core knowledge required to be successful in the developing geospatial industry.  All of the courses in the program are designed following the content of the BOK and to conform to curriculum guidelines it engenders. Courses are also designed to development of practical skills that conform to the Geospatial Technology Competency Models developed through the Geospatial Workforce Development Center. <br>
          </p>
          <h5>Meeting the Needs of Industry</h5>
          <p>This program has been developed following considerable consultation with local industry. A considerable effort to discover the needs of local businesses and government agencies was engaged in the early stages of development. The results of that survey informed course selection, the format for delivery, and the professional developments of the program.<br>
          </p>
          <p>This program is being built from the ground up to meet the needs that industry has identified. It is not hampered by legacy curriculum and can specifically adopt the latest guidelines in this respect. It focuses on the direction the industry is going with less reliance on individual GIS analysts solving problems and more on the development of systems where most of the functions and capabilities are developed on the systems side.</p>
          
          
<h5>Different from other Masters in GIS or Geography</h5>

          <p>Many recent programs focused on GIS utilize their existing geography courses and instructors to tailor (actually repackage) their courses as something appropriate for GIS professionals. This results in disconnected courses without a clear relationship to GIS as a technology or even specific elements of GIS in an enterprise context. These programs are still valuable but perhaps more accurately characterized as a technical geography masters. <br>
          </p>
          <p>The UMBC MPS in GIS reflects a paradigm shift in GIS. Until relatively recently, GIS has consistently been considered a valuable tool for analysis, especially of spatial phenomena. Analysts &ldquo;build&rdquo; a GIS to address and analyze a specific problem or perform an ongoing task. Such programs are not focused on the development of an Information System for leveraging the value of geographic information on an ongoing basis. Often as an integral part of an organizations business practices.<br>
          </p>
          <p>If you are interested in developing advanced technology skills with regards to GIS, this is the program from you. If you are interested in GIS from the project/program manager perspective and being involved in the development of GIS systems, this is the program for you. <br>
          </p>
          <p>You will develop significant analytical skills and capabilities as well as rounding out your professional skills in our program but you will also have a much deeper understanding of the system from beginning to end. <br>
          </p>
