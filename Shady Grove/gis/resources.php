          <h4><a href="#1" id="1"></a>What is GIS? <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>A GIS is a computer system capable of capturing, storing, analyzing, and displaying geographically-referenced information; that is, data that can be tied to some specific location. Any information that has position, size, or shape is known as spatial data. Generally this refers to land-related information, such as property details, man-made structures, natural resources, the environment, land use, civil infrastructure, and demographics. The potential content, however, is almost endless. The power of GIS lies in the ability to visualize and analyze database information in a spatial environment. The results typically appear as data that may be presented as a map though this in not always the case.</p>
          
          <p>While GIS and related technologies have been around for some decades, it is only with the development of more powerful computers and more intuitive interfaces that they are finally coming into the mainstream. An increasing volume of data that can be tied to geographic location is available for populating a GIS. While natural resource disciplines have historically been the principle users of these tools, they are now being applied to a wide range of areas including:</p>
          
          <ul>
            <li>Emergency services</li>
            <li>Facilities management</li>
            <li>DOD and Intelligence issues</li>
            <li>Human health and medical analysis</li>
            <li>Transportation</li>
            <li>Business marketing and analysis</li>
          </ul>
          
          <p>GIS technologies are growing in use and utility. Newer tools are being integrated into the technology infrastructure of large organizations and increasingly employees from a variety of disciplines are being asked to learn about GIS and its related technologies. Entire workforces are requiring significant retraining not only in the software, but in the underlying principles of spatial data and analysis. Consistent, effective training programs do not yet exist for this segment of the marketplace.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#2" id="2"></a>What are Geographic Information Systems (GIS)? <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>GIS is an information technology that allows the display and analysis of information based upon geographic location. One could think of them as advanced mapping systems that do more than just produce maps. They provide a powerful set of tools that allow us to ask complex questions about our world. It is important to understand that GIS are not strictly software packages. A GIS is an “Information System” specially designed to handle geographic information.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#3" id="3"></a>Who uses GIS? <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>GIS is a technology that in many ways is very similar to statistics. Statistics are used by many disciplines to analyze numbers. A GIS can be used by almost any discipline that is interested in objects or events that exist or occur on or near the surface of the earth. If information can be tied to a geographic location (often referred to as spatial data) it can be analyzed in a GIS. </p>
        </div>


        <div class="accordionheader">
          <h4><a href="#4" id="4"></a>How is GIS used?<i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>GIS is used by businesses interested in analyzing market areas or where to establish new ventures, environmental scientists to determine the impact of pollutants on an area, epidemiologists to track how a disease is spreading through a population or region of a country, or military forces to determine the best route. The ways a GIS may be used are almost endless.<br>
          </p>
        </div>


        <div class="accordionheader">
          <h4><a href="#5" id="5"></a>How is GIS changing?  <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>With recent advances in computing power and information technologies (IT), GIS has become more available and more broadly integrated into the IT infrastructure of organizations. It is increasingly being applied as a web based tool for providing information in a geographic fashion. Examples of this can be found at websites such as www.mapquest.com for road maps and address searches or interactive mapping for the location of recent crime events in.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#6" id="6"></a>What does a GIS consist of? <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>A GIS is a type of information system that deals with data tied to a real world geographic location (spatial data). There are a number of components that collectively make up the system including hardware, software, and spatial data. One of the most important elements however are the people involved - analysts, database specialists, application developers etc. These people need to have a fundamental understanding of GIS principles, spatial data issues, and the topic or question the tool is being applied against to make the most effective and rigorous use of GIS.</p>
        </div>
        
        
        
