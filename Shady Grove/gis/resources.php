            <li>Provide applications development and programming services, spatial database administration and development, computing systems administration and analytical support;</li>
            <li>Design and develop efficient program logic for the enhancement of existing systems, including appropriate system documentation; preparing test cases and carrying out unit integrations to ensure that modified systems perform correctly;</li>
            <li>Analyze systems documentation, program logic and manuals, including on-site review of user procedures to achieve a thorough understanding of the various systems functions;</li>
            <li>Coordinate projects with peers, supervisors and management to ensure that project and departmental objectives are met.</li>
          </ul>
