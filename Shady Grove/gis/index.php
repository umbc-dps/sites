<h3>UMBC Geographic Information Systems Graduate Programs</h3>

      <p>The Geographic Information Systems (GIS) Program at UMBC-Shady Grove in Rockville, MD offers a Master of Professional Studies in GIS and a Post-Baccalaureate Certificate in GIS. UMBC&rsquo;s program is unique with its focus on the information systems and computer science aspects of GIS. It provides an advanced level of education to professionals working or interested in the region's geospatial technology industry. This program focuses on foundation GIS principles while highlighting the latest developments in the geospatial industry, including: spatial databases, integration of geospatial tools with information systems, advanced spatial analysis and modeling, web-based data exploration and visualization, geospatial web-services and cloud computing, GIS desktop and server programming, and application development.</p>
      <p>Upon completion, students will possess an advanced knowledge of the information systems, computer science, and analytical aspects of the field of GIS. Students will utilize the knowledge, tools and techniques of database management, application development, and analytical assessment to appropriately address geographic information requirements and solve spatial problems.</p>

      <h3>Why Geographic Information Systems?</h3>

      <ul>
        <li>Geographical Information Science is one of the fastest expanding areas in information technology.</li>
        <li>With an estimated growth rate of 35-40 percent per year, the GIS job market is in need of qualified and experienced professionals to help design, implement, operate and manage geographic information systems.</li>
        <li>A background and knowledge base in GIS can work in a variety of fields including energy, public health, urban planning, civil engineering, environmental science, archaeology and landscape architecture. </li>
      </ul>


    <h3>Why UMBC?</h3>

      <ul>
        <li>UMBC's Geographic Information Systems Graduate Programs are led by a distinguished faculty of practitioners in the field.</li>
        <li>Curriculum covers all aspects of the GIS profession: web development and cloud computing, GIS application development, spatial databases, and enterprise systems implementation and management. </li>
        <li>Classes are offered at UMBC-Shady Grove in Rockville, MD, which is conveniently located near Baltimore, Washington D.C. and Northern Virginia. Classes are offered in a flexible format, designed for busy working professionals, with hybrid and in-person classes offered in the early evening.</li>
      </ul>

    
