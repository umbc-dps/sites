 <h3>Meet Nidhi Mittal, UMBC Cybersecurity MPS Alumna, 2013</h3>
 
<p><img src="images/alumni/UMBC_CyberAlum2.gif" alt="UMBC Cybersecurity Alum Andrew Blair" align="left" style="padding:5px">Nidhi  Mittal, who has a bachelor&rsquo;s degree in information systems, was working for the  U.S. Department of Defense when she decided that exploring the technical and  managerial side of Cybersecurity could help her excel in her field. </p>

       <p>&ldquo;I chose to attend the Cybersecurity program  at UMBC because of the diversity in classes the program offered,&rdquo; Mittal said.  &ldquo;This program allows you to gain leadership skills, as well as the technical  skills you need to succeed in the cybersecurity world.&rdquo; </p>
       
       <p>For Mittal, the  most valuable components of her experience earning her <a href="http://www.umbc.edu/shadygrove/cyber/">Master's of Professional Studies (MPS) in Cybersecurity</a> at UMBC were the professors and their  ability to meet student interests and address emerging topics in the field.</p>
       
       <p>&ldquo;They  offered a wide variety of experience both in the public and private sector,  which helped tie cybersecurity back to how it applies to work and what you do on  a daily basis.&rdquo;</p>

 <p>   Mittal also appreciated that the in-person classroom allowed  students to not only interact with their peers, but also engage professors on a  one-on-one basis. &ldquo;I felt like you could have a conversation with them,&rdquo; she  commented. </p>
 
<p>With flexible scheduling, including weekend and evening  classes, UMBC's Cybersecurity Programs offer professionals like Mittal the perfect opportunity to  develop educationally and professionally in a way that is immediately  recognizable on the job. </p>

<p>&ldquo;Upper management recognizes the importance of this  degree,&rdquo; Mittal explained. &ldquo;They are aware that I can now understand technical  components that are involved in our projects, and that&rsquo;s an added bonus in  working with developers on a daily basis and fulfilling customer requirements.&rdquo;</p>

 <p>Since  earning her degree, Mittal has been promoted to a managerial position. She  credits the Cybersecurity program for helping her grow both &ldquo;as an individual  and as a leader&rdquo; and recommends the program to anyone in a technical  field.   </p>
 
