        <div class="accordionheader">
          <h4><a href="#CYBR620" id="CYBR620"></a>CYBR 620 : Introduction to Cybersecurity <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course introduces students to the interdisciplinary field of cybersecurity by discussing the evolution of information security into cybersecurity, cybersecurity theory, and the
          relationship of cybersecurity to nations, businesses, society, and people. Students will be exposed to multiple cybersecurity technologies, processes, and procedures, learn how to analyze
          the threats, vulnerabilities and risks present in these environments, and develop appropriate strategies to mitigate potential cybersecurity problems.</p>


          <p><strong>Prerequisite:</strong> Enrollment in the CYBR program or in at least the second semester of graduate study. Other students may be admitted with instructor permission.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#CYBR621" id="CYBR621"></a>CYBR 621: Cyber Warfare <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course addresses some of the unique and emerging policy, doctrine, strategy, and operational requirements of conducting cyber warfare at the nation-state level. It provides students
          with a unified battlespace perspective and enhances their ability to manage and develop operational systems and concepts in a manner that results in the integrated, controlled, and
          effective use of cyber assets in warfare.</p>


          <p><strong>Prerequisite:</strong> Enrollment in the CYBR program or in at least the second semester of graduate study. Other students may be admitted with instructor permission.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#CYBR622" id="CYBE622"></a>CYBR 622: Global Cyber Capabilities and Trends <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course focuses on four general areas of cyber capabilities and trends in the global community: the theory and practice of cybersecurity and cyberwar; cyber capabilities of
          nation-states as well as non-state actors; trends in cyber-related strategies and policies; and cyber-related challenges facing the U.S. government. The course concludes with a national
          cybersecurity policy exercise that helps demonstrate the challenges and complexities of the dynamic and global cybersecurity environment.</p>


          <p><strong>Prerequisite:</strong> Enrollment in the CYBR program or in at least the second semester of graduate study. Other students may be admitted with instructor permission.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#CYBR623" id="CYBR623"></a>CYBR 623: Cybersecurity Law and Policy<i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>Students will be exposed to the national and international policy and legal considerations related to cybersecurity and cyberspace such as privacy, intellectual property, cybercrime,
          homeland security (i.e., critical infrastructure protection) and cyberwarfare, and the organizations involved in the formulation of such laws and policies. Broader technology issues also
          are discussed to demonstrate the interdisciplinary influences and concerns that must be addressed in developing or implementing effective national cybersecurity laws and policies.</p>


          <p><strong>Prerequisite:</strong> Enrollment in the CYBR program or in at least the second semester of graduate study. Other students may be admitted with instructor permission.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#CYBR624" id="CYBR624"></a>CYBR 624: Cybersecurity Project <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This is the capstone experience for graduate students in the M.P.S. Cybersecurity program. Normally taken in the final semester before graduation, the Cybersecurity Project provides an
          opportunity for students to carry out an individual piece of research (or project activity) on a specified topic in the cybersecurity or cyber operations domain. Their work should make an
          original contribution to the body of knowledge in the area of study or otherwise demonstrate the student’s comprehensive knowledge of cybersecurity or cyber operations.</p>


          <p><strong>Prerequisite:</strong> Enrollment in CYBR MPS program and completion of at least CYBR 620, CYBR 623, CYBR 650.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#CYBR644" id="CYBR644"></a>CYBR 644: Cyber Practitioner Development Lab</h4>
        </div>


        <div class="accordionbody">
          <p>This course provides an opportunity for students to carry out applied technical cybersecurity activities that prepares them for professional certification examinations required by the
          cybersecurity industry. It allows students to put into supervised practice the theoretical concepts provided elsewhere in the CYBR curriculum. Course topics and syllabi will vary from
          semester to semester but may include applied security, network administration, and other operational topics.<br>
          <br>
          <strong>Prerequisites:</strong> CYBR MPS Enrollment and completion of CYBR 620.&nbsp; Depending on the course(s) offered, additional technical pre-requisites will be indicated.<br>
          <br>
          <strong>Note:</strong> Course offered at UMBC Training Centers.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#CYBR650" id="CYBR650"></a>CYBR 650: Managing Cybersecurity Operations<i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course takes an operational approach to implementing and managing effective cybersecurity in highly networked enterprises. Topics include an evaluation of government and commercial
          security management models; security program development; risk assessment and mitigation; threat/vulnerability analysis and risk remediation; cybersecurity operations; incident handling;
          business continuity planning and disaster recovery; security policy formulation and implementation; large-scale cybersecurity program coordination; management controls related to
          cybersecurity programs; information-sharing; and privacy, legal, compliance, and ethical issues.</p>


          <p><strong>Prerequisite:</strong> Completion of CYBR 620 and in at least the second semester of graduate study. Other students may be admitted with instructor permission.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#CYBR658" id="CYBR658"></a>CYBR 658: Risk Analysis and Compliance <i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>This course focuses the student on a broad range of topics relative to risk-based planning for enterprise cybersecurity. The intent is focusing on creating risk assessment and modeling
          approaches to solve cybersecurity issues so that organizations can build security framework and sustain a healthy security posture. This course analyzes external and internal security
          threats, failed systems development and system processes and explores their respective risk mitigation solutions through policies, best practices, operational procedures, and government
          regulations. Risk frameworks covered include NIST SP 800-12, SP 800-37, SP 800-39, and CERT/CC risk analysis guidelines.</p>
        </div>


        <div class="accordionheader">
          <h4><a href="#CYBR691" id="CYBR691"></a>CYBR 691: Special Topics in Cybersecurity<i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>Courses on specialized or emerging cybersecurity topics offered on a timely or as-needed basis ranging from mobile device security and reverse engineering to secure software development,
          military operational cyber planning, and other specialized items.&nbsp; Prerequisite: Completion of CYBR 620 and in at least the second semester of graduate study. Course-specific
          prerequisites may be required by the instructor. Other students may be admitted with instructor permission.<br>
          <br>
          <strong>Prerequisites:</strong> Completion of CYBR 620 and in at least the second semester of graduate study. Course-specific prerequisites may be required by the instructor. Other students
          may be admitted with instructor permission.<br></p>
        </div>


   

        <div class="accordionheader">
          <h4><a href="#ENMG652" id="ENMG652"></a>ENMG 652: Management, Leadership, and Communication<i class="fa fa-angle-down"></i></h4>
        </div>


        <div class="accordionbody">
          <p>Students learn effective management and communication skills through case study-analysis, reading, class discussion and role-playing. The course covers topics such as effective
          listening, setting expectations, delegation, coaching, performance, evaluations, conflict management, negotiation with senior management and managing with integrity.</p>
        </div>
