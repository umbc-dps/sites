<h3>Admissions: Post-Baccalaureate Certificates </h3>
<h4>Application Dates</h4><p>The application deadline for Spring 2017 is <strong>December 1</strong>.  <br>
   Spring semester begins: January 30, 2017</p>
 <h4>Admissions Requirements </h4>
 <p>There are no specific constraints on the type of undergraduate degree required for this certificate. In general, applicants should understand the basic concepts and operation  of the Internet, networked information systems, and have a basic familiarity with information security concepts and/or practices. These general requirements usually can be satisfied with one or more courses in related areas or through documented professional experience. Those with a background in computer science or information systems, or those with significant professional experience in the IT industry, are encouraged to apply.</p>
 <ul>
   <li>GRE scores are not required for admission</li> 
   <li>Applicants should have a minimum undergraduate GPA of 3.0 on a 4.0 scale   (Applicants with an appropriate level of knowledge and professional experience   in the IT or cybersecurity fields are strongly encouraged to apply.) </li>
   <li>International students who earned a degree outside of the U.S. are required   to submit GRE scores and TOEFL scores.</li>
     <ul>
       <li>Graduate Record Exam (GRE) scores requirements: Minimum combined score of   1230. Verbal Reason should be at least 500 and Analytical Writing at least 4.5.</li>
       <li>TOEFL score requirements: 597 (Written), 247 (computerized), 99 (iBT).   Scores must be less than 2 years old.</li>
       <li>iBT Score Breakdown: Writing (23), Listening (23), Reading (25), Speaking   (28, 23 acceptable if have 2 years' work experience in supervisory or managment   position in the U.S.)</li>
       <li>IELTS Score:&nbsp; Minimum score of 7.5 required</li>
     </ul>
   </li>
 </ul>
 
 <h4>Application Process</h4>
 <p>Applications for the post-baccalaureate certificates are accepted for the Fall &amp; Spring semesters. To apply, complete a UMBC Graduate Application for Post-Baccalaureate Certificate Programs.</p>
 <ol>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_blank">Apply online</a> - Select Application for Post-Baccalaureate Certificate Program</li>
        <ul>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo; section</li>
       <li>Recommendations are optional. Please put N/A in these fields if you choose not to enter names of references.</li>
       <li>Complete residency information to be reviewed for in-state tuition rates</li>
       </ul>
   <li style="list-style-type:decimal">Request unofficial transcripts from all post-secondary institutions to be sent to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or emailed to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
   <li style="list-style-type:decimal">Submit resume to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a> or <a href="mailto:rcjones@umbc.edu">rcjones@umbc.edu</a>.</li>
   </ol>
 </ol>
 <p><strong>The following information is required to process your application:</strong></p>
 <ul>
   <li>Completed application including residency form (determines in-state tuition   eligibility)
     <ul>
       <li>Use Program code <strong>PSCB </strong>for Post-Baccalaureate Certificate in Professional Studies: Cybersecurity Strategy & Policy</li>
       <li>Use Program code <strong>PSCO</strong> for Post-Baccalaureate Certificate in Professional Studies: Cybersecurity Operations</li>
     </ul>
   </li>
   <li>Statement outlining the student's goals and expectations in the program.    This is an opportunity for applicants to further explain their background   and qualifications for the program and provide any additional information about   themselves or their career experience that they feel will benefit the evaluation   of their application.</li>
   <li>Current resume</li>  
   <li>Unoffical transcript(s) from each college university attended</li>
   <li>Non-refundable application fee ($50 online) </li>
 </ul>
 
