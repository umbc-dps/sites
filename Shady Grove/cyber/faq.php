<h3>Frequently Asked Questions</h3>

  <div class="accordionheader">
    <h4>How does a Master in Professional Studies degree (M.P.S.) differ from a Master of Science degree (M.S.)?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>The M.P.S. is a master’s degree that combines management and courses with advanced study in a scientific or technical discipline.  The M.P.S.  Cybersecurity incorporates management and policy courses with more technical courses in order to prepare you for the human, political, and technical aspects being a cybersecurity professional.  Rather than preparing you to perform pure research and/or pursue an academic career, this degree program is designed to help you advance your professional career. </p>
      <p>When developing this program, our Cybersecurity Advisory Board emphasized the need for educated employees who are both technically proficient and can communicate well, manage projects, and work successfully on teams.   Accordingly, several classes include relevant group projects conducted both in-person as part of their syllabus. Thus, the M.P.S. combines the practical expertise of an M.B.A. with the technical knowledge of an M.S. degree.</p>
    </div>

  <div class="accordionheader">
    <h4>I have a CISSP or other industry cybersecurity certificate. Can it transfer into the M.P.S. or post-baccalaureate certificate program for credit?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Unfortunately, we do not accept industry certifications for transfer credit into our cybersecurity graduate programs. However, UMBC can assist you in obtaining such non-credit certifications through the UMBC Training Center if you are interested.</p>    </div>
  <div class="accordionheader">
    <h4>Does UMBC offer a cybersecurity Ph.D. program?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Not at this time. However, UMBC does offer postgraduate programs in computer science and/or information systems where you can pursue cybersecurity research at the doctoral level. Contact these departments for additional information. (Courses taken within the cybersecurity M.P.S. may be applied towards those programs in some situations.)</p>    </div>
  <div class="accordionheader">
    <h4>How are UMBC's cybersecurity programs courses delivered?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>UMBC's cybersecurity program courses are offered in two main formats—traditional classroom delivery and hybrid courses. Classroom courses provide opportunities for rich interaction between instructors and students, and hybrid courses add the benefit of schedule flexibility by reducing the number of in-person class sessions. Hybrid courses replace up to 50% or more of class sessions with online activities that occur asynchronously (class does not convene online at a fixed time). In the online portions of a hybrid class, students work on self-paced activities and assignments (e.g., asynchronous discussion boards) or interact as part of small groups (e.g., discussions, chats/forums, project/assignments).</p>    </div>
  <div class="accordionheader">
    <h4>Can I transfer graduate credits from another institution into the UMBC Cybersecurity Program?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>You may be able to transfer <strong>up to six</strong> graduate credit hours earned from another institution into a UMBC cybersecurity program. Contact the Graduate Program Director, <a href="richard.forno@umbc.edu">richard.forno@umbc.edu</a>, to discuss your situation and be sure to include a complete course description. A 3.0 GPA is required for each course transferred. Once you are admitted and an enrolled student in UMBC's program, you must complete and submit a <a href="http://gradschool.umbc.edu/students/forms">Transfer of Credit Request Form</a>.</p>    </div>
 
      
