      <h3>Meet Andrew Blair, UMBC Cybersecurity MPS Alumnus, 2013</h3>


      <p><img align="left" alt="UMBC Cybersecurity Alum Andrew Blair" src="images/alumni/UMBC_CyberAlum1.gif" style="padding:5px">After earning his undergraduate degree in Business Administration and working in finance for several years, Andrew Blair was looking for a change. Although he once viewed a job in finance as a &ldquo;dream job,&rdquo; Blair found that the industry wasn&rsquo;t as exciting—or stable—as he had hoped for. &ldquo;I wanted something that I really liked to do,&rdquo; Blair recalls. &ldquo;I was getting up in the morning and didn&rsquo;t want to go to work.&rdquo;</p>


      <p>Then, a radio advertisement for UMBC&rsquo;s Cybersecurity Graduate programs prompted him to think about a career in the cybersecurity field. &nbsp;Blair applied to the program, and began his journey down a new career path.</p>


      <p>UMBC&rsquo;s in-person <a href="programmaster.php">Cybersecurity Master&rsquo;s in Professional Studies</a> (MPS) program offered Blair, who had always been interested in computers, the perfect opportunity to gain experience, network with other students, and leverage his previous experience toward a range of prospects within cybersecurity and cyber operations.</p>


      <p>&ldquo;A lot of the people in the program have jobs in all kinds of fields. You can really get an idea of where you want to take your career once you&rsquo;ve finished your degree,&rdquo; Blair said.</p>


      <p>Halfway through working on his degree, Blair secured a position as a Systems Support Technician at UMBC Training Centers, where he applied what he learned daily to his position. &ldquo;Especially policy and project management,&rdquo; he said. Today, Blair works as a Technical Consultant at BoxTone Inc. As an active cybersecurity professional, Blair particularly appreciated that the program&rsquo;s courses were developed and taught by current practitioners who drew on practical situations and experiences during instruction. &ldquo;It was really great to have professors with real-world experience teaching us,&rdquo; Blair noted. &ldquo;You could run issues by them, talk to them after class, and get real-world examples of what you were learning.&rdquo;</p>


      <p>Since earning his MPS from UMBC in May 2013, Blair has received a promotion and gained more responsibilities at work, including managing security projects and contributing to important discussions about security options.</p>


      <p>&ldquo;It has been a really great experience for me—to grow as much as I have in the last two-and-a-half years since I started the program, make this career change, and change my life.&rdquo;</p>


