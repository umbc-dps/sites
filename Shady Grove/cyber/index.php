<h3>UMBC Cybersecurity Graduate Programs</h3>

      <p>UMBC offers a variety of master&rsquo;s degree and certificate options. Our cybersecurity graduate programs leverage a student&rsquo;s experience toward a range of opportunities within the cybersecurity profession. UMBC's cybersecurity programs are designed to prepare computer science, information systems, and other experienced professionals to fill management and leadership roles in cybersecurity and cyber operations.</p>

      <h3>Why Cybersecurity?</h3>

      <p>Cybersecurity has emerged as a critical domain of global competition that reaches across the social, economic, political and military realms of influence.</p>
      <p>
      The infusion of the Internet and its related networked technologies into nearly every aspect of society, business, and government presents a target of opportunity for adversaries. As a result, nations and international organizations are developing new operational doctrines, advanced cyberwarfare capabilities, cybersecurity enhancements and the necessary domain-focused human capital needed to achieve or maintain their interests in cyberspace.</p>

    <h3>Why UMBC?</h3>

      <p>UMBC provides technical  and managerial training education to respond to Maryland&rsquo;s and the nation&rsquo;s needs  for qualified cybersecurity professionals.</p>
      <ul class="square">
        <li>UMBC is one of the few universities dual-designated as an <a href="https://www.nsa.gov/ia/academic_outreach/nat_cae/institutions.shtml" target="_blank">NSA/DHS Center of Academic Excellence for Information Assurance Education & Research</a>.</li>
        <li>Students are eligible to participate in federal scholarship programs,  including the NSF Federal Cybercorps, Scholarship For Service (SFS) and DOD  Information Assurance Scholarship Program (IASP). Open to US citizens only.</li>

        <li>UMBC at The Universities at Shady Grove is conveniently located in Rockville, MD, near I-270. The location is convenient to a host of federal agencies and government contractors in Montgomery County and the Washington D.C.-area that need cyber-related research, education, and training.
        </li>
        <li>UMBC  provides research leadership at the National  Cybersecurity Center of Excellence's Federally Funded Research and Development Center  (FFRDC) in Rockville, MD.</li>
        <li>Students can take advantage of ongoing professional development,  networking, and career opportunities through UMBC's Career Services Center and  our academic departments.</li>
      </ul>
    

