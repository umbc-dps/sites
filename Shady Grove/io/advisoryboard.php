  <p><img align="left" alt="Alex Alonso" src="images/advisoryboard/alonso.jpg">Alexander Alonso, PhD, SHRM-SCP is the Society for Human Resource Management's (SHRM's) Senior Vice President for Knowledge Development and currently serves as the head of examination development and operations for SHRM’s Certified Professional and Senior Certified Professional certifications.  He is responsible for all research activities including the development of the SHRM Competency Model.  He oversees the teams responsible for research products like People Insight engagement tools, SHRM Benchmarking Services, and the Workplace and Employment Trends Center.</p> 

