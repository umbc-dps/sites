<h3>I/O Psychology Resources</h3>

<p>One great tool for students to enhance their professional development is joining relevant professional organizations.  Such organizations will sponsor meetings and conferences both monthly and annually.  These events not only provide quality presentations on salient topics, but also offer a forum for networking that might be helpful for internship or employment opportunities.  In almost all cases, attending conferences will earn continuing education credits that will be needed for keeping certifications current.  Each organization will have updated job boards of open positions.  Membership rates are frequently reduced for students. Members of our <a href="advisoryboard.php" target="_blank">Advisory Board</a> are often involved in these organizations including serving in leadership positions.</p>

<p>Here are some recommended organizations to explore:</p>


<div class="accordionheader">
  <h4>American Psychological Association (APA) <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>The <a href="http://www.apa.org/" target="_blank">APA</a> is the most established organization for the discipline of Psychology.  There are many specialty divisions including SIOP (Division 14).  The annual conference includes papers and presentations from all of these niche areas including a section for Division 14.</p> 
</div>
    
<div class="accordionheader">
  <h4>Association for Training and Development (ATD) <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>The <a href="http://www.td.org/" target="_blank">Association for Talent Development</a> is a professional membership organization supporting those who develop the knowledge and skills of employees in organizations around the world. The association was previously known as the American Society for Training and Development (ASTD).</p> 
</div>

<div class="accordionheader">
  <h4>Chesapeake Bay Organization Development Network (CBODN) <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p><a href="http://www.cbodn.org/" target="_blank">CBODN</a> is the premier DC Metro area network and community of practice of Organization Development (OD) practitioners. Founded in Chesapeake Bay, MD, our name still pays homage to our founders, and we are grateful for their brilliant minds and vital contributions to the field of OD.  The organization holds regular events throughout the year including an annual conference.</p> 
</div>

<div class="accordionheader">
  <h4>Chesapeake Human Resources Association (CHRA) <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p><a href="http://www.chra.com/" target="_blank">CHRA</a> is the Baltimore area affiliate of SHRM.  The organizations have two large conferences a year in the Spring and Fall (dedicated to Employment Law updates).  CHRA also has monthly networking events as well as study classes for HR certifications.</p> 
</div>

<div class="accordionheader">
  <h4>Human Resource Certification Institute (HRCI) <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p><a href="http://www.hrci.org/" target="_blank">HRCI</a> offers different levels of industry-respected HR certifications including aPHR, PHR, SPHR, and GPHR.  These are often desired or required credentials for HR jobs.</p> 
</div>

<div class="accordionheader">
  <h4>Montgomery County SHRM <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>This is the local <a href="http://mcshrm.shrm.org/" target="_blank"> SHRM chapter</a> of which UMBC is currently a sponsor.  The organization sponsors regular educational and networking events.</p> 
</div>

<div class="accordionheader">
  <h4>Personnel Testing Council of Metropolitan Washington <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p><a href="http://www.ptcmw.org/" target="_blank">PTC</a> is one of the most established I/O organizations in our region.  They hold regular events throughout the year in the DC area.  Once a year, UMBC hosts a panel once a year from PTC for early career I/O Psychologists at USG.</p> 
</div>

<div class="accordionheader">
  <h4>Society of Human Resource Management (SHRM) <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p><a href="http://www.shrm.org/" target="_blank">SHRM</a> is the foremost professional organization for Human Resource professionals.  Many of our faculty and Advisory Board Members work in HR positions and students will often find themselves seeking jobs in HR.  The website has many useful resources and job listings.  The organization has a national conference each year with an attendance of about 20,000.  SHRM also offers HR certifications such as the SHRM-CP and SHRM-SCP.</p> 
</div>

<div class="accordionheader">
  <h4>Society of Industrial and Organizational Psychology (SIOP) <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p><a href="http://www.siop.org/" target="_blank">SIOP</a> is the premier international I/O organization and an affiliate of the APA.  The keynote event is the Annual Conference held in different cities each year.  There are various add-ons to the conference including workshops and consortia for students.  The SIOP website has much useful information by way of publications, salary surveys, and more.</p> 
</div>

<div class="accordionheader">
  <h4>USG Student SHRM Chapter <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>Universities at Shady Grove is in the process of reconstituting its student chapter with members across USG schools and programs.  For more information, please contact <strong>Elicia Moran</strong> at <a href="mailto:eliciamoran@gmail.com">eliciamoran@gmail.com</a>.</p> 
</div>

<br>

<p>A comprehensive list of organizations published by SIOP can be found at <a href="www.siop.org/IOGroups.aspx" target="_blank">www.siop.org/IOGroups.aspx</a>.</p>

