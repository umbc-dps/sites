   <p>The Post-Baccalaureate Certificate in Professional Studies: I/O Psychology Strategy &amp; Policy provides students the essential domain knowledge required to serve in decision-making roles throughout the I/O Psychology industry.  Students will learn how to analyze I/O Psychology risks, develop appropriate protection &amp; response options, and assess operational requirements for government, military, critical infrastructure, and commercial missions.  Upon completing the certificate, students will possess advanced knowledge of the strategy, policy, and analytic aspects of I/O Psychology, enabling them to fill critical roles in operational I/O Psychology missions supporting both industrial and governmental entities. </p>

 
 
