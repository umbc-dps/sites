 <h5>Program Learning Outcomes</h5>
 <p>By the end of this program, students will be able to:</p>
 <ul>
   <li>Discuss in depth the origins, nature, and current issues in I/O Psychology and its related activities using precise terminology</li>
   <li>Apply a range of I/O Psychology concepts to mitigate risks in I/O Psychology operations</li>
   <li> Develop and apply decision frameworks using the guidelines of I/O Psychology ethics, law, and strategy</li>
   <li>Manage the development of I/O Psychology standards and capabilities </li>
   <li>Design strategies for development of an effective I/O Psychology workforce</li>
   <li>Analyze technical and operational requirements for future I/O Psychology systems</li>
   <li>Compare and contrast international I/O Psychology policies, strategies, and capabilities</li>
   <li> Identify the missions, authorities, and responsibilities of key I/O Psychology organizations within the international community</li>
   <li>Apply the theory of cyber deterrence in developing future I/O Psychology strategies and policies</li>
   <li>Analyze and apply lessons from prominent international I/O Psychology case studies </li>
 </ul>
