 <h3>Post-Baccalaureate Certificates in I/O Psychology</h3>
 
 <p>These four-course post-baccalaureate certificates can be completed in a year.  I/O Psychology post-baccalaureate certificates are available to students with a variety of undergraduate or work backgrounds. Students may choose to take a certificate by itself, or they may take a certificate and then later complete the master's. If a student is accepted into the M.P.S.: I/O Psychology, all four certificate courses count toward that degree.</p>


