<h3>Frequently Asked Questions</h3>

<div class="accordionheader">
  <h4><a name="Degree"></a>What is an I/O Psychologist? <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>Develops and applies psychological techniques to personnel administration, management, and marketing problems: Observes details of work and interviews workers and supervisors to establish physical, mental, educational, and other job requirements. Develops interview techniques, rating scales, and psychological tests to assess skills, abilities, aptitudes, and interests as aids in selection, placement, and promotion. Organizes training programs, applying principles of learning and individual differences, and evaluates and measures effectiveness of training methods by statistical analysis of production rate, reduction of accidents, absenteeism, and turnover. Counsels workers to improve job and personal adjustments. Conducts research studies of organizational structure, communication systems, group interactions, and motivational systems, and recommends changes to improve efficiency and effectiveness of individuals, organizational units, and organization. Investigates problems related to physical environment of work, such as illumination, noise, temperature, and ventilation, and recommends changes to increase efficiency and decrease accident rate. Conducts surveys and research studies to ascertain nature of effective supervision and leadership and to analyze factors affecting morale and motivation. Studies consumer reaction to new products and package designs, using surveys and tests, and measures effectiveness of advertising media to aid in sale of goods and services.</p>
  
  <p>
    May advise management on personnel policies and labor-management relations. May adapt machinery, equipment, workspace, and environment to human use. May specialize in development and application of such techniques as job analysis and classification, personnel interviewing, ratings, and vocational tests for use in selection, placement, promotion, and training of workers and be designated Psychologist, Personnel (profess. &amp; kin.). May apply psychological principles and techniques to selection, training, classification, and assignment of military personnel and be designated Psychologist, Military Personnel (profess. &amp; kin.). May conduct surveys and tests to study consumer reaction to new products and package design and to measure effectiveness of advertising media to aid manufacturers in sale of goods and services and be designated Market-Research Analyst (profess. &amp; kin.) II </p>
</div>


<div class="accordionheader">
  <h4><a name="Practicum"></a>In terms of the practicum, does it require daytime work hours? If yes, how many?  Will students be paid for their time? <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>The practicum is a 3-credit course and it is anticipated that the students will work in a supervised business-type setting for a minimum of 9 hours per week.  These are not required to be daytime hours.  While UMBC faculty and staff will help place all qualified students in internships, they cannot guarantee that the internships will be paid.</p>
<p>Alternatively, instead of being placed in an internship, students who are already employed in an I/O-related job, may choose to find or design an enriching internship experience within his or her own company.  In addition to the work experience, students will meet on a regular basis and will complete a portfolio of their work-products.  The course will culminate with an in-depth written analysis of the experience and a formal presentation.  </p>
</div>


<div class="accordionheader">
  <h4><a name="Career"></a>What type of career/job search support does the program or UMBC provide upon graduation? <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>One form of assistance is the placement in the practicum, which may lead to a full-time job as well as possible connections to other organizations that have job openings.  To assist us in placing students in internships and jobs, we will have support from <a href="http://www.shrivercenter.org/" target="_parent">UMBC's Shriver Center</a>.  Our students will also have access to <a href="http://www.careers.umbc.edu" target="_parent">UMBC's Career Services</a> department and the <a href="http://www.shadygrove.umd.edu/student-services/career-and-internship-center" target="_parent">USG's Career Services</a> group.  Additionally, we will have access to our Advisory Board members who may also have job opportunities through their organizations and/or may know of opportunities in related areas.
  </p>
</div>


<div class="accordionheader">
  <h4><a name="Opportunities"></a>What opportunities/careers are available with this degree? <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>This is an applied degree, which is intended to provide recent college graduates or mid-level employees coursework and practical experience in the areas of human resources, organizational behavior management, human factors, and training and development.  Graduates from this program will work in Human Resources departments either for corporations, in the government, or in consulting firms.  Additionally, depending on their interests, students may choose to work on training teams and/or in areas that do human factors work.  For more information about what a master's-level degree in I/O psychology can do for you, visit the Society for Industrial/Organizational Psychology's (SIOP) web site and see the page on &quot;<a href="http://www.siop.org/guidelines.aspx" target="_blank">Guidelines for Education and Training at the Master's Level in Industrial-Organizational Psychology</a>.&quot;  </p>
</div>


<div class="accordionheader">
  <h4><a name="Field"></a>Will this degree facilitate my transition into the field of organizational behavior/development upon graduation? Or will I need work experience while in school? <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>This degree will provide some coursework and will give you a foundation in that area; however, practical work experience is highly recommended and can be arranged as part of the practicum.  </p>
</div>


<div class="accordionheader">
  <h4><a name="Recruiters"></a>Who are the main recruiters of your graduate program? <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>It is expected that the Human Resources departments and research contractors/consultants of many of the local Washington D.C. and metro area businesses and government agencies will be the primary recruiters from our program; however, we also expect that students will also be hired by I/O consulting firms and training organizations.</p>
</div>


<div class="accordionheader">
  <h4><a name="Metro"></a>Does the program/faculty have I/O contacts in the DC metro area? If yes, where? <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>Yes. Various locations. Some of these include,  but are not limited to: the Department of Education, Health and Human Services  (HHS), Food and Drug Administration (FDA), the U.S. Consumer Products Safety  Commission (CPSC), Computer Sciences Corporation (CSC), the Montgomery County  Public School System (MCPSS), Human Genome&nbsp; Sciences, Inc and other  governmental agencies.&nbsp; Networking activities and local professional  organizations provide additional opportunities to meet and talk with professionals.  </p>
</div>


<div class="accordionheader">
  <h4><a name="Support"></a>How much support/mentoring will there be if students decide to pursue doctoral studies? <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>This program is a terminal master's-level program.  While we do not specifically support or mentor doctoral-level students, we will certainly provide advising and information regarding next steps, to assist our students who decide to continue their studies beyond the master's level.  </p>
</div>


<div class="accordionheader">
  <h4><a name="Licensure"></a>What is licensure and what do I need for it?  <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
  <p>To find out about licensure, please see the <a href="http://www.siop.org/licensure/licensure.aspx" target="_blank">SIOP web site</a>.</p>
</div>



