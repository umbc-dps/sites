<p style="margin-bottom: 0;"><strong>Important note:</strong> While one faculty member or professional  advisor will provide registration clearance for you each semester, we encourage  students to seek additional academic and career planning from various members  of the UMBC academic and administrative community. High-Performing UMBC  students gather information, recommendations and guidance from a host of  faculty, advisors and administrators- keen to help them maximize the  opportunities and resources available at UMBC. Additionally, we encourage  students to actively pursue conversations and meetings with academic advisors,  faculty and staff throughout the entire academic year. Advising sessions about  registration are certainly important, yet on-going conversations and planning  about graduate school, independent study, and/or gaining entry into a  particular profession can (and should) take place at various times and in  various formats (including email, telephone, etc.).&nbsp; </p>
