<h3><strong><a href="http://www.umbc.edu/veterans/" target="_blank"><u><span class="style11">UMBC Veterans Services</span></u></a><br>
</strong></h3>
  <ul>
    <li> <a href="http://veterans.umbc.edu/benefits/" target="_blank">Educational Benefits</a><br>
      <br>
      <strong><em>Yellow Ribbon Program<br>
      <br>
      </em></strong>UMBC actively supports our  veterans and their families by proudly participating in the Veterans  Administration Yellow Ribbon Program. UMBC's participation in the Yellow Ribbon  Program provides additional funding to nonresident, 100% eligible Post-9/11 GI  Bill recipients who are not on active duty. To be eligible for award consideration,  students must have a VA Certificate of Eligibility and a UMBC Semester  Certification Form on file in the Registrar's Office. <br>
      <br>
      Awards are made on a first-come, first-served basis; a maximum of five (5)  participants (four undergrad and one graduate) are provided up to $500 per  semester, which is matched by the VA (for a total benefit of up to $1000 per  semester). Yellow Ribbon funding is only available for the fall and spring  semesters at UMBC. <br>
      <br>
      To apply for the UMBC Yellow Ribbon Program, submit a request at  http://registrar.umbc.edu/contact. In the &quot;Select a Topic&quot; drop down  menu, choose &quot;Veterans Certification&quot; and click &quot;Create a  Ticket.&quot; Please type &quot;Yellow Ribbon Program&quot; in the subject box.  Requests must be made during the registration period for the upcoming semester.  Requesting participation in the Yellow Ribbon Program through this process does  not guarantee funding. A waitlist will be kept each semester until all funding  has been awarded. <br>
      <br>
      If you have questions about this or other veterans programs and services,  please email <a href="mailto:veterans@umbc.edu">veterans@umbc.edu</a>.</li>
  </ul>
