  <h1>Veteran Resources</h1>
  <p>&nbsp;</p>
  <p>UMBC-Shady Grove welcomes all veterans, active duty military  and dependents.  We are committed to  providing the best possible educational experience and service to assist you in  achieving your academic and personal goals.   As a UMBC-Shady Grove student, you will have access to UMBC Veteran  Services, as well as USG&rsquo;s Office of Veterans&rsquo; Resources, working together to  ensure your success. UMBC will facilitate all application, certification, and  application of benefits. USG is your on-site resource for common questions,  network building, and referrals as necessary. Please see the resources below: <br>
    </p>
