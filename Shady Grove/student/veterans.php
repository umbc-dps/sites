  <ul>
    <li><strong>Center  for Vets:</strong> Located in Building III, Room 2241A, this room is exclusively  available for student veterans as a space to connect with other vets, relax,  study, and find information about various student resources<br>
      <br>
    </li>
    <li><strong>USG Vets  Student Organization<br>
      <br>
    </strong>All  students who are veterans of the military are welcome to join the USG Vets. USG  Vets is the student organization instituted to address the number one concern  for veterans attending college – fitting in. Veterans come to college with a  depth and breadth of experience that most undergraduate students do not have,  and as a result, often face a challenge in connecting with other undergrad or  grad students. Given the importance of establishing a network of students,  colleagues, and friends while in college, the USG Vets serves as an  organization that enables connections to be made organically with veterans and  non-veterans. For more information about the organization and opportunities to  get involved, please contact the President, Walter Ramirez at <strong>usgvets@gmail.com</strong><br>
    </li>
