  <h1>Getting Started</h1>
  <p>&nbsp;</p>
  <p>Service-Learning engages students in weekly service with a designated service placement.  Through this sustained partnership, students build relationships with those whom they serve.  Service-Learning also allows students the opportunity to engage in formal and informal reflections on their service, which enables students to connect their service with their learning.  </p>
  <p>    Participating in service-learning through the Shriver Center enhances a  student&rsquo;s resume and their contribution also appears on their official academic  transcript. Every student who participates in service-learning receives a zero  credit practicum notation on their transcript to reflect their semester of  engagement in the community. With structured opportunities to reflect upon  service, students are able to learn through an exchange of experiences while  providing quality service to the community.<br>
  </p>
  <p>&nbsp;</p>
<blockquote>
    <p><strong>Step  1: </strong>Become familiar with some of our service sites and form an idea of what  kind organization you are interested in serving. Please also read <a href="http://www.umbc.edu/shadygrove/documents/PoliciesofProfessionalConductforStudentsasof2012.08.18.pdf" target="_blank">Policies of Professional Conduct for Students</a><br>
      </p>
    <p><strong>Step 2</strong>: Contact  the placement representative to determine if the placement is a good fit for  you. <br>
      </p>
    <p><strong>Step 3</strong>: If you  and the placement representative decide to move forward with the partnership,  contact Ashley Waters at <a href="mailto:awaters@umbc.edu">awaters@umbc.edu</a> to discuss your interests and sign up for service.  Service enrollment includes signing up for  the 096 Practicum, a zero-credit course that is graded Pass/Fail.  The 096 Practicum will appear on your  official academic transcript, acknowledging your participation in a  university-sanctioned service placement.<br>
      </p>
    <p><strong>Step 4</strong>: Reconnect  with your site supervisor and begin service! Your supervisor may be a staff  member or volunteer coordinator at the community organization. Additionally,  some organizations require you to take additional steps before you are able to  serve, such as completing a volunteer application, background check, or health  clearance.<br>
      </p>
    <p><strong>Step 5</strong>: Complete  the necessary requirements throughout the semester and contact Ashley Waters  with any questions along the way. </p>
  </blockquote>
