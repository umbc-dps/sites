              I have so many favorite memories from school but I will mention two of the most: The first one is that I actively participated in collecting funds for a non-profit and that between my classmates and I competed for whom will sell the most tickets for the ruffle. I believe we were able to collect about $1,800 for A Wider Circle. My classmates and I made a group of about 10 friends that still to the day we get together once in a while to catch up and spend time with each other.</p>
              
              <p>The second one is that on my last semester at UMBC at the time we were taking our finals, I went through a difficult time as I was struggling with family, work, and school responsibilities. One day I broke down into tears while walking down a hallway as I felt desperate. I wiped my tears and as I was doing that, Prof. Morris approached me and stopped what she was doing to be there with me, she talked to me and made me feel so much better. When I went home and opened my email, I saw a message from her wishing me well and opening her door to talk to her when I felt that I needed to. Her actions made me feel that my professors are not only focused on school materials and that they care about me as a person and human being. At UMBC-Shady Grove we were like a close family.</p>

            <p><strong>What is the impact of UMBC-Shady Grove/your degree on where you are now?</strong><br>
            The impact of UMBC at Shady Grove on my career path has been huge. When I first decided to take the Social Work path I was not sure what that would look like, all I knew at the time about social work was because I was a client of a social worker and I liked how my social worker treated me. When I started at UMBC, I had been living in the US for five years, I was married and had a three year-old child. I was very insecure of myself and my capabilities. When I arrived to my first class I observed students and what impacted me most was the diversity. People were from different cultural backgrounds, I did not feel left out. I felt welcomed by my classmates and professors.</p>
            
              <p>At UMBC Shady Grove, I was able to pave the way for my Social Work career path, the diversity of the students at that campus gave me an overview of the different populations I could work with.The professors at Shady Grove were the key players on my career path as their experiences, energy, and love for the profession inspired me and I pictured myself as being one of them in the future. My professors helped me gain the confidence that I needed to become an advocate in my profession. While at Shady Grove, I knew that if at any point I struggled either with school, work, or field, I had the doors open to reach out to my professors to get some ideas and learn how to overcome the challenges that I faced. While at Shady Grove, I dreamed of working with the Hispanic population as I am one of them. I had some insight on the challenges that the undeserved populations face and what stoop up the most was the language barrier. Since my mother tongue language is Spanish, I made myself a goal to work and advocate for this population as I can communicate with them in their own language.</p> 
              
              <p>Upon graduation from UMBC, I felt that I had the tools to go out and follow my dream, but I have knew that I needed to pursue my Master’s in Social Work. I knew that with my undergraduate degree I could do some, but not most of what I wanted to do, so that is why I decided to start my masters. My three years of field, I worked with the Hispanic population, and after I obtained my social work license, I started working at a health clinic providing a variety of behavioral health services that include short-term mental health therapy, medication management, case management, and more. I love my job and I do not picture myself being anywhere else. I am achieving my goal and I am sure that I will continue to do more and hopefully create a positive impact in the lives of the people that I come across the same way that happened to me at UMBC at Shady Grove
