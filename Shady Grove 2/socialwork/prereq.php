    <h1>Recommended Prerequisites</h1>
    <p>&nbsp;</p>
    <p><span style="color: rgb(51, 51, 51); font-family: Arial, Helvetica, sans-serif; font-size: 12px; font-style: normal; font-variant: normal; font-weight: normal; letter-spacing: normal; line-height: 17px; orphans: auto; text-align: start; text-indent: 0px; text-transform: none; white-space: normal; widows: auto; word-spacing: 0px; -webkit-text-stroke-width: 0px; display: inline !important; float: none; background-color: rgb(255, 255, 255);">UMBC-Shady Grove provides upper-level coursework for transfer students who have completed or are near completion of lower-level course requirements.</span>  As such, only  upper-level courses are offered at UMBC-Shady Grove specific to each of our  undergraduate programs.  Most students  transferring into UMBC-Shady Grove have completed <em>at least</em> 45-50 credits  prior to transferring and many students have earned their Associate  Degree.&nbsp; <strong><em>Please note that the Social Work Department requires a minimum 2.5 GPA for transfer to UMBC-Shady Grove.</em> </strong> The Social Work Department also<strong> strongly encourages</strong> you to complete the following courses prior to transfer:</p>
    <ul>
      <li><strong>Introduction to Sociology</strong></li>
      <li><strong>Introduction to Psychology</strong></li>
      <li><strong>English Composition</strong></li>
      <li><strong>General or Human Biology (with lab)</strong></li>
    </ul>
    <p>In addition to the above courses, the following courses must be  completed prior to graduating from UMBC. Ideally, a student will complete as  many of the courses below as possible prior to transferring to UMBC-Shady  Grove. Given that these courses are lower-level, they must be taken at an  institution other than UMBC-Shady Grove:</p>
    <ul>
      <li>3 Arts and Humanities  courses (from at least two different disciplines)</li>
      <li>Political Science or Economics course</li>
      <li>Statistics</li>
      <li>Non-lab science course</li>
      <li>2 Physical Education  courses (consult advisor about exceptions)</li>
      <li>World Language  201-Level (consult advisor about exceptions)</li>
    </ul>
