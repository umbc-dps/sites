    Click <a href="http://www.umbc.edu/undergrad_ed/ai/faculty.html" target="_blank">here</a> for detailed information on  UMBC's Academic Integrity policies, tools and resources to prevent and educate,  and instructions on submitting reports. You can also find links at the  bottom of your myUMBC Faculty Center page. </p>
<p style="margin-bottom: 0;"><span style="font-family:Arial, Helvetica, sans-serif; font-size:12px;"><strong>I have a student attending in a course in which they are not  on the course roster for after the schedule adjustment period. Is this ok? </strong></span></p>
<p style="margin-top:0; margin-bottom: 0;">It is against university policy for a student to  attend and/or participate in a course for which they are not on the course  roster after the end of the schedule adjustment period. Should you be  approached by a student who has not gained a seat in a course by the end of the  schedule adjustment period please keep the following factors in mind: </p>
<ul style="margin-top: 0;">
  <ul style="margin-bottom: 0">
    <li>A student should not be encouraged to submit a  late-add petition unless, failure to enroll was for a reason outside their  control.</li>
    <li>Failure to pay an overdue balance or having a  registration block is not out of a student&rsquo;s control and does not provide basis  for approving a late-add petition.</li>
    <li>Having access to Blackboard or having a spot on  the waitlist does not constitute formal registration in a course and does not  provide basis for approving a late-add petition.</li>
    <li>Courses added after the end of the schedule  adjustment period (even with OUE approval) may not be considered for the  student&rsquo;s financial aid eligibility due to federal regulations.</li>
    <li>Students may incur additional charges for late  registration.</li>
  </ul>
</ul>
