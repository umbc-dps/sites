  <p><strong>My class will be conducting research with  human subjects this semester. How do I receive IRB approval? Do I  need IRB approval?</strong> <br>
    Recently,  UMBC has updated their policies and procedures on IRB approval.  Please visit their website for <a href="http://www.umbc.edu/research/ORPC/irb_forms.html" target="_blank">more  instruction</a>. Any  questions can be directed to your Program Director or Tim Sparklin at  410-455-2737 or <a href="mailto:sparklin@umbc.edu">sparklin@umbc.edu</a>.</p>
