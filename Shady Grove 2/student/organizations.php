  <h1>Student Organizations</h1>
  <p>&nbsp;</p>
  <p style="margin-bottom: 0;">Being involved in a student  organization is an integral part of your college career. As a UMBC-Shady Grove  student, you have the opportunity to participate in many student organizations,  including a student association for each of our four undergraduate programs.  Please review the organizations below and become involved as they provide  excellent networking opportunities, a chance to collaborate with other students  on campus, and enhanced exposure to your field of interest outside of the  classroom. <br>
    </p>
  <p style="margin-top: 0; margin-bottom: 0;">&nbsp;</p>
<hr>
  <p style="margin-top: 0; margin-bottom: 0;">&nbsp;</p>
<h3 style="margin-top: 0;"> <strong>History Student Association</strong> </h3>
  <p>The <strong>History Student Association</strong> (HSA) is for students who are first and  foremost passionate about history. Our group focuses on approaching issues,  current and past, from a historical perspective. Whether your interest is  cultural history, intellectual history, scientific history, political history,  or any other form of history from any time period, we want you as a part of our  group! At HAS, we aim to provide students with a respectful, open, and academic  atmosphere as we discuss the issues that interest us. We hope to hold debates,  round-table discussions, field trips, and more for students throughout the  semester. To learn more, email <a href="mailto:HSA.USG@gmail.com">HSA.USG@gmail.com</a>.<br>
  </p>
  <h3> <strong>Political Science Student Organization</strong></h3>
  <p>As members of the <strong><a href="http://my.umbc.edu/groups/usg-psso" target="_new">Political Science Student Organization</a></strong>,  we bring together UMBC Political Science students at Shady Grove and others  interested in subject matter of Political Science at the Universities at Shady  Grove. Our mission is to promote the scientific study of politics through  discussion, seminars, social activities and public service projects. We also  act as liaison between our members, the administration, faculty, and staff for  both the UMBC main campus and the Universities at Shady Grove. To learn more,  email <a href="mailto:psso.usg@gmail.com">psso.usg@gmail.com</a>. <br>
  </p>
  <h3> <strong>Psychology Student Association</strong></h3>
  <p class="style12" style="margin-bottom: 0;"><span class="style11"><a href="https://my.umbc.edu/loggedin?u=https%3A%2F%2Fmy.umbc.edu%2Fgroups%2Fpsausg"><span style="color:#333333;">Psychology Student Association</span></a><span style="color:#333333;">&nbsp;</span></span><span style="font-family:Arial, Helvetica, sans-serif; color:#333333;">(PSA) with UMBC at Universities at Shady Grove hosts academic  events in which speakers come to discuss future employment opportunities for  careers in psychology and current research in psychology, social events to meet  other psychology students and discuss common interests, and fundraising events  to work to raise money for different organizations including NAMI (National  Alliance of Mental Illness), breast cancer research and autism research. To  learn more, email <a href="mailto:psa-usg@umbc.edu">psa-usg@umbc.edu</a>.</span> If  you are interested in learning more about the Psychology Student Association,  you may also visit <a href="https://shadygrove.collegiatelink.net/organization/psa">https://shadygrove.collegiatelink.net/organization/psa</a>.</p>
  <h3 style="margin-top: 0;"><strong>Social Work Student Association</strong></h3>
  <p>The <a href="http://my.umbc.edu/groups/usg-swsa" target="_new"><strong>Social Work Student Association</strong></a> (SWSA) is an organization designed to involve students into the community and  volunteer their time and energy. Throughout the school year, we hold several  events/fundraisers, organize groups of volunteers for needs throughout the  holiday seasons, and host a large fundraiser event to benefit an organization  of our choice. This is beneficial to students who are interested in learning  about their communities and their multiple needs. It is a great way to increase  professional development while taking classes and it is a great resume builder.  To learn more, email <a href="mailto:swsa-usg@umbc.edu">swsa-usg@umbc.edu</a>.</p>
  <h3><strong>Tau Sigma</strong></h3>
  <p><strong><a href="http://my.umbc.edu/groups/tausigma" target="_blank">Tau Sigma</a></strong> is a National Honor Society aiming to recognize the academic achievement of students transferring to UMBC from another academic institution and to encourage and promote the students' involvement at UMBC. To join, a student must transfer with a 3.5 GPA or in top 20% of incoming class and with at least 30 credits and be a full-time enrollment while maintaining a 3.3 GPA at UMBC. Members must attend 2 general meetings and participate in 2 events per semester prior to an informal interview and induction. Why join? Students will gain recognition for academic achievement and leadership experience. Get support through the transition to UMBC so you can continue to succeed! To learn more, <span style="font-family:arial;font-size:12px;color:#333333;">contact Marilennis Cruz at&nbsp;</span><span style="font-family:Times New Roman;font-size:15px;"><a href="mailto:mcruz1@umbc.edu"><span style="font-family:arial;font-size:12px;">mcruz1@umbc.edu</span></a></span>.</p>
  <h3><strong>UMBC Peer Advisory Team</strong></h3>
  <p style="margin-bottom: 0;">As current UMBC-Shady Grove students, <strong><a href="pat.php" target="_new">UMBC Peer Advisory Team</a></strong> (PAT) members serve as a critical point of contact in the successful and seamless transition of new students transferring to UMBC-Shady Grove from other institutions. PAT members undergo extensive leadership development and work closely with UMBC-Shady Grove administrative staff, faculty, and fellow current students to reach out, serve and create meaningful connections with prospective students through a variety of formal and informal recruitment and retention events.  To learn more, email Chelsea Moyer at <a href="mailto:csmoyer@umbc.edu">csmoyer@umbc.edu</a>.<br>
  </p>
