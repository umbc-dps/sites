    <a name="policies" id="registration5"></a><span class="style10">Academic Policies</span><br>
  </strong> </h3>
  <p><strong>How many times may I repeat a single course?</strong></p>
  <ul>
    <li>Students may not register for a course more than two  times.&nbsp; Students are considered registered for a course when enrolled  after the end of the schedule adjustment period.&nbsp; Students may petition  the Office of Undergraduate Education for a third and final attempt of a course  taken at UMBC or another institution.&nbsp; An attempt is defined as enrollment  in a course or its equivalent at another institution in which the student has  received a grade or an official withdrawal.&nbsp; This includes credit by exam  or withdrawal from the entire semester/session.&nbsp; Students should consult  with an <a href="http://www.umbc.edu/shadygrove/admissions/academicadvising.html" target="_parent">academic  advisor</a> for additional restrictions or policies regarding repeated courses. Undergraduate  Petition Forms may be accessed here: <a href="http://academicpolicy.umbc.edu/home/undergraduate-petitions/">http://academicpolicy.umbc.edu/home/undergraduate-petitions/</a></li>
