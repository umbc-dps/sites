    <a name="other" id="other"></a><span class="style10">Taking Classes at Other Institutions</span></strong></p>
  <p><strong>I plan to take a course at another  institution and want to make sure that the course transfers properly and I earn  the appropriate credits.&nbsp; What should I do?</strong></p>
  <ul>
    <li>Submit a signed <a href="http://www.umbc.edu/shadygrove/documents/Verification-of-Transferability.pdf" target="_parent">Verification  of Transferability</a> (VOT) form at least <em>two weeks prior</em> to taking the  course.&nbsp; Follow these steps:</li>
    <li>Download, complete, and sign the <a href="http://www.umbc.edu/shadygrove/documents/Verification-of-Transferability.pdf" target="_parent">Verification  of Transferability</a> (VOT) form.&nbsp; (NOTE:&nbsp; if you will be within  your final 30 credits when taking the course, you must also check the  &quot;Residency Waiver permission&quot; box)</li>
    <li>Submit your VOT form through the UMBC RT Help  system.&nbsp;Open a new UMBC RT Help ticket by logging in to myUMBC and  clicking &quot;Request Help&quot; from the &quot;Help&quot; drop-down  menu.&nbsp; Click &quot;Registration&quot; under &quot;Classes &amp; Grades&quot;  then attach your completed VOT form.</li>
    <li>The processed VOT form will be returned to your UMBC  email account within one week of submission.&nbsp; It will indicate (a) whether  or not the course will transfer for credit and (b) what requirements it will  satisfy.&nbsp; It will also indicate if your residency waiver request has been  approved.&nbsp; If you have any questions, bring the returned VOT form to your <a href="http://www.umbc.edu/shadygrove/admissions/academicadvising.html" target="_parent">academic  advisor</a>.</li>
    <li>After taking the course and your final grade has posted,  send an official transcript to the UMBC Office of the Registrar (1000 Hilltop  Circle, Baltimore, MD 21250).</li>
    <li>Review your Transfer  Credit Report and Degree Audit in myUMBC 4-6 weeks later to confirm that the course  transferred properly.&nbsp; Contact your <a href="http://www.umbc.edu/shadygrove/admissions/academicadvising.html" target="_parent">academic  advisor</a> with any questions or concerns. </li>
  </ul>
