  <h3><strong><br>
    <a name="GER" id="GER"></a><span class="style10">General Education Requirements and Waivers</span></strong><br>
</h3>
  <p><strong>Do I have to take a foreign language and, if  so, to what level?</strong></p>
  <ul>
    <li>Yes, you must complete a foreign language through the  intermediate level (NOTE:&nbsp; at Montgomery College, the 201-level qualifies  as intermediate). </li>
  </ul>
  <p><strong>Does American Sign Language qualify as a  foreign language?</strong></p>
  <ul>
    <li>Yes. </li>
  </ul>
  <p><strong>Are there any language waivers or exemptions?</strong></p>
  <ul>
    <li>If you passed four years of a single foreign language in  high school, send your high school transcript to UMBC to receive a waiver of  the foreign language requirement.&nbsp; There are additional opportunities for  international students to demonstrate proficiency.&nbsp; Click <a href="http://www.umbc.edu/shadygrove/documents/Applicationfor201proficiencyformrev9.9.13.doc" target="_parent">here</a> and download  &quot;Language Proficiency for International Students&quot; for more  information. </li>
  </ul>
  <p><strong>Where can I get more information about the  CLEP exam?</strong></p>
  <ul>
    <li>Click <a href="http://clep.collegeboard.org/register/exam" target="_parent">here</a>.&nbsp; Students should  review the <a href="documents/Appendix_II.pdf" target="_blank">Appendix II</a> of the UMBC  Catalog to learn which tests and scores will be accepted prior to registering for an  exam.&nbsp; The UMBC CEEB code for CLEP is 5835. </li>
  </ul>
  <p><strong>Where can I get more information about the  NYU exam?</strong></p>
  <ul>
    <li>Click <a href="http://scps.nyu.edu/academics/departments/foreign-languages.html" target="_parent">here</a>.&nbsp;Students should  review the <a href="documents/Appendix_II.pdf" target="_blank">Appendix II</a> of the UMBC  Catalog to learn which tests and scores will be accepted prior to registering for an  exam. A list of testing  centers is available <a href="http://ncta-testing.org/resources/standards/subscribers.php" target="_parent">here</a>.&nbsp; One possibility  is the University of Maryland, College Park Testing Office.&nbsp; Appointments  should be made at least one week in advance by e-mailing <a href="mailto:testingoffice@umd.edu">testingoffice@umd.edu</a> or calling 301-314-0319. </li>
  </ul>
  <p><strong>What qualifies for a Physical Education (PE)  waiver and how do I apply?</strong></p>
  <ul>
    <li>There are four types of PE waivers available:&nbsp; </li>
  </ul>
  <p>&nbsp;&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 1.&nbsp; Age:&nbsp; 30 years of age or older  at the time of initial enrollment at UMBC<br>
    <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  2.&nbsp; Military Service:&nbsp; Six months of active service = 1.5 physical  education credits<br>
  <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  3.&nbsp; Documented medical condition or disability<br>
  &nbsp;&nbsp; <br>
  &nbsp;&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; 4.&nbsp; Collegiate Athletic Participation:&nbsp; 1 year =  1.5 physical education credits</p>
  <ul>
    <li>All PE Waiver requests require a written memo requesting  the waiver with supporting documentation (see <a href="http://www.umbc.edu/shadygrove/documents/PEWaiver.doc" target="_blank">here</a> for a sample and  documentation details).&nbsp; Email the memo and supporting documentation to  Mr. John Jancuska, Supervisor of Physical Education, at <a href="mailto:jancuska@umbc.edu">jancuska@umbc.edu</a>. </li>
  </ul>
  <h3><strong><br>
    <a name="graduation" id="registration2"></a><span class="style10">Graduation</span></strong><br>
  </h3>
  <p><strong>How can I graduate with honors?</strong></p>
  <ul>
    <li>To be eligible for UMBC graduation honors, students must  complete at least 45 credits at UMBC (excluding institutional credits) prior to  the semester in which they are graduating.&nbsp; Degree candidates whose  cumulative GPA <em>at the time of graduation</em> (which is before final grades  for the current semester are posted) is between 3.5 and 3.7499 receive the  degree Cum Laude; between 3.75 and 3.9499 receive the degree Magna Cum Laude;  between 3.95 and 4.0 receive the degree Summa Cum Laude. </li>
  </ul>
  <p><strong>Can I &quot;walk&quot; in the upcoming  commencement ceremony if I am just a few credits short of completing my degree?</strong></p>
  <ul style="margin-top: 0; margin-bottom: 0;">
    <li><span style="color: rgb(51, 51, 51); font-family: Arial, Helvetica, sans-serif; font-size: 12.8000001907349px; font-style: normal; font-variant: normal; font-weight: normal; letter-spacing: normal; line-height: normal; orphans: auto; text-align: start; text-indent: 0px; text-transform: none; white-space: normal; widows: auto; word-spacing: 0px; -webkit-text-stroke-width: 0px; display: inline !important; float: none; background-color: rgb(255, 255, 255);">Undergraduate students who are enrolled to complete all degree requirements by the end of the spring semester may elect to participate in the May commencement ceremony. Undergraduate students who are enrolled to complete all degree requirements by the end of the fall semester may elect to participate in the December commencement ceremony. Students who complete their degree requirements in one of the summer sessions may elect to participate in the following December commencement ceremony and those completing their requirements during the winter session may elect to participate in the following May commencement ceremony. Students may only participate in one commencement ceremony based upon the semester in which all degree requirements will be met.</span></li>
    <li><span style="color: rgb(51, 51, 51); font-family: Arial, Helvetica, sans-serif; font-size: 12.8000001907349px; font-style: normal; font-variant: normal; font-weight: normal; letter-spacing: normal; line-height: normal; orphans: auto; text-align: start; text-indent: 0px; text-transform: none; white-space: normal; widows: auto; word-spacing: 0px; -webkit-text-stroke-width: 0px; display: inline !important; float: none; background-color: rgb(255, 255, 255);">In-progress coursework, including incomplete grades, will be included in the calculations when determining student’s eligibility for undergraduate commencement participation (120 credit requirement, 45 upper level requirement and 2.0 GPA requirement).</span></li>
  </ul>
<p><span style="font-family:Times New Roman; font-size:12px;"><strong>Where do I get my cap and gown for graduation? Do I have to  go to main campus? </strong></span></p>
<ul>
  <li>Approximately six weeks into the semester, you  will begin to receive emails referencing the purchasing of your cap and gown,  which must be bought new to be eligible to walk in commencement. This  information will come from the bookstore on main campus and reference ordering  in person in Catonsville. </li>
  <li>However, there is an online option to order your  cap and gown. You will receive an email from UMBC-Shady Grove administration with  details on that process once available. You do not need to go to Main Campus to  purchase your cap &amp; gown. </li>
</ul>
<p><span style="font-family:Arial, Helvetica, sans-serif; font-size:12px;"><strong>How many tickets do I receive for my friends and family to  attend commencement? </strong></span></p>
<p style="margin-bottom: 0;">&nbsp;</p>
<ul style="margin-top: 0;">
  <li>For Fall commencement, you will receive four  tickets per graduating student. The only way to receive more is to obtain them  from another graduating student who may not be using all of their assigned  tickets.</li>
  <ul style="margin-bottom: 0">
    <li>Please note there is a viewing room on campus  where additional friends and family can view the ceremony. </li>
    <li>For Spring commencement, there are no tickets.  Seating is open as it is held off-campus at a larger venue. Graduating students  are welcome to invite as many guests as they would like. </li>
    <li>For more information on commencement, visit  umbc.edu/commencement.</li>
  </ul>
</ul>
<p><strong>It has been more than eight weeks since  graduation and I still haven't received my diploma.&nbsp; What should I do?</strong></p>
  <ul>
    <li>Check your Degree Audit on myUMBC and verify that  everything is listed as &quot;satisfied.&quot;&nbsp; If anything is not  satisfied and you think it should be, please do the following:</li>
    <li>Confirm that you submitted all required forms, including  final transcripts from any other institutions.</li>
    <li>Confirm that you have no outstanding charges on your  account.</li>
    <li>Contact your <a href="http://www.umbc.edu/shadygrove/admissions/academicadvising.html" target="_parent">academic  advisor</a> to inform him or her of the issue.</li>
    <li>Submit your issue through the UMBC RT Help  system.&nbsp;Open a new UMBC RT Help ticket by logging in to myUMBC and  clicking &quot;Request Help&quot; from the &quot;Help&quot; drop-down  menu.&nbsp; Click &quot;Graduation&quot; under &quot;Classes &amp; Grades&quot;  then describe your issue in detail.</li>
    <li>Remember, diplomas are not distributed at the  commencement ceremony.&nbsp; <em>The process of mailing diplomas may take up to  eight weeks after graduation.</em></li>
  </ul>
