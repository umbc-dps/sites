  <ul>
    <li>Submit an <a href="http://www.umbc.edu/shadygrove/documents/Course-Description-Review.pdf" target="_parent">Evaluation  of Transfer Credit</a> form to request a review of your course(s).&nbsp; Follow  these steps:</li>
    <li>Download and complete the <a href="http://www.umbc.edu/shadygrove/documents/Course-Description-Review.pdf" target="_parent">Evaluation  of Transfer Credit</a> form.</li>
    <li>Submit your Evaluation of Transfer Credit form through  the UMBC RT Help system.&nbsp;Open a new UMBC RT Help ticket by logging in to  myUMBC and clicking &quot;Request Help&quot; from the &quot;Help&quot;  drop-down menu. Click &quot;Registration&quot; under &quot;Classes &amp;  Grades&quot; then attach your completed form.</li>
    <li>Notification of the  outcome will be sent to your UMBC email account within 3-4 weeks of submission. </li>
  </ul>
