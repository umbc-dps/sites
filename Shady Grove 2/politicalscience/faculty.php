    <p><span style="font-family:Times New Roman;font-size:15px;"><span class="style2" style="font-family:Arial, Helvetica, sans-serif;">Dr. Sunil Dasgupta is the  Program Director of UMBC&rsquo;s Political Science program at the Universities at  Shady Grove. He teaches international relations, foreign policy, and security  studies as well as introductory courses in political science. In his teaching,  Dr. Dasgupta has developed innovative pedagogical approaches using online  instruction and new technologies.</span></span></p>
<p>Dr. Dasgupta&rsquo;s research  focuses on insurgency and civil wars, civil-military relations, defense  R&amp;D, and Asian security issues. His work has appeared in <em>Foreign  Affairs, Current History, </em>the<em> Washington Quarterly,</em> and the <em>New  York Times</em>, among others<em>. </em>He is co-author, with Stephen P. Cohen, of <em>Arming Without Aiming: India&rsquo;s Military Modernization</em> (Brookings Press,  2010). His paper, &ldquo;Regional Politics and the Prospects of Stability in  Afghanistan,&rdquo; was published in April 2013 by the United States Institute of  Peace. </p>
    <p class="style1 style3">Before joining UMBC at USG in  2009, Dr. Dasgupta taught international relations and security studies at the  George Washington and Georgetown Universities. Prior to his academic career,  Dr. Dasgupta worked as a news reporter covering national security and military  affairs and wrote a column on business and economics. He remains a non-resident  senior fellow at the Brookings Institution in Washington D.C. Click <a href="http://www.brookings.edu/experts/dasguptas" target="_blank">here</a> to view  Dr. Dasgupta's publications on his Brookings page<span class="style4">.</span></p>
    <p><span style="font-family:Arial, Helvetica, sans-serif; font-size:12px;">Dr. Dasgupta has an M.A.  and Ph.D. in Political Science from the University of Illinois at  Urbana-Champaign.</span><span style="font-family:Times New Roman; font-size:12px;"></span></p>
