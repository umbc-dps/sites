<h1 style="margin-top: 0;">Political Science</h1>
<p>&nbsp;</p>
    <p>UMBC provides a unique environment for  students to earn a degree in Montgomery County at The Universities at Shady  Grove (USG).&nbsp; Students transferring from regional community colleges or  four year institutions will have access to UMBC&rsquo;s best faculty, dynamic  learning environment, and individualized advising without commuting to  Baltimore.&nbsp; At The Universities at Shady Grove, UMBC offers you the best  of both worlds by providing <em>our reputation in your backyard.</em>  A dynamic mix of talented, engaged and  experienced professors teach students in small classroom sized-settings.&nbsp;  Students complete their lower-level courses, including prerequisites and  general education requirements and electives at a Maryland community college  and their upper level requirements with UMBC-Shady Grove. Classes are taught by  UMBC professors, and students receive a UMBC degree upon graduation. </p>
    <p><strong>UMBC&rsquo;s Bachelor of Arts in Political Science </strong>prepares students to become a part of the  &quot;business&quot; of the Washington metropolitan area. Students learn about  the&nbsp;central problems of politics&nbsp;such as&nbsp;the distribution of  power and authority in society and the aggregation of preferences across large  populations as well as develop analytical and writing skills. The program offers&nbsp;courses  in pre-law, public policy, and international affairs that enable students to  focus on&nbsp;their core interests and pursue practical learning opportunities  through internships at federal, state, and local agencies, and  at&nbsp;international and non-governmental organizations. <br>
    </p>
    <p>Students  enrolling in the Bachelor of Arts program in Political Science at UMBC-Shady  Grove will complete approximately two years of full-time study prior to entry  and complete the remainder of their upper-level credits with UMBC-Shady Grove.  Students may transfer a maximum of 60 credits  from a two-year school and 90 credits from a four-year school to UMBC.</p>
    <p>Students planning to attend  UMBC-Shady Grove should see their Transfer Counselor and/or a UMBC-Shady Grove  program representative<strong>&nbsp;</strong>to allow for a smooth  transition and to best prepare to reach their academic goals. To meet with a  UMBC-Shady Grove representative, please register to attend an upcoming <a href="../info/uginfosession.php" target="_new">Information Session</a>.</p>
