 <h5>Program Learning Outcomes</h5>
 <p>By the end of this program, students will be able to:</p>
 <ul>
   <li>Discuss in depth the origins, nature, and current issues in cybersecurity and its related activities using precise terminology</li>
   <li>Apply a range of cybersecurity concepts to mitigate risks in cybersecurity operations</li>
   <li> Develop and apply decision frameworks using the guidelines of cybersecurity ethics, law, and strategy</li>
   <li>Manage the development of cybersecurity standards and capabilities </li>
   <li>Design strategies for development of an effective cybersecurity workforce</li>
   <li>Analyze technical and operational requirements for future cybersecurity systems</li>
   <li>Compare and contrast international cybersecurity policies, strategies, and capabilities</li>
   <li> Identify the missions, authorities, and responsibilities of key cybersecurity organizations within the international community</li>
   <li>Apply the theory of cyber deterrence in developing future cybersecurity strategies and policies</li>
   <li>Analyze and apply lessons from prominent international cybersecurity case studies </li>
 </ul>
 
 
