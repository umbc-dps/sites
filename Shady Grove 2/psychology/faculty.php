    <p style="margin-top: 0;">Karen Watkins-Lewis, Ph.D., serves as a full-time Instructor in the UMBC Psychology Department at the Universities at Shady Grove.&nbsp; Karen received her undergraduate degree in Mechanical Engineering and her doctorate degree in Developmental Psychology from Howard University in Washington, DC.&nbsp; Her research areas of interest are socio-cultural child development and the processes of parenting as it relates to the mental health and academic success of children in underserved communities.&nbsp; While pursuing her graduate degree, Karen worked as a research associate with the Center for Research on the Education of Students Placed at Risk (currently known as CAPSTONE) at Howard University.&nbsp; She has completed post doctoral fellowships with the Child Development Institute at the University of Maryland, College Park and the National Center for Research on Early Childhood Education at the University of Virginia.&nbsp; She has co-authored several peer reviewed publications and book chapters in the areas of socio-cultural child development and education.&nbsp; Karen has over 5 years of experience as a lecturer in psychology and human development, and has served as an Instructor and mentor for two student scholars programs:&nbsp; the Child Advocacy Scholars program at the University of Maryland, College Park and the Meyerhoff Scholars program at the University of Maryland, Baltimore County.</p>
