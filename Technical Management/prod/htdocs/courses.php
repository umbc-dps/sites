          <h3>Course Descriptions</h3>


          <p><strong>Note:</strong> Not all courses are offered every semester, and new courses may be added at any time. Check the <a href="schedule">schedule of classes</a>, for the latest offerings. </p>

<div class="accordionheader">
  <h4><a href="#650" id="650"></a>ENGM 650: Project Management Fundamentals [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>Students learn the fundamentals of managing projects in a systematic way. These fundamentals can be applied within any industry and work environment and will serve as the foundation for more specialized project management study. Principles and techniques are further reinforced through practical case studies and team projects in which students simulate project management processes and techniques.</p>
   </div>
   
<div class="accordionheader">
  <h4><a href="#652" id="652"></a>ENMG 652: Management Leadership and Communications [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>Students learn effective management and communication skills through case study-analysis, reading, class discussion and role-playing. The course covers topics such as effective listening, setting expectations, delegation, coaching, performance, evaluations, conflict management, and negotiation with senior management and managing with integrity.</p>
  </div>
   
<div class="accordionheader">
  <h4><a href="#654"  id="654"></a>ENGM 654: Leading Teams and Organizations [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course achieves leadership development through the use of a collaborative environment where students learn leadership skills through the use of case studies, discussions, and analysis papers. Case study analysis papers are written by leaning groups of three students.  Discussions, both in-person and on-line, are graded since todays leaders are challenged to communicate through many channels. The course reviews leadership through a variety of lens: leading change, power management, and creating a engaged company culture.<br>
   </p>
          </div>
   
<div class="accordionheader">
  <h4><a href="#656" id="656"></a>ENMG 656: Engineering Law & Ethics [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course provides a comprehensive overview of important legal principles affecting engineers, engineering sciences and corporate management, with a focus on the intersection of these legal principles with business ethics. The student learns how to think through and process legal problems consistent with ethical norms, and how to analyze business risks in light of operative legal constructs, taking into consideration ethical issues, to arrive at a range of correct business decisions. Throughout the course, the student will learn substantive legal principles including an overview of constitutional, contract, tort, and corporate and regulatory law. Students will work in groups during certain exercises, role-play in real and hypothetical case studies, and make a final presentation of a comprehensive legal and ethical engineering problem.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#658" id="658"></a>ENMG 658: Financial Management [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course will cover the fundamentals of setting up, reading and analyzing financial statements and reports in a business setting.  Course topics will include: project budgeting, profit planning, return on investment and basic corporate finance. Students will analyze case studies from specific industries.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#659" id="659"></a>ENMG 659: Strategic Management [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course is intended to integrate the learning from the previous management courses and to focus it on the perspective and problems of the Chief Executive Officer and other organizational strategic managers. The theme of the course is that any organization improves its chances of sustained success when its managers formulate an action-oriented strategic business plan based on the strategic management process. Case studies are included to illustrate the concepts and their applications. <br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#660" id="660"></a>ENMG/ENEE 660: Systems Engineering Principles [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course provides an introduction to the discipline of Systems Engineering (SE) and Systems Architecting (SA). Key industry standards for SE and SA and a standard definition for the “The Systems Engineering (SE) Process” are provided and are used throughout the course. The course describes how the SE process is implemented in standard life cycle models and through various standard organizational structures. Students will develop a requirements document, and integrated architecture, and a Systems Engineering Plan (SEP). Homework and Exams are designed to provide the opportunity to practice the concepts learned in class.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#661" id="661"></a>ENMG 661: Leading Virtual Global Teams [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This completely online course is designed to help the student apply managerial concepts and skills to managing and leading virtual and/or global work teams. Geographically dispersed work teams have great challenges to overcome such as time zones, audio communications access, language and cultural differences. Students will learn to empower others, build credibility, communicate appropriately and adapt quickly across cultures and technologies.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#663" id="663"></a>ENMG 663: Advanced Project Management Applications [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This advanced course in project management builds on the beginner level project management courses to expand the hands-on applications, with a focus on critical evaluation of project performance and ultimately creating an environment for maximizing one's own project management performance. With a strong emphasis on the importance of learning through application, the course will bridge academia with the professional business environment to provide opportunities for students to interact with industry professionals as the students execute their course work. Students will also confront the real challenges facing project managers associated with the growing global and virtual workforce through the use of on-line learning tools and methods of collaboration. At the successful completion of the course, students will have the requisite skills and experiences necessary to function effectively, and artfully, as skilled project managers.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#664" id="664"></a>ENMG 664: Quality Engineering Management [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course provides an overview of the basic principles and tools of quality and their applications from an engineering perspective. The primary quality schools of thought or methodologies, including Total Quality Management, Six Sigma and Lean Six Sigma, and quality approaches from key figures in the development and application of quality as a business practice, including W. Edwards Deming and Joseph M. Juran will be analyzed. Some of the key mathematical tools used in quality systems will be discussed, including Pareto charts, measurement systems analysis, design of experiments, response surface methodology, and statistical process control. Students will apply these techniques to solve engineering problems using the R software. Reading assignments, homework, exams, and the project will emphasize quality approaches, techniques, and problem solving.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#668" id="668"></a>ENMG 668: Project and Systems Engineering Management [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course will cover fundamental project control and systems engineering management concepts, including how to plan, set up cost accounts, bid, staff and execute a project from a project control perspective.  It provides an understanding of the critical relations and interconnections between project management and systems engineering management.  It is designed to address how systems engineering management supports traditional program management activities to break down complex programs into manageable and assignable tasks.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#672" id="672"></a>ENEE/ENMG 672: Decision and Risk Analysis [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course provides an overview of decision and risk analysis techniques. It focuses on how to make rational decisions in the presence of uncertainty and conflicting objectives. It covers modeling uncertainty, the principles of rational decision-making, representing and solving decision problems using influence diagrams and decision trees, sensitivity analysis, Bayesian decision analysis, deductive and inductive reasoning, objective and subjective probabilities, probability distributions, regression analysis, defining and calculating the value of information, modeling risk attitudes and utility functions. Concepts will be illustrated through case studies and practiced by students through homework.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#680" id="680"></a>ENMG 680 International Project Management [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course explores the best management practices of international projects, emphasizing the importance of leadership skills and virtual teamwork to successfully navigate through managing an international project. International projects differ from domestic projects by their complexity of culture, increased communications and collaboration requirements, local customs and practices, differing languages and currencies, processes, and the type of resources that may be available. The course describes how to conduct project planning in each of the life cycle acquisition process phases and them to execute the plan through recommended international organizational structures.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#690" id="690"></a>ENMG 690: Innovation and Technology Entrepreneurship [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course offers an overview of innovation and its role in entrepreneurial ventures, both in new companies and within existing corporations. The basics of entrepreneurship with specific emphasis on technology-based business start-up are investigated. For the purposes of this course, technologies include IT, engineering and biotech. The course covers where to find innovative ideas and how to determine if a business idea is feasible along with an overview of the critical success factors in a new venture start-up.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#692" id="692"></a>ENMG 692: Principles of Organizational Learning [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course studies how organizations create and use knowledge to support their operations and strategic planning. A “knowledge-creating” company is said to be one that consistently creates new knowledge, disseminates it widely throughout the organization, and quickly embodies it in new technologies and products, and whose sole business is continuous innovation. Actions are investigated which corporate executives and managers can take to improve their management, translation, and utilization of knowledge, to increase their organization’s absorptive capacities and ability to learn quickly, to posture themselves for innovative responses to changing market conditions, to handle disruptive technology cycles, to implement the effective use of data analytics, and to develop sustainable business models and improve organizational performance. After this course, you will be familiar with the most significant research dedicated to optimizing business and project processes that has been released in the last decade. Each research paper has been summarized in 2 to 3 page reports to help students manage the significant amounts of information and to track the key points being made in each paper.<br>
   </p>
          </div>
          
          <div class="accordionheader">
  <h4><a href="#693" id="693"></a>ENMG 693: Management Project [3]<i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>The Management Project course is the capstone for management programs. This course is normally taken in the final semester of their management program. Students will carry out individual research in a management topic of interest to the student and approved by the faculty member. The students work shall demonstrate mastery of the management and leadership skills obtained in the program. The result of the research is typically in the form of a case study of management problem of interest. This course meets in person approximately five times during the semester.<br>
   </p>
          </div>
   

