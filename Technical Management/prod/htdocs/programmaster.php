 <h3>M.P.S. of Technical Management</h3>

<p>The Technical Management program is built around a core set of six courses that provide the foundational knowledge in leadership, communications, ethics, and project management. The core courses are supplemented with technically oriented courses in entrepreneurship, international project management, and organizational leadership. Students can earn certificates in systems engineering, and cybersecurity along with the Masters degree.</p>
 

