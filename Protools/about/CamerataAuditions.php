	  <p><strong><a name="Where" id="Where"></a>Where are Camerata auditions?</strong><br />
	    Auditions are held in PAHB 254.  Come with your voice already warmed up, practice rooms are located in the building.  Come early to your time; you will be greeted by a Camerata Board member, asked to fill out a form, sit for an informal picture for our records, and type some information into our database.</p>
	  <p><strong><a name="WhenRehearsal" id="WhenRehearsal"></a>When is the first Camerata rehearsal?</strong><br />
	    The first rehearsal will be Thursday, August 27 at 1pm in the new PAHB Concert Hall, PAHB 144.  Auditions are completed the evening before and notification is sent to all students informing them, (1) who is seated in the ensemble for the semester, and (2) which singers have been accepted into the Music Performance Fellowship program. Seated students purchase music in the bookstore hours before the Thursday 1pm start time so as not to be stuck in the bookstore check out and late for rehearsal.</p>
	  <p><strong><a name="MPF" id="MPF"></a>How to apply to be a Music Performance Fellow?</strong><br />
The Music Performance Fellowship program is sponsored by the UMBC Department of Admissions to support select First-Year students in their desire to sing in Camerata and take voice lessons.  Four First-Years will be chosen for the program on the basis of their Camerata audition (any voicing).  Students invited to accept the fellowship sing in Camerata for the entire year and receive one-hour voice lessons with a UMBC professor free of charge.  Fellows register concurrently for <em>Camerata</em> (MUSC303 or MUSC358, see below) and Performance Studies (MUSC190). For a fellowship application, contact <a href="mailto:baileyc@umbc.edu">Connie Bailey</a>.</p>
	  <p><strong><a name="HowRegister" id="HowRegister"></a>How is registration for Camerata carried out?</strong><br />
	    On the basis of student auditions, names will be entered into the system giving them permission to register for<em> Camerata</em> (MUSC303).  Once the permissions are entered, each individual will log into the system to add the course to their schedule. Camerata is a two credit course.</p>
	  <p><strong><a name="ArtsHumanities" id="SmallGroup"></a>Will singing in Camerata satisfy an Arts and Humanities General Education Program (GEP) requirement?</strong><br />
	    Yes, if for one semester you register for <em>Music  in Performance </em>(MUSC358) instead of <em>Camerata</em> (MUSC303). <em>Music in  Performance</em> consists of your ensemble rehearsal times plus a one-hour,  once-a-week lecture course that introduces students to important topics in  music performance and criticism. </p>
	  <p></p>
	  <p>MUSC358, therefore, is a combined class and grade  between Camerata and your work in the lecture portion of the course. All First-Year  students who are seated in a Department of Music ensemble, including majors in  music, are strongly encouraged to register for MUSC358.</p>
	  <p><strong><a name="AdditionalThings" id="AdditionalThings"></a>What are the additional things for which students are responsible?<br />
	    </strong>Ladies are required to purchase a Music Department black performance dress and costume pearl necklace. The cost of the dress is approximately $68, and pearls approximately $7. Information on the dress and pearls will be supplied the second week of the semester. Men are required to own a black suit, a solid black silk  necktie,  a dress black shirt, a black belt, and black dress  shoes. In addition, students are required to purchase scores from the UMBC  bookstore in time for the first rehearsal. Semester packets are typically no  more than $25 or so.</p>
	  <p><a name="honorssection" id="honorssection"></a><strong>I am a student in the Honors College at UMBC. Is there an Honors section of Camerata for which I may enroll?</strong></p>
	  <p>Yes, it is Camerata (MUSC303H). This Honors section is a 3-credit course. Students research the literature Camerata is studying, prepare short presentations for the class, and turn their research into concise spoken program notes for the concert. Honors students may enroll as many times as they wish in MUSC303H, but only one of those semesters, of course, will satisfy the requirements of the Honors College.</p>
