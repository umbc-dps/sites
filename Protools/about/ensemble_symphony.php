	  <p>Upon the retirement of Wayne Cameron after 20 years as  conductor of the UMBC Symphony Orchestra, <strong>E. Michael Richards</strong> assumed the responsibilities of conductor beginning in the Fall of 2007.  Dr. Richards has 30 years of conducting  experience, including work as conductor of the Hamilton College Orchestra for  17 years, the Bowdoin College Orchestra, and as assistant conductor with the La  Jolla Civic Orchestra (San Diego).  He  has also guest conducted the Syracuse Society for New Music, and served as  woodwind/clarinet coach with the Syracuse Symphony Youth Orchestra, the  Rochester Philharmonic Youth Orchestra, and the Greater Baltimore Youth  Orchestra. </p>
