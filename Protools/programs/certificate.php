	This program is designed for four types of students:
	  <ol>
	    <li>International  music students looking to enter a US graduate school in music, but who don’t  yet have the required TOEFL skills for entrance (the UMBC certificate program  offers music courses to students with TOEFL skills under 500, in addition to  English and TOEFL courses to prepare students for qualifying for graduate  school in the US).</li>
	    <li>International music students looking for a  one-year experience in the US <br />
	      studying music and English.</li>
	    <li>American  students (and other students whose native language is English) looking to  strengthen their skills before applying to graduate music programs.</li>
	    <li>American students (and other students whose native language is English)  who wish to focus their music study on  American contemporary music.         </li>
	  </ol>
	  <p>UMBC faculty are all recognized experts in the creation and performance of contemporary music. Collectively they have premiered and recorded over a thousand new works in the past 30 years. Students have full access to the Recital Hall, which houses a beautiful 9-foot Steinway, and to the state-of-the-art recording facilities on campus. The program is open to both American and international students (who also take English language classes), enabling rich cultural exchange and collaboration among participants.</p>
	  <p>Student performers  are paired with faculty who are experts in the performance of contemporary  music, and develop repertoire through weekly private lessons, informal and formal recitals and ensemble performances. </p>
	  <p>Music technology students work closely with faculty in a variety of recording, video and sound design projects with high-end equipment and software. Student composers collaborate with student and faculty ensembles for readings and performances of their work along with private lessons in composition.Students applying for graduate school are mentored and assisted by faculty.  Credits earned at UMBC often transfer to other institutions.</p>
