 <h3>Admissions: Master's Degree </h3>
 <h4>Application Dates</h4>
<p>The application deadline for Spring 2017 is <strong>December 1</strong><br>
  Spring 2017 Classes Start: <strong>January 30, 2017</strong></p>
  
  <p>The application deadline for Fall 2017 is <strong>August 1</strong><br>
  Fall 2017 Classes Start: <strong>August 30, 2017</strong></p>

 <h4>Admissions Requirements:</h4>
 <p>Applicants to the MPS degree program are expected to have current IT or cybersecurity industry experience and/or a solid academic background in computer science, information systems, or a field related to working in cybersecurity. Those without such backgrounds must demonstrate other relevant professional or academic expertise that may qualify them for admission.</p>
 <ul>
   <li>GRE scores are not required for admission</li>
   <li>A bachelor’s degree in Computer Science, Information Systems, Mathematics, or a related field. </li>
   <li>Applicants should have a minimum undergraduate GPA of 3.0 on a 4.0 scale. (Applicants with an appropriate level of knowledge and professional experience in the IT or Cybersecurity fields are strongly encouraged to apply.)</li>                                                          
   <li>International applicants should refer to the <a href="international.php">International Admission Page</a> for special admission information. </li>                 
   </ul>
 <h4>Application Process:</h4>
 <p>Applications for the Master's program&nbsp;are accepted for the Fall and Spring semesters:</p>

 <ul>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_blank">Apply online</a>
     <ul>
              <li>Select proposed program Cybersecurity  &gt;&gt; Cybersecurity (MPS)</li>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo; section</li>
       <li>Recommendations are optional. Please put N/A in these fields if you choose not to enter names of references.</li>
       <li>Complete residency information to be reviewed for in-state tuition rates</li>
     </ul>
   </li>
   <li style="list-style-type:decimal">Request official transcripts from all post-secondary institutions to be sent to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or emailed to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
   <li style="list-style-type:decimal">Submit resume to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a> or <a href="mailto:gambino@umbc.edu">gambino@umbc.edu</a>.</li>
 </ul>
 <p>Your application should include the following   documents:  <em>(Incomplete applications will not be reviewed.)</em></p>
 <ul>
   <li>Completed application including residency form (residency form determines in-state tuition eligibility)</li>                
   <li>Statement outlining the student's goals and expectations in the program. This also is an opportunity for applicants to further explain their background and qualifications for the program and provide any additional information about themselves or their career experience that they feel will benefit the evaluation of their application.</li>                
   <li>Current resume    </li>
   <li>Recommendation letters (optional)</li>                          
   <li>Official transcript(s) from each college &amp; university attended</li>                
   <li>Non-refundable application fee ($50 online) </li>
 </ul>
 <h4>Mailing Instructions </h4>
 <p><strong>All application documents should be mailed to:</strong> <br>
UMBC Graduate School, MPS: Cybersecurity <br>
   1000 Hilltop Circle<br>
   Baltimore, MD 21250</p>
