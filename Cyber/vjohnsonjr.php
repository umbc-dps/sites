 <h3>Meet Van Johnson Jr.,  UMBC Cybersecurity MPS Alumnus, 2013</h3>
 
<p><img src="images/alumni/UMBC_CyberAlum3.gif" alt="UMBC Cybersecurity Alum Andrew Blair" align="left" style="padding:5px">After earning an undergraduate degree in Information Systems  from UMBC and spending a few years working in sales, Van Johnson Jr. decided he  needed a career in an industry he was passionate about.   </p>

       <p>Johnson, who has always been interested  in technology, also spent some time in the military, where he developed an  interest in security.  In cybersecurity,  he found an industry that combined his interests, while providing an encouraging  career outlook. </p>
       
       <p>&ldquo;I wanted to find a  degree program that not only had a promising career that paid well, but in an  industry that I also actually enjoyed to work in,&rdquo; Johnson said. </p>
       
<p>Johnson graduated from UMBC with a <a href="http://www.umbc.edu/cyber/">Master's in Professional Studies (MPS) in Cybersecurity</a> in 2013 and now works as a Business  Analyst and Project Manager with the U.S. Department of Health and Human Services.</p>

 <p>While in UMBC&rsquo;s Cybersecurity Program, Johnson felt he  benefitted from the in-person format. &ldquo;UMBC is one of the few schools that  offers in-person options for their classes. I didn&rsquo;t feel that I would get the  full experience from an online university,&rdquo; he said.   </p>
 
 <p>Interactions with program faculty were another advantage of  the program, Johnson recalls. &ldquo;I feel like the most valuable component of  UMBC&rsquo;s program was the professors with real-world experience who were  constantly bringing that back into the classroom,&rdquo; said Johnson. &ldquo;You weren&rsquo;t  constantly just getting a textbook lesson; you were getting to understand how  this knowledge can be applied to the real world.&rdquo;</p>
 
 <p>That real-world perspective is especially important in the  cybersecurity industry.  &ldquo;The professors  in the program were actively involved in what was going on,&rdquo; Johnson said.  Cybersecurity is constantly changing and  developing, so having someone who has eyes-on and hands-on is a great asset.&rdquo; </p>
 
 <p>In the future, Mr. Johnson hopes he can continue to grow  into a role as a senior security analyst in the government sector.  &ldquo;If I had not gotten my MPS from UMBC, I  would not have been able to even think about that type of career path.&rdquo;  </p>
 
