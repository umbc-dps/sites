<h3>Frequently Asked Questions</h3>
  <div class="accordionheader">
    <h4>What is cybersecurity?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Cybersecurity has emerged as a critical domain of global competition that reaches across the social, economic, political and military realms of influence.</p>

      <p>National Security Presidential Directive 54/Homeland Security Directive 23 (NSPD-54/HSPD-23) defines cyberspace as the interdependent network of information technology infrastructures, and includes the Internet, telecommunications networks, computer systems, and embedded processors and controllers in critical industries. Common usage of the term also refers to the virtual environment of information and interactions between people.</p>

      <p>This infusion of the Internet and its related networked technologies into nearly every aspect of society, business, and government presents a target of opportunity for adversaries. As a result, it is not surprising to see nations and assorted international organizations developing new operational doctrines, advanced cyberwarfare capabilities, and the necessary domain-focused human capital needed to achieve or maintain their interests in cyberspace.</p>

      <p>Meeting these challenges successfully requires developing qualified professionals who understand this environment and the risks (or opportunities) therein. UMBC stands ready to help build this next generation of cybersecurity professionals.</p>    </div>
  <div class="accordionheader">
    <h4>Why study cybersecurity at UMBC?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>UMBC is uniquely positioned to provide education and training that respond to the state’s need for qualified technical professionals in the cybersecurity field:

      <ul class="square">
        <li><strong>Reputation and Affordability:&nbsp;</strong> From 2009-2014, UMBC was ranked #1 in the <em>U.S. News and World Report's</em> list of "national up-and-coming" universities.&nbsp;Providing exceptional value at a public institution price, UMBC offers bachelor's, master's, and doctoral degrees in a variety of fields relevant for cybersecurity.&nbsp;UMBC is certified as a Center of Academic Excellence in Information Assurance Education (CAE) as well as a Center of Academic Excellence in Research (CAE-R) sponsored by the National Security Agency and Department of Homeland Security (DHS).&nbsp;Students attending UMBC are eligible to apply for scholarships and grants through the Department of Defense Information Assurance Scholarship Program.</li>

        <li>
          <strong>Industry Relevance:</strong> To ensure that UMBC’s cybersecurity programs are current, relevant, and responsive to the needs of industry, UMBC formed a <a href="advisoryboard.php">Cybersecurity Advisory Board</a> in 2009.&nbsp;Consisting of representatives from organizations such as MITRE, Northrop Grumman, Battelle, and others, the board continues to provide guidance on the development of UMBC’s cybersecurity programs.&nbsp;
        </li>

        <li><strong>Convenient Location: </strong>UMBC is conveniently located off of I-95 near the BWI Airport, approximately ten miles from Fort Meade (future home of DISA and the U.S. Cyber Command).&nbsp;UMBC is convenient to a host of federal agencies and government contractors that need cyber-related research, education, and training.&nbsp;</li>
      </ul>
 </div>
  <div class="accordionheader">
    <h4>Are courses offered online?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      UMBC's Graduate Cybersecurity Program is an in-person classroom academic experience. Although some courses may have online components, the program is not considered "online" or "distance-based".    </div>
  <div class="accordionheader">
    <h4>What programs does UMBC offer in cybersecurity?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Undergraduate and graduate students have the option to complete concentrations in Information Assurance—as part of degree programs in computer science, information systems, electrical engineering, and computer engineering.</p>

      <p>In addition, UMBC offers two new graduate programs in cybersecurity:</p>

      <ul class="square">
        <li><strong>Post-Baccalaureate Certificate in Professional Studies: Cybersecurity Strategy and Policy</strong>: This four-course post-baccalaureate certificate can be completed in a year.&nbsp;Because these courses are not technical (although a technical background may be helpful), this program is available to students with a variety of undergraduate backgrounds.&nbsp;Once accepted into the M.P.S.: Cybersecurity, all four courses count toward that degree.&nbsp;Students may choose to take this certificate by itself, or they may take this certificate and then later complete the master’s.</li>

        <li><strong>Master's in Professional Studies: Cybersecurity:</strong> This ten-course master’s degree incorporates courses in cybersecurity strategy, policy, and management with more technical, hands-on cybersecurity courses.&nbsp;Rather than having to choose between a management or policy-oriented cybersecurity degree or a completely technical cybersecurity degree, the M.P.S. allows you to customize the mix of courses that best meets your career development needs.&nbsp;The master’s degree may be completed in two to three years, depending on how many courses you take each semester.&nbsp;Classes are offered fall, spring, and summer semesters. M.P.S. courses are offered in a classroom or hybrid (50% online, 50% classroom) format. This approach offers flexibility for students while also providing opportunities for in-person professional networking through a dynamic seminar and research experience.</li>
      </ul>    </div>
  <div class="accordionheader">
    <h4>How does a Master in Professional Studies degree (M.P.S.) differ from a Master of Science degree (M.S.)?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>The M.P.S. is a master’s degree that combines management and courses with advanced study in a scientific or technical discipline. The M.P.S. Cybersecurity incorporates management and policy courses with more technical courses in order to prepare you for the human, political, and technical aspects being a cybersecurity professional. Rather than preparing you to perform pure research and/or pursue an academic career, this degree program is designed to help you advance your professional career.</p>

      <p>When developing this program, our Cybersecurity Advisory Board emphasized the need for educated employees who are both technically proficient and can communicate well, manage projects, and work successfully on teams. Accordingly, several classes include relevant group projects conducted both in-person as part of their syllabus. Thus, the M.P.S. combines the practical expertise of an M.B.A. with the technical knowledge of an M.S. degree.</p>    </div>
  <div class="accordionheader">
    <h4>As a current UMBC undergraduate student, can I take cybersecurity courses as electives?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Yes. However, you should check first with your academic advisor and/or the cybersecurity graduate program director to determine course availability, your suitability for these courses and how they fit into your academic program.</p>    </div>
  <div class="accordionheader">
    <h4>I have a CISSP or other industry cybersecurity certificate. Can it transfer into the M.P.S. or post-baccalaureate certificate program for credit?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Unfortunately, we do not accept industry certifications for transfer credit into our cybersecurity graduate programs. However, UMBC can assist you in obtaining such non-credit certifications through the UMBC Training Center if you are interested.</p>    </div>
  <div class="accordionheader">
    <h4>Does UMBC offer a cybersecurity Ph.D. program?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>Not at this time. However, UMBC does offer postgraduate programs in computer science and/or information systems where you can pursue cybersecurity research at the doctoral level. Contact these departments for additional information. (Courses taken within the cybersecurity M.P.S. may be applied towards those programs in some situations.)</p>    </div>
  <div class="accordionheader">
    <h4>How are UMBC's cybersecurity programs courses delivered?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>UMBC's cybersecurity program courses are offered in two main formats—traditional classroom delivery and hybrid courses. Classroom courses provide opportunities for rich interaction between instructors and students, and hybrid courses add the benefit of schedule flexibility by reducing the number of in-person class sessions. Hybrid courses replace up to 50% or more of class sessions with online activities that occur asynchronously (class does not convene online at a fixed time). In the online portions of a hybrid class, students work on self-paced activities and assignments (e.g., asynchronous discussion boards) or interact as part of small groups (e.g., discussions, chats/forums, project/assignments).</p>    </div>
  <div class="accordionheader">
    <h4>Can I transfer graduate credits from another institution into the UMBC Cybersecurity Program?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>You may be able to transfer <strong>up to six</strong> graduate credit hours earned from another institution into a UMBC cybersecurity program. Contact the Graduate Program Director, <a href="richard.forno@umbc.edu">richard.forno@umbc.edu</a>, to discuss your situation and be sure to include a complete course description. A 3.0 GPA is required for each course transferred. Once you are admitted and an enrolled student in UMBC's program, you must complete and submit a <a href="http://gradschool.umbc.edu/students/forms/">Transfer of Credit Request Form</a>.</p>    </div>
  <div class="accordionheader">
    <h4>Are GRE's required?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      GREs are only required if the undergraduate degree was not completed at an accredited U.S. university. Graduate Record Exam (GRE) minimum combined score of 306. Verbal Reasoning should be at least 153 and Analytical Writing at least 4.5.    </div>
  <div class="accordionheader">
    <h4>Is there a thesis requirement?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      No, there is no formal thesis defense. However, the CYBR MPS includes a semester-length research project which involves significant research and writing.    </div>
  <div class="accordionheader">
    <h4>How many courses can (or should) I take?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      Typically, students who are working full-time enroll in 1 or 2 courses per semester. Enrolling in more than 3 courses during a semester while also working full-time is strongly discouraged.    </div>
  <div class="accordionheader">
    <h4>How long do I have to complete my degree?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      According to University policy, students have five years to complete their degree requirements.    </div>
  <div class="accordionheader">
    <h4>What financial aid opportunities exist for me?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      Because this program is designed primarily for working professionals, graduate or teaching assistantships are not available. In many cases, students receive financial support from their employers. However, for Cybersecurity M.P.S. students only, two specialized federal scholarship programs are available: The DoD Information Assurance Scholarship Program (IASP) and Federal Cybercorps Scholarship for Service (SFS) programs. Both cover full tuition, annual stipends, and other educational costs in exchange for working in a cybersecurity capability for the US government upon graduation. US Citizenship, full-time enrollment, and ability to obtain a US Government security clearance are required. Visit <a href="http://www.cisa.umbc.edu/scholarships.php">http://www.cisa.umbc.edu/scholarships.php</a> for more information and application information.    </div>
  <div class="accordionheader">
    <h4>Where are courses held?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>The UMBC Graduate Cybersecurity Program is <strong>not</strong> an online or distance-based program. Courses are held at UMBC Main Campus in Catonsville, the Universities at Shady Grove (USG), and other locations as indicated <a href="locations.php">here</a>. Consult the Schedule of Classes for information on times and locations for classes during a given semester.</p>    </div>
  <div class="accordionheader">
    <h4>Do you admit students for summer sessions?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>No. We only admit students for Fall and Spring semesters.</p>    </div>
  <div class="accordionheader">
    <h4>Are summer courses offered?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      Yes, we generally offer one or more courses (either 6, 8, or 12 weeks) over the summer session. Consult the Schedule of Classes for information on times and locations. Summer courses may meet more frequently due to the shortened summer schedule.    </div>
  <div class="accordionheader">
    <h4>I am a recent college graduate. Can I apply?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      The UMBC Cybersecurity MPS degree is designed for professionals with several years of experience in the IT or cyber operations industry. Recent graduates may be admitted based on the relevance of the applicant's undergraduate degree field, academic performance, extracurricular activities, and stated career goals.    </div>
  <div class="accordionheader">
    <h4>Can UMBC help me obtain a cybersecurity job?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      While at UMBC, you may take advantage of UMBC's Career Services Center and other special professional networking opportunities, job fairs, training programs, and related events related to the cybersecurity industry. We provide these tools and opportunities for students to explore and research the cybersecurity profession and enhance your 'marketability' and/or value as a cybersecurity practitioner. However it is up to you to take advantage of them as well as continue to learn about the field on your own outside of the classroom – a graduate degree alone will not automatically lead to employment in this field.    </div>
  <div class="accordionheader">
    <h4>Can I take courses both at UMBC's Main Campus and the Universities at Shady Grove?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      No. Students must take courses offered at their enrolled campus.    </div>
  <div class="accordionheader">
    <h4>How can these programs help with my career development in a 
      cyber-related field?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      UMBC’s cybersecurity programs are designed for students and working professionals who aspire to make a difference in the security, stability, and functional agility of the national and global information infrastructure. Our graduate programs prepare students to create and lead processes, or serve as managers or team members in cyber-related fields within government, military and industry such as:

      <ul class="square">
        <li>Network and Information Security</li>
        <li>Intelligence</li>
        <li>Information Operations</li>
        <li>Law Enforcement and Counterintelligence</li>
        <li>System and Operational Requirements Analysis</li>
        <li>Cybersecurity Procurement</li>
        <li>Cybersecurity Exercise, Test and Evaluation</li>
      </ul>    </div>
  <div class="accordionheader">
    <h4>What's the demand like for cybersecurity professionals?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>The demand for professionals with cybersecurity expertise is strong and growing. Cybersecurity is critical to maintaining our national infrastructure—including the banking system, the stock market, the power grid, and military operations. The need for cyber-literate professionals is complex and crosses many fields. From engineers and computer scientists to information technology professionals and federal managers, qualified professionals are needed to manage cybersecurity operations, as well as to do the hands-on work.</p>

      <p>In July 2009, Booz Allen Hamilton and the Partnership for Public Service published a valuable report on the state of cybersecurity workforce preparedness—“Cyber IN-SECURITY—Strengthening the Federal Cybersecurity Workforce.” This report highlights the critical national shortage of employees who are adequately educated and trained in the cybersecurity area.</p>

      <p>Moves associated with the Base Realignment and Closure Act (BRAC) will create a significant increase in demand for engineering, information technology, and cybersecurity related education and training. An overall estimate of 100,000 new federal jobs and transferred positions due to BRAC has been planned for over the next seven years. The IT related positions are estimated to be over 45,000 new jobs moving to or being created in the greater Baltimore area that will have a strong connection to cybersecurity.</p>    </div>
  <div class="accordionheader">
    <h4>I don't have <u>any</u> IT or cybersecurity experience. Can I apply?<i class="fa fa-angle-down"></i></h4></div>

    <div class="accordionbody">
      <p>The UMBC Cybersecurity MPS degree is designed for professionals with several years of experience in the IT or cyber operations industry. Applicants without that prior knowledge are welcome to apply -- in addition to academic performance, admission decisions will be based on the applicant's career activities, stated goals, and their overall suitability for the program.</p>
    </div>    
