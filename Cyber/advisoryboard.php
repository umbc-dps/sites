            <p>James Howland is a Technical Director of MITRE&rsquo;s E530, Electronic Systems and Technology division. E530 works across MITRE&rsquo;s C3I Federally Funded Research and Development Center sponsors, and is dedicated to technical expertise focused on communications, electromagnetics, sensors, and signal processing technologies. He previously served also as the corporate program lead for several counter improved explosive devices programs for both the DoD and intelligence communities.</p>


            <p>Mr. Howland previously served as an associate director in MITRE&rsquo;s Washington C3 Center, from October 2002 to October 2006. In that capacity he was responsible for the communications, networking, and signal processing technical departments and served as a technical director in W900. In that capacity he was responsible for technology support and W900 technical center support to the intelligence community. He was responsible for expanding the WC3 laboratory and hands-on work programs and assisted in developing the corporate laboratory and capital equipment resources. Prior to his assignment as a director, Mr. Howland was a responsible for the direction of WC3&rsquo;s Signal Processing Center as a department manager.</p>


