 <h3>Admissions: International Students</h3>
 <p>The following information is provided for international applicants interested in applying to the Masters of Professional Studies in Cybersecurity. International applicants should read the information provided carefully prior to applying.</p>
 <h4>International Student Admission Requirements:</h4>
 <p> All students need to meet the following minimum requirements:</p>
 <ul>
   <li>A bachelor’s degree in Computer Science, Information Systems, Mathematics, or a related field  (Applicants with professional experience in the IT or Cybersecurity fields are strongly encouraged to apply.)</li>
   <li>Current resume</li>
   <li>Personal statement outlining the student's goals and expectations of the program.  Statement should include applicant’s background, qualifications, career experience, and  any additional pertinent information.</li>
   <li>Minimum undergraduate GPA of 3.0 on 4.0 scale, or equivalent, is desired.  Applicants with grades on a scale other than 4.0, will be evaluated based on standards for the country of their degree.</li>
   <li>Graduate Record Exam (GRE) scores with a minimum combined score of 306. Verbal Reasoning should be at least 153 and Analytical Writing at least 4.5. GRE scores are not required if your undergraduate degree was completed at an accredited U.S. university.</li>
   <li>TOEFL Scores: Minimum scores: 99 (iBT). Scores must be less than 2 years old. iBT Score Breakdown: Writing (23), Listening (23), Reading (25), Speaking (28, 23 acceptable if have 2 years' work experience in supervisory or management position in the U.S.) OR</li>
   <li>IELTS Score: Minimum score of 7.5 required</li>
 </ul>
 <p>*If you have a three-year degree versus a four-year degree, your transcript will be reviewed to determine whether you have met the necessary pre-requisites for the program. Eligibility for admission can only be determined upon receipt of a complete application.</p>
 
 
 <h4>Application Process</h4>
 <p>All international applicants are encouraged to apply as early as possible to allow sufficient time to receive all the necessary documents and to process and assess the application.</p>
 <p>International applications are accepted for the fall and spring semester. We encourage all international applicants to send in their documents at least six months prior to the start of the semester applying to.</p>

 <ul>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_blank">Apply online</a>
     <ul>
       
          <li>Select proposed program Cybersecurity  &gt;&gt; Cybersecurity (MPS)</li>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo; section</li>
       <li>Insert names of references into “References” section (Note: we only require two. Put N/A in third line)</li>
       <li>Complete residency information to be reviewed for in-state tuition rates</li>
     </ul>
   </li>
   <li style="list-style-type:decimal">Request official transcripts from all post-secondary institutions to be sent to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or emailed to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
   <li style="list-style-type:decimal">Submit resume to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a> or <a href="mailto:gambino@umbc.edu">gambino@umbc.edu</a>.</li>
   <li style="list-style-type:decimal;">Request for test  scores to be sent by ETS to UMBC (code 5835)</li>
 </ul>
 
 <p><strong>All documents should be mailed to:</strong></p>
 <p>UMBC Graduate School, M.P.S. Cybersecurity<br>
   1000 Hilltop Circle<br>
   Baltimore, MD 21250 U.S.A.</p>

 <p><strong>Your application packet should include the following documents:</strong></p>
 <ul>
   <li>Completed degree-seeking application (including residency form). Use program code CYBR and concentration code (Refer to Master's Admission page for list of codes).</li>
   <li>Two letters of recommendation</li>
   <li> Personal statement</li>
   <li>Official transcript(s) from each college &amp; university attended</li>
   <li>Graduate Record and TOEFL scores forwarded to UMBC by ETS - UMBC’s institutional code is 5835</li>
   <li>Application fee ($50 if apply online / $70 if apply by mail)</li>
   <li>If paying by check, the check must be drawn on a bank with a U.S. location. The check should be payable to UMBC.</li>
 </ul>
 
 <p><strong>Please note:</strong> All international students are required to submit TOEFL scores unless you attended a university in an English-speaking country or hold an undergraduate degree from a U.S. institution. IELTS score are accepted in substitution for TOEFL scores.</p>
 
 <h4>Obtaining Immigration Documents</h4>
 <p>If you intend to apply for an F-1 visa and require an I-20 from UMBC, please include the following additional documents with your application.</p>
 <ul>
   <li>Copy of valid passport</li>
   <li>Certified bank statement or bank verification letter from financial sponsor showing a minimum of $29,000 (USD) in available funds.</li>
   <li>Letter from financial sponsor stating they will be responsible for all living expenses and school-related expenses for the duration of your stay at UMBC.</li>
 </ul>
 <p>Send these documents after receipt admission acceptance.  Note: I-20 is processed after formal acceptance into the program.</p>
 <p>All immigration related questions should be addressed to International Education Services by phone at (410) 455-2624 or by e-mail, <a href="mailto:ies@umbc.edu">ies@umbc.edu</a>.</p>
 
 <h4>Financial Aid/Tuition/Assistantships</h4>
 <p>Presently, the program is not offering any financial aid or graduate assistantships.</p>
 <p>If you require further assistance, please contact Lisa Gambino at 410-455-3034 or email <a href="mailto:gambino@umbc.edu">gambino@umbc.edu</a></p>    
