<p>Join us for a UMBC Cybersecurity Webinar! </p>

<p>Dr. Terry Thompson, of the UMBC Cybersecurity Graduate Program faculty, will present on the political, economic, military, and foreign policy dimensions of China's cyber strategy and operations.
</p>

<strong>We will explore: </strong>
<ul>
  <li>How does cyber fit into China's military strategy?</li>
  <li>Why is there so much focus on offensive cyber operations?</li>
  <li>What is the Chinese view of the U.S. response (or lack of responses) to their cyber attacks on U.S. government and industry?</li>
  <li>Given the large amount of U.S. debt held by China, what is the rationale for cyber attacks that can damage the U.S. economy?</li>
  <li>Who is behind the strategy and operational planning and execution of China's cyber attacks on the U.S.? </li>
</ul>

<p>This webinar is a preview of Dr. Thompson's Spring 2016 course CYBR 691 Special Topics in Cybersecurity: "Chinese Cyber Power: Perspectives and Implications."</p>

<p><em>Please Note: Due to limited time, not all topics may be discussed during the webinar. </em>
