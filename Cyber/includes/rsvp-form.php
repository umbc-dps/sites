<span>Join us for a UMBC Cybersecurity Webinar! 
<br>
<br>Dr. Terry Thompson, of the UMBC Cybersecurity Graduate Program faculty, will present on the political, economic, military, and foreign policy dimensions of China's cyber strategy and operations.
<br>
<br>We will explore: 
<br>* How does cyber fit into China's military strategy? 
<br>* Why is there so much focus on offensive cyber operations?
<br>* What is the Chinese view of the U.S. response (or lack of responses) to their cyber attacks on U.S. government and industry? 
<br>* Given the large amount of U.S. debt held by China, what is the rationale for cyber attacks that can damage the U.S. economy? 
<br>* Who is behind the strategy and operational planning and execution of China's cyber attacks on the U.S.?
<br>
<br>
<br>This webinar is a preview of Dr. Thompson's Spring 2016 course CYBR 691 Special Topics in Cybersecurity: "Chinese Cyber Power: Perspectives and Implications."  Please note: due to limited time, not all topics may be discussed during the webinar. 
