<h4 style="font-weight:bold">UMBC Cybersecurity and GIS Forum</h4>
<h6>​​The UMBC Cybersecurity and GIS Forum on Privacy and Security will explore the legal concerns of topics from personal privacy and tracking​,​ to exploiting spatial data for security, law enforcement, and/or regulatory purposes. Join UMBC's expert faculty, distinguished speakers, technology demonstrators, and local peers on Friday, June 9th for this one-day event that​ addresses the complex relationship between technology, cybersecurity and geospatial information.​ 
</h6>
<br>

<h6>
​The various sessions throughout the forum will ​cover questions​ such as​ : Is a new approach to risk analysis and/or liability required? How can law and policy catch-up to actual real-world practices? What other security concerns does this present? This dual-track ​f​orum will drive an interactive discussion with the goal of fostering greater understanding of the issues, while providing viable approaches to addressing them ​in the future.</h6>
<br>


<h4 style="font-weight:bold">Date:</h4>
<h5>Friday, June 9, 2017, 7:30AM - 3:00PM</h5>
