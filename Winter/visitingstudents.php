<p>Students who attend another college or university (and wish to earn credits at UMBC to transfer back to their home institution) must first apply for admission to summer or winter sessions. Visiting student admission is only valid for a single, non-consecutive summer or winter session.</p>

<p>The deadline for visiting students to apply for Winter Session 2017 is December 21. Applications cannot be accepted after the admission deadline dates.</p>

<div class="accordionheader">
	<h4>Apply<i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">

<ul>
<li>Apply as a visiting <a href="applyundergraduate" target="_blank">undergraduate student</a></li>

<li>Apply as a visiting <a href="applygraduate" target="_blank">graduate student</a></li>
</ul>
</div>

<div class="accordionheader">
	<h4>Visiting Student Requirements<i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
<ul>
<li>Must apply for admission to UMBC as a visiting student by December 21.</li>

<li>Must be admitted before registering for courses (last day to register without a late fee is January 3).</li>

<li>Late registration for classes with open seats is January 6.</li>

<li>Must be in good academic standing at your home institution and provide a current transcript to enroll in UMBC courses with prerequisite requirements.</li>

<li><strong>NOTE:  Visiting students will initially be blocked from registering in any UMBC course with prerequisite requirements. You must first obtain <a href="coursesprerequisites" target="_blank">a prerequisite override</a> before you can enroll.</strong></li>

<li>If you attend another University of Maryland System (USM) school, please be aware that USM inter-institutional enrollment only applies to fall and spring semesters. Therefore, students from other USM schools who wish to enroll in summer or winter session at UMBC must apply as visiting students.</li>
</ul>
</div>

<div class="accordionheader">
	<h4>Confirm Your Status Before You Apply<i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
<ul>
  <li>If you plan to enroll at UMBC over multiple consecutive terms (including fall and spring), but do not intend to pursue a full degree program or major, apply as a <a href="http://www.umbc.edu/undergraduate/apply/nondegree.html" target="_blank">non-degree student</a>.</li>

<li>If you previously attended UMBC and left in good standing, apply for <a href="http://www.umbc.edu/undergraduate/apply/readmit.html" target="_blank">readmission</a>.</li>

<li>If you previously attended UMBC and did not leave in good standing, apply for <a href="http://www.umbc.edu/undergraduate/apply/reinstate.html" target="_blank">reinstatement</a>.</li>

<li>If you are currently a high school student and want to earn college credits apply for <a href="http://www.umbc.edu/undergraduate/apply/early_enroll.html" target="_blank">early admission</a>.</li>

<li>If you are an <a href="incomingstudents" target="_blank">incoming new student</a> (admitted to begin full- or part-time at UMBC in the fall or spring) you do not need to apply for admission to enroll in the preceding summer or winter session. <a href="http://www.umbc.edu/undergraduate/quicklinks/contact.html" target="_blank">Contact</a> the Office of Undergraduate Admissions and ask to change your semester start date to the summer or winter term.</li>
</ul>
