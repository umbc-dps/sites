    <h4>Schedule adjustment period<i class="fa fa-angle-down"></i></h4>
  </div>

<div class="accordionbody">
     <ul>
       <li>During the <a href="scheduleadjustment">Schedule Adjustment Period</a> you can swap courses (with the  same number of credits) with no academic or financial penalty.</li>
       <li>Use <a href="http://my.umbc.edu/topics/classes-and-grades"><em>my</em>UMBC</a> to add or drop summer courses through January 7, 2016 with no academic or  financial penalty.</li>
       <li>After January 7, 2016, please be aware of  <a href="importantdates.php">academic and financial implications</a> for adding or dropping  courses.</li>
     </ul>
     
     </div>   
  
  <div class="accordionheader">
    <h4>How to search the online schedule of classes <i class="fa fa-angle-down"></i></h4>
  </div>

<div class="accordionbody">
     <ul>
       <li><em>my</em>UMBC  displays the entire catalog of UMBC course subjects, but not all subjects are  offered each semester.</li>
       <li> <a href="courses" target="_blank">View courses  offered</a>  by  subject during the session.</li>
       <li>Within the course listings, course descriptions provide  information about seat availability, additional fees, class format and  enrollment requirements.</li>
       <li><a href="https://www.youtube.com/watch?v=hXIWIzOsn5w" target="_blank" class="video">How to search the schedule of classes</a></li>
     </ul>
     
     </div>   
  
  <div class="accordionheader">
    <h4>Waitlist information<i class="fa fa-angle-down"></i></h4>
  </div>

<div class="accordionbody">
     <ul>
       <li>If a course if full, use <em>my</em>UMBC to place your name on a waitlist.</li>
       <li>When adding the class during registration, check the  box &quot;Waitlist if class is full&quot; and continue the enrollment process.</li>
       <li>If the class has a waitlist, you will be notified of  your position on the waitlist after you have completed registration.</li>
       <li>If a seat opens in a closed course, and you are in the top position on the waitlist, you will be notified via your umbc.edu email address that you have been enrolled in the class. </li>
       <li>It is your responsibility to check your umbc.edu email account regularly to see if you have been enrolled into a class for which you placed yourself on the waitlist.</li>
       <li>You will NOT be automatically enrolled from a waitlist  if you have a time conflict with another course, or if you try to enroll for  excess credits (without approval).</li>
       <li>The last day students will automatically be enrolled in  classes from the waitlists is <strong>January 1</strong>.  After this date, contact the Office of Summer and Winter Programs for  assistance regarding seat availability.</li>
     </ul>
     
     </div>   
  
  <div class="accordionheader">
    <h4>Credit limits and excess credit approval<i class="fa fa-angle-down"></i></h4>
  </div>

<div class="accordionbody">
     <ul>
       <li>You can enroll in up to 8 credits per summer session  (maximum 16 total credits in summer session) </li>
       <li>You can enroll in up to 4.5 credits in winter session.</li>
       <li>Permission to enroll in excess credits requires  advanced approval from your primary major advisor and the Office of Academic  and Pre-Professional Advising.</li>
       <li>Request <a href="excesscredit">excess  credit approval</a> at least one week before classes begin.</li>
     </ul>
     
     </div>   
  
  <div class="accordionheader">
    <h4>Course time conflicts<i class="fa fa-angle-down"></i></h4>
  </div>

<div class="accordionbody">
     <ul>
       <li>You are not permitted to register for more than one  course that meets at the same time or overlaps in meeting patterns. The  registration system will block your enrollment.</li>
       <li>If there are extenuating circumstances, you may be  permitted to register for courses that share a time conflict if the overlap is  minimal and both instructors agree to the exception.</li>
       <li>To request a time conflict exception, complete a <a href="http://registrar.umbc.edu/files/2012/06/Time-Conflict-Exceptions-Form.pdf">time conflict form</a>, obtain written approval  from both instructors, and submit the signed form to the Registrar&rsquo;s Office  before the end of the <a href="scheduleadjustment">Schedule Adjustment Period</a>.</li>
     </ul>
     
     </div>   
  
  <div class="accordionheader">
    <h4>Course repeat policy<i class="fa fa-angle-down"></i></h4>
  </div>

<div class="accordionbody">
     <ul>
       <li>You may not register for a course more than two times.  You are considered registered for a course if you are enrolled after the end of  the schedule adjustment period. You may <a href="http://academicpolicy.umbc.edu/home/undergraduate-petitions/">petition</a> the Office of Undergraduate Education  (OUE) for a third and final attempt of a course taken at UMBC [or] another  institution.</li>
       <li>An attempt is defined as enrollment in a UMBC course  (or its equivalent at another institution) in which the student has received a  grade or an official withdrawal noted on the transcript. </li>
       <li> <a href="http://www.umbc.edu/undergrad_ed/ai/documents/courseRepeat_StudentChecklist.pdf">View</a> Course Repeat Checklist.</li>
     </ul>
     
     </div>   
  
  <div class="accordionheader">
    <h4>Independent study courses<i class="fa fa-angle-down"></i></h4>
  </div>

<div class="accordionbody">
     <ul>
       <li>Academic departments schedule independent study courses  on an &ldquo;as needed&rdquo; basis. To request the course be added to the schedule,  contact the appropriate academic department before the <a href="importantdates.html">final day</a> to register.</li>
     </ul>
