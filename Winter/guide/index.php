<h2 style="color:#FFF; font-weight:bold">Best Way to Recover from a Cold?</h2>

<h3>
<p>Sometimes the worst thing to have is a cold during finals, midterms, or even just during the semester. Period. Studying is stressful enough without having the sniffles, a constant cough, and a throbbing headache. </p>

<p>For poll #7, we asked students how they recover from a cold. At the top of the list came an oldie, but a goodie: sleep and rest.Other top suggestions included drinking tea, eating chicken noodle soup, and taking some over the counter medications from  Health Services. 
