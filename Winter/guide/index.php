<h2 style="color:#FFF; font-weight:bold">Best Way to Lose Holiday Pounds?</h2>

<h3>
<p>Holiday cheer, as great as it is, sometimes leads to a few unwanted holiday pounds. And who’s to blame with all the pies, cakes, and cookies laying around? But as college students, it’s sometimes hard to stay on your A game when trying to make the grade. </p>

<p>For poll #9, we asked students what the best way to lose holiday pounds. Most students suggested to get moving or hit the gym to get some much needed exercise. Another popular option was portion control, which means maybe not eating <em>all</em> the cookies. And surprisingly some students responded “just don’t,” because, at the end of the day, you have to eat!</p>

