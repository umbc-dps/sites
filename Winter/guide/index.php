<h2 style="color:#FFF; font-weight:bold">Favorite Holiday Movie?</h2>

<h3>
<p>There is nothing quite like snuggling up during a cold winter night and watching your favorite holiday movie. However, it’s often hard to remember to take a few moments to enjoy the holiday season when you’re running round the library finishing up last minute papers, and pulling all nighters to study for exams. It’s stressful just thinking about it. Holiday movies especially, are a great way to relax and detox from the college grind.</p>

<p>For poll #8, we asked students to name their favorite holiday movie. Elf topped the list, with classic family favorites like Home Alone, “It’s A Wonderful Life,” “Nightmare Before Christmas,” and “How the Grinch Stole Christmas” rounding out the list.</p>

