<h3>
<p>
Everyone knows the best way to survive as a college student is with a good cup of coffee (or 10)! But the real question is which coffee is the best coffee at UMBC? Poll #1 asked students just that. My personal favorite is Starbucks, because they do a really good job at making coffee not taste like coffee.</p>

<p>
According to the results it’s clear Starbucks takes the cake, ranking as number one with 114 votes. However, some students (51) still think that even though Starbucks tastes better than your average cup of Joe, Au Bon Pan is the place to go to wake up and get your day started. Pure La Vida and the Coffee Shoppe in the Administration Building finished with 23 and 14 votes, respectively. What do you think?</p>
