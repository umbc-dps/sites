<p>Online applications are managed by CollegeNET, UMBC's  application hosting service partner. CollegeNET's online application process is  safe and secure.</p>

<p><h5>Apply by December 21.</h5></p>

<h4>How to apply as a visiting undergraduate student</h4>

<ul>
  <li>Go to UMBC's <a href="https://www.applyweb.com/apply/umbc/">Undergraduate  Admissions Main Menu</a> page</li>
  <li>Create an account through ApplyWeb (the CollegeNET  website).</li>
  <li>Keep a record of your ApplyWeb user name and password.  You can save and end your session before you submit your application, and  you&rsquo;ll use this user name and password to access an application in progress.</li>
  <li>Once you have created your ApplyWeb account, choose the <strong>Visiting Students</strong> option.</li>
  <li>Log-in to &quot;ApplyWeb&quot; and complete the UMBC  visiting student application.</li>
  <li>If you plan on taking a course with a <a href="coursesprerequisites">prerequisite requirement</a>, be sure to include  your transcript showing that you have completed (or will complete) the  prerequisites prior to the start of the session.</li>
  <li>Submit a $50 non-refundable application fee by credit  card over a secure Internet connection.</li>
</ul>

<h4>Application confirmation</h4>
<ul>
  <li>You'll receive an e-mail confirmation from CollegeNET  immediately after you submit your application.</li>
  <li>You'll receive a confirmation from UMBC (sent to the  e-mail address you provide) once your application is forwarded from CollegeNET.</li>
  <li>Once received, it typically takes 3 - 5 business days  to process online applications.</li>
  <li>The UMBC confirmation e-mail will include your UMBC  campus ID.</li>
</ul>

<h4>Admission decisions</h4>
<ul>
  <li>UMBC sends admission decisions to the e-mail address  provided on your application.</li>
  <li>Once admitted, you will need to <a href="https://webadmin.umbc.edu/admin/User/Create">create</a> a <em>my</em>UMBC account to register for  classes using your UMBC campus ID.</li>
  <li><strong>Please note:</strong> the user name and password for the application account on ApplyWeb is <strong>not the same</strong> as the user name and  password used for your <em>my</em>UMBC  account.</li>
  <li> <a href="faq">Learn more</a> about <em>my</em>UMBC.</li>
</ul>

