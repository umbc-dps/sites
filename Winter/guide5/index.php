<h2 style="color:#FFF; font-weight:bold">Best Place For Coffee</h2>

<h3>
<p>
Everyone knows the best way to survive as a college student is through coffee! But the real question is which coffee is the best coffee at UMBC. Poll #1 asked students where the best place to get coffee was on campus. My personal favorite is Starbucks, because they do a really good job at making coffee, not taste like coffee.</p>

<p>
According to the results it’s clear Starbucks takes the cake, ranking as number one with 114 votes, leading Au Bon Pan with 51, and Pure La Vida and Admin with 23 and 14. However, some students think that even though Starbucks tastes better than your average dose of caffeine, Au Bon Pan is the place to go to wake up and get your day started. What do you think?</p>
</h3>


