 <h3>Green Office Initiative</h3>    
 <p>&nbsp;</p>
 <p><strong>What is the Green Office Initiative?</strong><br>
 </p>
  <p>It is UMBC's commitment   to sustainable practices with regards to energy conservation, water   conservation, waste reduction, and greenhouse gas emission reduction,   among other things.  <br>
    <br>
    Many offices around campus are already   Green Office Space certified. Dr. Hrabowski has asked that we redouble   our efforts to shift our offices to greener practices.  For more information, you can visit the <a href="http://sustainability.umbc.edu/" target="_blank">Sustainability Matters at UMBC Website</a>.<br>
    <br>
    To become   certified, we need your help and commitment to conserving our energy   resources. </p>
  <p><strong>DPS Greener Practices</strong></p>
  <p>The following checklist is DPS's commitment to focus on   greener practices.</p>
  <ol>
    <li>Turn off lights and electronics off when leaving any space for more than five minutes.</li>
    <li>Turn off lights and electronics when passing empty common spaces (conference/break room).</li>
    <li> Reduce number of bulbs in overhead lights. </li>
    <li>Unplug appliances and/or turn off power strips to reduce energy drain. </li>
    <li>Post reminder labels above light switches and outlets. </li>
    <li>Use natural sunlight and task lighting (such as a desk lamp) rather than overhead lights. </li>
    <li>Set offices computers to hibernate (better than sleep) before 6 hours (automatic is timing set by IT). </li>
    <li>Use power saving settings to hibernate (better than sleep) for all printers, faxes, copiers, etc. </li>
    <li>Report deviant temperatures and issues with office infrastructure to work control.</li>
    <li> Lower the blinds in the summer to reduce heat gain.</li>
    <li> Share equipment such as printers, fridges, coffeemakers, etc. </li>
    <li>When purchasing equipment or appliances, look for products with the Energy Star label. </li>
    <li>Update procedures to limit the need for printing. </li>
    <li>Set computers to automatically print double sided. </li>
    <li>Unsubscribe to advertising and other junk mailings. </li>
    <li>Post stickers in break rooms and rest rooms with reminders to energy &amp; resource.</li>
    <li> Keep a supply of reusable bottles &amp; mugs at the office for personal use and in break rooms. </li>
    <li>Use reusable rather than disposable containers, silverware, and plateware for lunch and events.</li>
    <li>Donate furniture, electronics and other large items that can be re-purposed. </li>
    <li>Provide dishwashing soap and a sponge at the sink and dish towel or drying rack to encourage reuse. </li>
    <li>Recycle bins for paper, glass/plastic/cans placed side by side with standard UMBC labels. </li>
    <li>Strategically place recycle bins in the copy room, break room.</li>
    <li> Keep desktop paper recycling containers at work stations instead of or alongside trash cans. </li>
    <li>Devise a collection system for e-cycling, ink cartridges, batteries, etc. </li>
    <li>Keep a water filter pitcher and reusable bottles in office, instead of bottled water. </li>
    <li>Post the transportation alternatives (zip car, transit, biking) flier on a communal bulletin board.  </li>
  </ol>
