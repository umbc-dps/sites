 <h4>Learning Outcomes</h4>
 <p>By the end of this program, you will  be able to: </p>
 <ul type="disc">
   <li>Apply the fundamental principles of systems  engineering to manage the development of products.</li>
   <li>Develop functional, physical, and operational  architectures of a system or enterprise that meets user requirements.</li>
   <li>Construct design specifications from user and  system requirements.</li>
   <li>Formulate modeling, simulation, and analysis  techniques to solve systems engineering problems.</li>
   <li>Develop and lead the system integration process.</li>
   <li>Develop and lead the system verification  process in an operational environment.</li>
   <li>Apply effective leadership skills for  multidisciplinary system engineering teams.</li>
   <li>Discuss in depth the origins, nature, and  current issues in cybersecurity and its related activities using precise  terminology.</li>
   <li>Understand cybersecurity concepts to mitigate  risks in cybersecurity operations.</li>
   <li>Develop and apply decision frameworks using  the guidelines of cybersecurity ethics, law, and strategy.</li>
   <li>Compare and contrast international  cybersecurity policies, strategies, and capabilities.</li>
   <li>Identify the missions, authorities, and  responsibilities of key cybersecurity organizations within the international  community.</li>
 </UL>
 
 <h4>Curriculum</h4>
<p>The Master’s of Science in Systems Engineering program with a certificate in cybersecurity is a non-thesis program. Students must earn 30 credit hours, including ENEE 670: Systems Engineering Project. In the project course, students complete an industry-based SE project and write a related technical report.</p>

<p>The curriculum consists of five core SE courses that equip students with the processes, techniques, and tools required to practice systems engineering, four required courses in cybersecurity and one elective course that can be chosen from many technical or management electives.</p>

<h4>Six Required Systems Engineering Courses</h4>
<p>These six core systems engineering courses  can be applied to a Post-Baccalaureate Certificate in SE or the first half of a M.S. in  Systems Engineering, Electrical Engineering, Computer Science, or Engineering  Management. <strong>ENEE 672 is a required course for students entering in the Fall of 2015 and afterwards.</strong></p>
<ul type="disc">
  <li>ENEE 660: Systems Engineering       Principles [3]</li>
