 <h3>MS in Systems Engineering with Certificate in Cybersecurity</h3>
<p>The Systems Engineering (SE) Program  at UMBC is designed to accelerate the development of systems engineers by  providing practical experience that can be immediately applied on the job.  Students learn from industry experts how to develop operable systems that meet  customer requirements, while successfully navigating the complexities of system  design.  The M.S. in SE program provides  the broad background required for successful systems engineers in the 21st  century.</p>
 <p>Courses are developed and taught by  senior systems engineers and address the entire systems engineering life cycle,  including requirements analysis, systems architecture and design, modeling,  simulation and analysis, and system implementation and test. Classes are held  at convenient late afternoon or early evening times to meet the needs of  working professionals.</p>
 <p>The  Post-Baccalaureate Certificate in Professional Studies: Cybersecurity Strategy &amp;  Policy provides students the essential domain knowledge required to serve in  decision-making roles throughout the cybersecurity industry. Students will  learn how to analyze cybersecurity risks, develop appropriate protection &amp;  response options, and assess operational requirements for government, military,  critical infrastructure, and commercial missions. Upon completing the certificate,  students will possess advanced knowledge of the strategy, policy, and analytic  aspects of cybersecurity. </p>
