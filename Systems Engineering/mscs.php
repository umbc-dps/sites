 <h3>MS in Computer Science: Systems Engineering Track</h3>
 <p>The UMBC Masters of Science in   Computer Science program with a Systems Engineering track (<STRONG>CMSC-SE</STRONG>) contains the following courses: </p>
<UL>
<LI>Computer Science (5 core courses)</LI>
<LI>Systems Engineering fundamentals and practice (5 courses)              
   </LI>
<LI>Technical elective (1 course) </LI>
 </UL>
 
 <p>The   curriculum is designed to provide a depth of knowledge in computer science and to help prepare engineers and IT professionals in industry to design systems using a methodical systems engineering process. This includes the knowledge of   how to network subsystems to achieve a desired total system performance and how   to manage the process of incorporating systems into larger systems to achieve   interoperability. </p>
 
 <p>The   Systems Engineering track is designed to give the systems engineer or computer   scientist the skills and knowledge they need to successfully guide a system's development. The subjects covered include: </p>
 
<UL>
	<LI>Computer Architecture</LI>
<LI>Computer Operating Systems</LI>
<LI>Design and Analysis of Algorithms</LI>
<LI>Database Systems</LI>
<LI>Computer Networks</LI>
<LI>Systems Engineering Principles</LI>
<LI>System Architecture and Design</LI>
<LI>Functional Decomposition of a System                   
   </LI>
<LI>System Operational Effectiveness and Life Cycle Analysis                 
   </LI>
<LI>Modeling and Simulation</LI>
<LI>Decision and Risk Analysis</LI>
<LI>System Integration and Test</LI>
<LI>Management of Engineering Projects </LI>
 </UL>
 <p>Optional   areas of study include:</p>
 <UL>
 <LI>Advanced Software Engineering</LI>
<LI>Artificial Intelligence</LI>
<LI>Computer Network Architecture</LI>
<LI>Mobile Computing</LI>
<LI>Systems of Systems </LI>
 </UL>
 
 <h4>Curriculum </h4>
 <p>The   Master Science in Computer Science with a Systems Engineering track (MSCS-SE) is   a non-thesis program. Students must earn 33 credit hours, including CMSC/ENEE 698 (Systems Engineering Project). In the project course, students complete an   industry-based SE project and write a related technical report or scholarly   paper that must be approved by the department. Normally, two faculty members   must approve the Project course scholarly paper, but in order to encourage local   industry and government partnership, the scholarly paper can be approved by the student's advisor and an industry/government mentor approved by the department. </p>
 <p><STRONG>The program curriculum is comprised of: </STRONG></p>
 <h5>Five CMSC Requirements: </h5>
<UL>
<LI>CMSC 611: Advanced Computer Architecture                
   </LI>
<LI>CMSC 621: Advanced Operating Systems                
   </LI>
<LI>CMSC 641: Design and Analysis ofAlgorithms                
   </LI>
<LI>CMSC 661: Principles of Database Systems                
   </LI>
<LI>CMSC 681: Advanced Computer Networks </LI>
 </UL>
 
 <h5>Five Systems Engineering Requirements**: </h5>
<UL>
<LI> <STRONG></STRONG><STRONG>Either </STRONG>CMSC 615: Introduction to Systems   Engineering and System Architecting <STRONG>or</STRONG>&nbsp;ENEE 660: Systems   Engineering Principles                 
   </LI>
<LI>ENEE 661: System Architecture and Design                
   </LI>
<LI> <STRONG>Either</STRONG> ENEE 662: System Modeling, Simulation and Analysis <STRONG>or </STRONG>CMSC 625: Modeling and Simulation of Computer Systems                
   </LI>
<LI>CMSC 618/ENEE 663: System Implementation, Integration and Test                
   </LI>
<LI>CMSC/ENEE 698: Systems Engineering Project </LI>
 </UL>
 <p>**All SE courses can be applied to the Certificate in SE </p>
 
 <h5>One elective, (the following are   suggestions): </h5>
