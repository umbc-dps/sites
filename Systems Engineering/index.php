<h3>UMBC Systems Engineering Graduate Programs</h3>

      <p>The Systems Engineering Program at UMBC offers a Master of Science in Systems Engineering, a Master of Science in Systems Engineering with a Certificate in Cybersecurity, and a Certificate in Systems Engineering. Our graduate programs are designed for working engineers.</p>
      <p>UMBC's program is designed to accelerate the development of systems engineers by providing practical, real-world experience that can be immediately applied on the job. Students learn from industry experts how to develop operable systems that meet customer requirements, while successfully navigating the complexities of system design.</p>
    <p>UMBC's rich curriculum covers all aspects of a system's life cycle using state-of-the-art principles, practices and technologies. This program, designed in collaboration with some of the leading employers in this field, balances practical application and theoretical understanding.</p>

      <h3>Why Systems Engineering?</h3>
      <ul><li>The flexibility of a Systems Engineer is always in high demand, as organizations need experts who can easily adjust from one environment to another.</li><li>Organizations seek experts who can integrate all the aspects of the engineering process into a coherent and effective system. </li><li>System engineering professionals play the critical role of acting as the primary liaison between management, customers, suppliers, and specialty engineers in the systems development process.</li></ul>

      <h3>Why UMBC?</h3>

      <ul><li>Systems Engineering Program at UMBC is taught by the region's top systems engineers from leading defense contractors and overseen by a board of faculty and industry leaders.</li><li>UMBC is classified by the Carnegie Foundation as a Research University (High Research Activity). </li><li>For six years running (2009-2014), UMBC was ranked #1 in the U.S. News and World Report's list of &quot;national up-and-coming&quot; universities.</li><li>Classes are conveniently offered in the evening on UMBC's main campus, located just five minutes from BWI Airport, with easy access to I-95 and the 695 Beltway.</li></ul>

    


