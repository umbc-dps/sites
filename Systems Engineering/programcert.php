 <h3>Post-Baccalaureate Certificate in Systems Engineering</h3>
  <p>The  Systems Engineering (SE) post-baccalaureate certificate program at UMBC is designed to  accelerate the development of systems engineers by providing practical  experience that can be immediately applied on the job. Students learn from  industry experts how to develop operable systems that meet customer  requirements, while successfully navigating the complexities of system design.</p>
  
 <p>Courses  are developed and taught by senior systems engineers and address the entire  systems engineering life cycle, including requirements analysis, systems  architecture and design, modeling, simulation and analysis, and system  implementation and test. Classes are held at convenient late afternoon or early  evening times to meet the needs of working professionals.</p>
 
 <p>The  post-baccalaureate certificate in Systems Engineering is designed to allow the student to  rapidly acquire the fundamental skills in Systems Engineering. The certificate program can be completed in  three semesters (approximately 10 months of classes) and has a simplified <a href="certificate.php">admissions  process</a>.Â Credits from the certificate program can be transferred to several  Masters of Science programs: MS of Systems Engineering, MS of Electrical Engineering, MS of Computer Science, or MS of Engineering Management. </p>
 
 <p>Beginning in Fall 2017, the Systems Engineering certificate requires four (4) courses. </p> <p> Four (4) required courses (12 credits). </p>
 <h5>OPTION A: </h5>
 <ul>
   <li>ENEE 660: Systems Engineering Principles  </li>
   <li>ENEE 661: System Architecture and Design</li>
   <li>ENEE 663: System Implementation, Integration, and Test</li>
   <li>ENEE 662: System Modeling, Simulation, and Analysis (*) (**)</li>
 </ul> 
 
 <h5>OPTION B: </h5>
 <ul>
   <li>ENEE 660: Systems Engineering Principles</li>
   <li>ENEE 661: System Architecture and Design</li>
   <li>ENEE 663: System Implementation, Integration, and Test</li>
   <li>ENEE 672: Decision and Risk Analysis</li>
 </ul>
 <p>(*) ENEE 662 has a prerequisite of either passing ENEE 669 or testing out of the class. See the instructor for details. ENEE 669 is a one credit course.</p>
 <ul>
   <li><a href="courses.php#ENEE669">ENEE 669: Mathematics  and MATLAB Fundamentals for Engineers</a></li>
 </ul><p>(**) Students enrolled in the Masters program for Electrical Engineering or Computer Science must take ENEE 662.</p>
 
 
