<h3>Admissions: Post-Baccalaureate Certificate </h3>
<h4>Application Dates</h4> <p>The application deadline for Fall 2016 is <strong>August 1</strong><br>
Fall 2016 Classes Start: <strong>August 31, 2016</strong></p>
 <h4>Admissions Requirements </h4>
 <ul>
      <li>A bachelor’s degree in Engineering, Computer Science or Information Systems.</li>
      <li>Minimum undergraduate GPA of 3.0 on a 4.0 scale</li>
      <li>GREs and letters of       recommendation are <strong>not</strong>&nbsp;required       if you earned an undergraduate degree from an accredited U.S. university. </li></ul>

 
 <h4>Application Process</h4>
 <p>To apply, complete a UMBC Graduate Application for Post-Baccalaureate Certificate Programs and use program code <strong>CENS</strong>.</p>
   <p>Apply online:</p>
 <ul>
   <li style="list-style-type:decimal"><a href="https://www.applyweb.com/umbcg/" target="_blank">Apply online</a>
     <ul>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo; section</li>
       <li>Recommendations are optional. Please put N/A in these fields if you choose not to enter names of references.</li>
       <li>Complete residency information to be reviewed for in-state tuition rates</li>
     </ul>
   </li>
   <li style="list-style-type:decimal">Submit unofficial transcripts from all post-secondary institutions to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or email to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
   <li style="list-style-type:decimal">Submit resume to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a> or <a href="mailto:kedmonds@umbc.edu" target="_blank">kedmonds@umbc.edu</a><a href="mailto:gambino@umbc.edu"></a>.</li>
 </ul>
 <p><strong>The following information is required to process your application:</strong></p>
 <ul>
   <li>Completed application including residency form (determines in-state tuition eligibility)</li>
   <li>Statement of academic goals</li>
   <li>Transcript(s) from each college university attended</li>
   <li>Current resume: Submit as additional document during application process or email to <a href="mailto:awaters@umbc.edu" target="_blank">kedmonds@umbc.edu</a>) </li>
   <li>Non-refundable application fee ($50 online) </li>
 </ul>
 
 <h4>Mailing Instructions </h4>
 <p>All application documents should be mailed to: </p>
UMBC Graduate School, Certificate in Systems Engineering <br>
   1000 Hilltop Circle<br>
   Baltimore, MD 21250</p>

