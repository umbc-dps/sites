 <h3>Master of Science in Systems Engineering</h3>
 <p>The  Systems Engineering (SE) Program at UMBC is designed to accelerate the  development of systems engineers by providing practical experience that can be  immediately applied on the job. Students learn from industry experts how to  develop operable systems that meet customer requirements, while successfully  navigating the complexities of system design.</p>
 <p>Courses  are developed and taught by senior systems engineers and address the entire  systems engineering life cycle, including requirements analysis, systems  architecture and design, modeling, simulation and analysis, and system  implementation and test. Classes are held at convenient late afternoon or early  evening times to meet the needs of working professionals.</p>
 <p>The  M.S. in SE program provides the broad background required for successful  systems engineers in the 21st century.</p>
 
 <h4>Curriculum  Overview</h4>
 <p>The Master&rsquo;s of Science in Systems  Engineering program is a non-thesis program. Students must earn 30 credit hours  (approximately 10 courses). The program has a CAPSTONE project course: ENEE  670: Systems Engineering Project. In the project course, students complete an  industry-based SE project and write a related technical report.</p>
 <p>The  core curriculum consists of six System Engineering courses, shown below, that  equip students with the processes, techniques, and tools required to practice  systems engineering. The elective portion of the program is structured so that  students can tailor the courses to meet their specific needs.</p>
 <p><strong>By the end of this program, the  student will be able to: </strong></p>
 <ul>
   <li>Apply the fundamental principles of systems  engineering to manage the development of products.</li>
   <li>Develop functional, physical, and operational  architectures of a system or enterprise that meets user requirements.</li>
   <li>Construct design specifications from user and  system requirements.</li>
   <li>Formulate modeling, simulation, and analysis  techniques to solve systems engineering problems.</li>
   <li>Develop and lead the system integration  process.</li>
   <li>Develop and lead the system verification  process in an operational environment.</li>
   <li>Apply effective leadership skills for  multidisciplinary system engineering teams.</li>
 </ul>
 
