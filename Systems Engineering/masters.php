 <h3>Admissions: Master's Degree </h3>
 <h4>Application Dates</h4>
<p>The application deadline for Fall 2016 is <strong>August 1</strong><br>
Spring 2016 Classes Start: <strong>August 31, 2016</strong></p>

 <h4>Admissions Requirements:</h4>
  <UL>
   <LI>An undergraduate degree in Engineering or Information   Technology. </LI>
   <LI>Minimum undergraduate GPA of 3.0 on a 4.0 scale</LI>		        
   <LI>GREs and letters of   recommendation are <STRONG>not</STRONG> required if you earned an undergraduate   degree from an accredited U.S. university. </LI>
 </UL>
 
 <h4>Application Process:</h4>
 <p>Applications for the Master's in System Engineering program are accepted for the Fall and Spring semesters </p>
 <p>Students applying to the Masters in Systems Engineering and the Post  Baccalaureate Certificate in Cybersecurity program, must also submit the&nbsp;<a href="http://gradschool.umbc.edu/students/forms/" target="_blank">Application for Enrollment in a Post Baccalaureate Certificate</a>&nbsp;form and current resume to be considered for the Cybersecurity  Certificate program. Admission to the Systems Engineering program does not  guarantee admission to the Cybersecurity certificate program. For additional  information, contact Lisa Gambino,&nbsp;<a href="tel:410-455-3034" target="_blank">410-455-3034</a>&nbsp;or&nbsp;<a href="mailto:gambino@umbc.edu"   target="_blank">gambino@umbc.edu</a>.</p>


 <ul>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_blank">Apply online</a>
     <ul>
       <li>Select proposed program Systems Engineering  &gt;&gt;   Systems Engineering Management SYST</li>
       <li>Submit your statement of goals as an attachment in the &ldquo;Academic Goals and Research Interests&rdquo; section</li>
       <li>Complete residency information to be reviewed for in-state tuition rates</li>
     </ul>
   </li>
   <li style="list-style-type:decimal">Request official transcripts from all post-secondary institutions to be sent to UMBC Graduate School, 1000 Hilltop Circle, Baltimore, MD 21250 or emailed to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
   <li style="list-style-type:decimal">Submit resume to <a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a> or <a href="mailto:kedmonds@umbc.edu" target="_blank">kedmonds@umbc.edu</a><a href="mailto:gambino@umbc.edu"></a>.</li>
 </ul>
 
 
 
 <p>Your application should include the following   documents:<em> (Incomplete applications will not be reviewed.)</em></p>
 <ul>
   
   <LI>Completed application including residency form (The residency   form determines in-state tuition eligibility.)                   
   <LI>Statement of academic goals                   
   <LI>Official transcript(s) from each college &amp; university   attended                   
   <LI>Current resume: Submit as additional document during application process or email to <a href="mailto:awaters@umbc.edu" target="_blank">kedmonds@umbc.edu</a>)       
   <LI>Application fee ($50 online / $70 paper) </LI>
 </ul>
 <h4>Mailing Instructions </h4>
 <p><strong>All application documents should be mailed to:</strong> <br>
UMBC Graduate School, MS Systems Engineering <br>
   1000 Hilltop Circle<br>
   Baltimore, MD 21250</p>
 <p>If you require further assistance, please contact <a href="mailto:kedmonds@umbc.edu">Kim Y. Edmonds</a> or 410-455-3445.</p>

