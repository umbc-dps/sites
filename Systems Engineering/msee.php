 <h3>MS in Electrical Engineering: Systems Engineering Track</h3>
<p>The UMBC Masters of Science in Electrical Engineering   program with a Systems Engineering track   (<STRONG>EENG-SE</STRONG>) consists of   the following courses: </p>
<UL>
   <LI>Electrical Engineering (4 core courses)   
</LI>   <LI>Systems Engineering Fundamentals and Practice (5   courses)   
</LI>   <LI>Electives from Electrical Engineering, Computer   Engineering, Computer Science, Mechanical Engineering, Physics, or Mathematics (2   courses) </LI>
 </UL>
 <p>The MSEE-SE curriculum is designed to provide a depth of   knowledge in electrical engineering and to help prepare engineers to design   systems using a methodical systems engineering process. This includes the   knowledge of how to network sub-systems to achieve a desired total system   performance and how to manage the process of incorporating systems into larger   systems to achieve interoperability.</p>
 <p>The Systems Engineering track is designed to give the   systems engineer the skills and knowledge they need to successfully guide a   system's development. Content areas include: </p>
 
<UL>
   <LI>Microelectronics and Photonics and/or Communications and   Signal Processing   
</LI>   <LI>Systems Engineering Principles   
</LI>   <LI>System Architecture and Design   
</LI>   <LI>Functional Decomposition of a System   
</LI>   <LI>System Operational Effectiveness and Life Cycle Analysis     
</LI>   <LI>Modeling and Simulation   
</LI>   <LI>Decision and Risk Analysis   
</LI>   <LI>System Integration and Test   
</LI>   <LI>Management of Engineering Projects </LI>
 </UL>
 
 <p><strong>Optional areas of study include: </strong></p>
<UL>
   <LI>Systems of Systems   
</LI>   <LI>Computer Engineering   
</LI>   <LI>Computer Science   
</LI>   <LI>Information Systems   
</LI>   <LI>Mechanical Engineering   
</LI>   <LI>Civil Engineering   
</LI>   <LI>Physics   
</LI>   <LI>Mathematics </LI>
 </UL>
 <h4>Curriculum </h4>
 <p>The Master&rsquo;s of Science in Electrical Engineering program   with a Systems Engineering track (MSEE-SE) is a non-thesis program. Students   must earn 33 credit hours, including ENEE 698 (Systems Engineering Project). In   the project course, students complete an industry-based SE project and write a   related technical report or scholarly paper that requires approval by the CSEE   department. Normally, two faculty members must approve the ENEE 698 scholarly   paper, but in order to encourage local industry and government partnership, the   project can be approved by the student's advisor and an industry/government   mentor approved by the department. </p>
 
 <p><STRONG>The program curriculum is comprised of four core Electrical   Engineering courses in one of the following two specifications: </STRONG></p>
 
 <h5>Microelectronics and Photonics area </h5>
<UL>
   <LI>ENEE 630: Solid State Electronics [3]   
</LI>   <LI>ENEE 631: Semiconductor Devices [3]   
</LI>   <LI>ENEE 680: Electromagnetic Theory I [3]   
</LI>   <LI>ENEE 683: Lasers [3] </LI>
 </UL>
 
 <h5>Communications and Signal Processing   area</h5>
<UL>
   <LI>ENEE 601: Signal and Linear Systems Theory   [3]   
</LI>   <LI>ENEE 610: Digital Signal Processing [3]   
</LI>   <LI>ENEE 620: Probability and Random Processes   [3]   
</LI>   <LI>ENEE 621: Detection and Estimation Theory   [3] </LI>
 </UL>
 
 <h5>Five three-credit Systems Engineering (SE)   courses: </h5>
<UL>
   <LI>ENEE 660: Systems Engineering Principles   [3]   
</LI>   <LI>ENEE 661: System Architecture and Design   [3]   
</LI>   <LI>ENEE 662: System Modeling, Simulation, and Analysis   [3]   
</LI>   <LI>ENEE 663/CMSC 618: System Implementation, Integration,   and Test [3]   
</LI>   <LI>ENEE/CMSC 698: Systems Engineering Project   [3] </LI>
 </UL>
 
 <h5>Elective   Courses</h5>
 <p>Two   three-credit graduate level technical electives (advisor approved) from   Electrical Engineering, Systems Engineering (such as ENEE 664/CMSC 691 Special   topics in Systems Engineering), Computer Engineering, Computer Science,   Information Systems, Mechanical Engineering, Civil Engineering, Physics, or   Mathematics. </p>
 
 <p>Students  who are interested in completing the Master of Science in Electrical  Engineering, or the Master of Science in Computer Science with a Systems  Engineering Track should visit the CSEE department website for admission  information: <a href="http://www.csee.umbc.edu/programs/graduate/" target="_blank">http://www.csee.umbc.edu/programs/graduate/</a></p>


