<div class="accordionheader">
            <h4><a href="#ENEE601" id="ENEE601"></a>ENEE 601: Signal and Linear Systems Theory [3] <i class="fa fa-angle-down"></i></h4>
          </div>

          <div class="accordionbody">
            <p>Fundamentals of Signals and Systems, Mathematical Theory of Continuous and Discrete Systems, Linear Time Invariant Systems, Linear Time Varying Systems, State Space Model and Approaches, Stability, Controllability and Observability, Minimal Realizations. <strong>Co-requisite:</strong> ENEE 620. </p>
</div>

<div class="accordionheader">
            <h4><a href="#ENEE610" id="ENEE610"></a>ENEE 610: Digital Signal Processing [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This is a first year graduate course for communication and signal processing majors in electrical engineering (EE) that covers the fundamentals of digital signal processing (DSP). The goal of this course is to provide the first year EE graduate student with the foundations and tools to understand, design, and implement DSP systems, in both hardware and software. MATLAB and SystemView will be the primary vehicles to provide the student with hands-on DSP design and simulation experience. The 
   student will also acquire an understanding of DSP hardware basics and architecture. Topics covered include: (1) A/D-D/A conversion and quantization, number Revised 10/11/2004 48 representations, and finite wordlength effects; (2) FIR, IIR, and lattice filter structures, block diagram and equivalent structures; (3) Multirate DSP and filterbanks; (4) Digital filter design methods and verification; (5) DSP hardware architecture; and (6) DSP simulation/laboratory experiences. <strong>Prerequisites:</strong> ENEE 601, 620, or their equivalent, or permission of instructor. </p>
</div>

<div class="accordionheader">
            <h4><a href="#ENEE620" id="ENEE620"></a>ENEE 620: Probability and Random Processes [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>Fundamentals of probability theory and random processes for electrical engineering applications and research: set and measure theory and probability spaces; discrete and continuous random variables and random vectors; probability density and distribution functions, and probability measures; expectation, moments, and characteristic functions;conditional expectation and conditional random variables, limit theorems and convergence concepts; random processes (stationary/non-stationary, ergodic, point processes, Gaussian, Markov, and second-order); applications to communications and signal processing. <strong>Prerequisite:</strong> Undergraduate probability or consent of instructor. </p>
</div>

<div class="accordionheader">
            <h4><a href="#ENEE621" id="ENEE621"></a>ENEE 621: Detection and Estimation Theory I [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>Fundamentals of detection and estimation theory for statistical signal processing applications: theory of hypothesis testing (binary, multiple, and composite hypotheses, and Bayesian, Neyman Pearson, and minimax approaches); theory of signal detection (discrete Revised 10/11/2004 49 and continuous time signals; deterministic and random signals; white Gaussian noise, general independent noise, and special classes of dependent noise, e.g., colored Gaussian noise; signal design and representations); theory of signal parameter estimation: Minimum variance unbiased (MVU) estimation, Cramer-Rao lower bound, general MVU estimation, linear models, maximum likelihood estimation, least squares, general Bayesian estimators (minimum mean square error and maximum a posteriori estimators), linear Bayesian estimators (Wiener filters), and Kalman filters. <strong>Prerequisite:</strong> ENEE 620 or consent of instructor. </p>
</div>

<div class="accordionheader">
            <h4><a href="#ENEE630" id="ENEE630"></a>ENEE 630: Solid-state Electronics [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>Fundamentals of solid-state physics for the microelectronics field: review of quantum mechanics and statistical mechanics, crystal lattices, reciprocal lattices, dynamics of lattices, classical concepts of electron transport, band theory of electrons, semiconductors, and excess carriers in semiconductors. <strong>Prerequisite:</strong> Consent of instructor.</p>
</div>

<div class="accordionheader">
            <h4><a href="#ENEE631" id="ENEE631"></a>ENEE 631: Semiconductor Devices [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>Principles of semiconductor device operation: review of semiconductor physics, p-n junction diodes, bipolar transistors, metal semiconductor contacts, JFETs and MESFETs, and MIS and MOSFET structures. <strong>Prerequisite:</strong> ENEE 630, or consent of instructor. </p>
</div>

<div class="accordionheader">
            <h4><a href="#ENEE680" id="ENEE680"></a>ENEE 680: Electromagnetic Theory I [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>Fundamentals of dynamics in electromagnetic theory: theoretical analysis of Maxwell's equations, Electrodynamics, plane waves, waveguides, dispersion, radiating systems, and diffraction. <strong>Prerequisite: </strong>Consent of instructor. </p>
</div>

<div class="accordionheader">
            <h4><a href="#ENEE683" id="ENEE683"></a>ENEE 683: Lasers [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>Introduction to basic theory of lasers: Introduction to quantum mechanics and time dependent perturbation theory, interaction of radiation and matter, stimulated and spontaneous emissions, rate equations, laser amplification and oscillation, noise in lasers and laser amplifiers, semiconductor lasers. <strong>Prerequisites:</strong> ENEE 680, or consent of instructor. </p>
</div>



<div class="accordionheader">
            <h4><a href="#ENEE698" id="ENEE698"></a>ENEE 698: Research Project in Electrical Engineering [1-3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>Individual project on topic in electrical engineering. The project will result in a scholarly paper, which must be approved by the student's advisor and read by another faculty member. Required of non-thesis option M.S. students. NOTE: May be taken for repeated credit up to a maximum of three credits. <strong>Prerequisite: </strong>Completion of core courses, or consent of instructor.<br>
 </p>
</div>

<div class="accordionheader">
            <h4><a href="#ENEE6982" id="ENEE6982"></a>ENEE 698: Research Project in Electrical Engineering (Systems Engineering Project) [1-3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This is an individual industry-based Systems Engineering project. The project will result in a technical-report/scholarly paper, which must be approved by the student's advisor and an industry/government mentor approved by the department. <strong>Prerequisite: </strong>Completion of core courses, or consent of instructor. </p>
</div>


