          </div>


          <div class="accordionbody">
            <p>This  course provides an overview of network communications concepts, architectures, waveforms,  protocols, and technologies. Upon completion of the course, students will be  able to construct, and assess the completeness of architectures for simple LAN  and WAN communications networks. Topics include wire/fiber and wireless WANs  and LANs, the OSI and TCP/IP models, propagation media, analog and digital data  and signals, error detection, error correction, data link layer protocols,  multiple access techniques, medium access control, circuit and packet  switching, Ethernet, switches, routers, routing techniques, congestion control,  and quality of service (QoS) metrics. <strong>Prerequisites</strong>:  Calculus, linear systems theory, and basic computer/network architectures</p>
</div>

