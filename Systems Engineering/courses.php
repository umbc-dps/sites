          <h3>Course Descriptions</h3>


          <p><strong>Note:</strong> Not all courses are offered every semester, and new courses may be added at any time. Check the <a href="schedule">schedule of classes</a>, for the latest offerings. For most up-to-date course descriptions please consult the online course <a href="http://catalog.umbc.edu/content.php?catoid=4&navoid=163 " target="_blank">catalog</a>.</p>

