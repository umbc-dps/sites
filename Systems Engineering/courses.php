<div class="accordionheader">
            <h4><a href="#ENEE660" id="ENEE660"></a>ENEE 660: Systems Engineering Principles [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>The  Systems Engineering Principles course provides an introduction to the  discipline of Systems Engineering and Systems Architecting. Key industry  standards for the &ldquo;The Systems Engineering Process&rdquo; are taught and used  throughout the course. The course describes how the SE process is implemented  in standard life cycle models and through various standard organizational  structures. This course introduces Model-Based Systems Engineering concepts, <em>e.g</em>., DoDAF, UML, and SysML. System  engineering technical process topics range from Requirements Definition through  system Verification and Validation. System engineering management process  topics include: Decision Analysis, Technical Planning, Risk Management, and  Interface Management. The course also covers aspects of project management: Integrated  Product Teams, technical performance measurement, earned value measurement, and  work breakdown structures. Students will develop a requirements document, and an  integrated architecture, and a System Engineering Plan (SEP). Homework and  Exams are designed to provide the opportunity to practice the concepts learned  in class. </p>
 
</div>


<div class="accordionheader">
            <h4><a href="#ENEE661" id="ENEE661"></a>ENEE 661: System Architecture and Design [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>The  System Architecture and Design course focuses on the role of the systems  architect in the system development life cycle. In the operational analysis  phase, the emphasis is on understanding the context of the system within the  larger customer problem area, and the identification of requirements that  influence system partitioning. In the functional analysis phase, the  emphasis is on the dependencies between processing steps. In the architectural  design phase, the emphasis is on partitioning the system into generic  components, and ultimately instantiating them into physical components.  A  precision landing system is used throughout the course as a common case study.  Within the classroom sessions, a search and rescue system is used. Three  presentations by each group are given to simulate: (1) RFI review, (2) SRR, and  (3) SDR. These reviews progressively reveal each group&rsquo;s proposed solution to  the precision landing system for a mythical country with unique complicating  characteristics. <strong>Prerequisite</strong>:  ENEE 660. ENEE 660 may be taken concurrently with instructor permission.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENEE662" id="ENEE662"></a>ENEE 662: Modeling, Simulation, and Analysis [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>The  Modeling, Simulation, and Analysis  (MS&amp;A) course covers the use of modeling,  simulation, and analysis in the development and test of systems. The course  covers leading MS&amp;A activities, architecting simulations, and making  decisions based on statistical analysis of the simulation results. The  techniques discussed in class are motivated through the use of examples. Typical modeling problems discussed include performance,  cost, reliability, and maintainability modeling.  Students will develop simple models and  simulations using MATLAB and complete several course projects. <strong>Prerequisites</strong>:  ENEE 660, ENEE 669. The ENEE 669 class requirement may be waived by passing the  Mathematics and MATLAB Fundamentals Proficiency Exam.  See the instructor for details.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENEE663" id="ENEE663"></a>ENEE 663: System Implementation, Integration, and Test [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>The  System Implementation, Integration, and Test course is a follow-on to  ENEE 661. The course covers the translation of design specifications into  product elements, the integration of these elements into a system, and the  verification that the resulting system performs as intended in its operational  environment.  The course follows the product development life cycle beyond  system architecture and design. The system is decomposed into component level  elements suitable for software coding and hardware fabrication. These elements  are then individually tested and gradually integrated together as the various  modules and sub-systems are subjected to unit test, verification and  validation. Eventually the full system goes through Operational Test and  Evaluation, and finally makes it into production and operation. This course  covers the System Engineer role, activities and processes that are needed  during this phase of the product development cycle. Areas of study will include  technical planning, requirement &amp; interface management, standards,  technical performance measures, technical evaluation, technical readiness,  implementation, integration, verification, validation, production, transition  to operation and complexity. <strong>Prerequisites</strong>:  ENEE 660 and ENEE 661 or consent of instructor.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENEE664" id="ENEE662"></a>ENEE 664: Advanced Systems Architecture [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course emphasizes the many partitioning  alternatives that can be employed when developing physical systems  architectures, including hierarchical partitioning, federated partitioning,  scalable architectures, high availability architectures, and collaborative  systems. The course also deals with methods for architecting successful  systems, such as achieving data integrity, managing system workflow, and  constructing representation models.
            <strong>Prerequisites:</strong> ENEE 660 and ENEE 661.</p>
</div>


<div class="accordionheader">
            <h4><a href="#ENEE666" id="ENEE"></a>ENEE 666: Architecting Security [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This  timely course focuses on incorporating security in the initial phases of  architecting Systems and Services. The goal is to teach our systems engineers  and architects how to develop systems and system of systems (SoS) that have  native security as a foundational component that is dynamic and scales to  mission need. Consideration of data-at-rest, data-in-transit and processed data  is a critical architectural facet. Incorporating data tagging and digital  policy-based routing to facilitate secure data flow is an important aspect of  securing data. The course assumes that the students have initial knowledge of  systems engineering and architecture and are familiar with needs and basic  methods of securing systems. The course will incorporate student participation  researching risks and mitigation approaches in current and recent past systems  that have been deployed and their impact on stakeholders. Lessons learned are  highlighted and practical methods considered in order to allow direct  application. <strong>Prerequisites:</strong> ENEE 660 </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENEE667" id="ENEE2"></a>ENEE 667: Advanced Systems Engineering Processes <i class="fa fa-angle-down"></i>[2]</h4>
          </div>


          <div class="accordionbody">
            <p>This  two-credit course provides students the opportunity to deepen their  understanding of the systems engineering processes introduced in ENEE 660.  Specifically, this course provides an in-depth study of the systems engineering  processes outlined in the International Standard for Systems and Software  Engineering (ISO/IEC 15288:2008), the International Council on Systems  Engineering (INCOSE) Handbook, and the INCOSE Systems Engineering Body of  Knowledge. This course will emphasize that Systems Engineering Technical  Processes operate within the envelope of the Project as dictated by Contracts  as set forth by an Organization. In the end, the student will have a good  understanding and appreciation of the process framework required to create  man-made systems. As a part of this course, students will select, research, and  report on systems engineering process areas of particular importance to them. <strong>Prerequisites:</strong> ENEE 660 </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG664" id="ENMG664"></a>ENMG 664: Quality Engineering &amp; Management <i class="fa fa-angle-down"></i>[3]</h4>
          </div>


          <div class="accordionbody">
            <p>This course provides an overview of the basic principles and tools of quality and their applications from an engineering perspective. The primary quality schools of thought or methodologies, including Total Quality Management, Six Sigma and Lean Six Sigma, and quality approaches from key figures in the development and application of quality as a business practice, including W. Edwards Deming and Joseph M. Juran will be analyzed. Some of the key mathematical tools used in quality systems will be discussed, including Pareto charts, measurement systems analysis, design of experiments, response surface methodology, and statistical process control. Students will apply these techniques to solve engineering problems using the R software. Reading assignments, homework, exams, and the project will emphasize quality approaches, techniques, and problem solving. </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENMG668" id="ENMG668"></a>ENMG 668: Project and Systems Engineering Management [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This course will cover fundamental project control and systems engineering management concepts, including how to plan, set up cost accounts, bid, staff and execute a project from a project control perspective. It provides an understanding of the critical relations and interconnections between project management and systems engineering management. It is designed to address how systems engineering management supports traditional program management activities to break down complex programs into manageable and assignable tasks. </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENEE669" id="ENEE669"></a>ENEE 669: Mathematics and MATLAB Fundamentals for Engineers [1] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This  course provides an introduction to programming in MATLAB and a review of fundamental  engineering mathematics, e.g., probability, calculus, linear algebra, ordinary  differential equations, difference equations, and some numerical methods). It  is designed to refresh students&rsquo; basic skills in these areas of mathematics and  to establish basic proficiency in MATLAB. Course work focuses on developing  MATLAB programs that use these mathematical techniques to solve simple problems  of systems engineering interest. <strong>Prerequisites: </strong> Knowledge of a programming language. </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENEE670" id="ENEE670"></a>ENEE 670: Systems Engineering Project [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>In  this course, the student performs in an industry-based work environment on a SE  project. The project spans the essential phases of the System Life Cycle and  results in the development of a simulation model of the objective system.  During the course of system development, engineering artifacts are created to  substantiate system development. A final summary technical report summarizing  the artifacts and simulation results are compiled in a form representative of a  professional report in partial satisfaction of course requirements. Starting six weeks before the beginning of  the semester, students form Integrated Product Teams, usually not exceeding 5  students per team. During the six weeks before the semester begins, the team  prepares a proposal for the project that is submitted to the instructor for  approval. The advisor may approve the  project proposal, subject to adjustment, as needed. To increase the realism of  the environment, an industry mentor may collaborate with the advisor during the  periodic milestone reviews of the project. <strong>Prerequisites</strong>:  ENEE 660, ENEE 661, ENEE 662 or ENEE 669, ENEE 663, or consent of instructor. </p>
</div>


<div class="accordionheader">
            <h4><a href="#ENEE672" id="ENEE672"></a>ENEE 672: Decision and Risk Analysis [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>This  course provides an overview of decision and risk analysis techniques. It  focuses on how to make rational decisions in the presence of uncertainty and  conflicting objectives. This course covers modeling uncertainty; rational decision-making principles; representing decision problems with value trees, decision trees, and influence diagrams; solving value hierarchies, decision trees, and influence diagrams; defining and calculating the value of information; incorporating risk attitudes into the analysis; and conducting sensitivity analysis. Students are expected to have an elementary understanding of probability theory.  <br>
   </p>
</div>

