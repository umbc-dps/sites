          </div>


          <div class="accordionbody">
            <p>Advanced topics in the area of database management systems: data models and their underlying mathematical foundations, database manipulation and query languages, functional dependencies, physical data organization and indexing methods, concurrency control, crash recovery, database security and distributed databases. <strong>Prerequisite:</strong> CMSC 461 or consent of instructor.</p>
</div>

<div class="accordionheader">
            <h4><a href="#CMSC671" id="CMSC671"></a>CMSC 671: Principles of Artificial Intelligence [3] <i class="fa fa-angle-down"></i></h4>
          </div>


          <div class="accordionbody">
            <p>A study of topics central to artificial intelligence, including logic for problem-solving, intelligent search techniques, knowledge representation, inference mechanisms, expert systems and AI programming. <strong>Prerequisite:</strong> CMSC 471 or consent of instructor.</p>
