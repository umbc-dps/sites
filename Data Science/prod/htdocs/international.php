 <h3>Admissions: International Students</h3>
  
 <h4> Minimum requirements:</h4>
 <ul><li>A bachelor's degree (in any subject) and 3.0 GPA</li>
   <li>Prospective students must have also completed the following courses at the undergraduate or graduate level:
     <ul>
       <li>One semester of statistics</li>
       <li>Calculus I or II (depending upon track)</li>
       <li>Candidates must have academic or professional experience equivalent to basic programming courses.</li>
       <li>Students who do not have formal undergraduate programming courses or on-the-job training are encouraged to use MOOCs or Khan Academy. </li>
     </ul>
   </li>
   <li>iBT Scores  Breakdown: Writing (23), Listening (23), Reading (25), Speaking (28, 23  acceptable if have 2 years&rsquo; work experience in supervisory or management  positions in the U.S.) OR IELTS Score: Minimum score of 7.5 required.</li>
 </ul>
 <p>*If you have a three-year degree versus a four-year degree, your transcript will be reviewed to determine whether you have met the necessary prerequisites for the program. Eligibility for admission can only be determined upon receipt of a complete application.</p>
 
 
 <h4>Application Process</h4>
 <p>All international applicants are encouraged to apply as early as possible to allow sufficient time to receive all the necessary documents and to process and assess the application.</p>
 <p>International applications are accepted for the Fall and Spring semester. We encourage all international applicants to send in their documents at least six months prior to the start of the semester applying to.</p>

 <ul>
   <li style="list-style-type:decimal"><a href="http://gradschool.umbc.edu/admissions/apply" target="_blank">Apply online</a>
     </li>
   <ul>
     <li>Submit  your statement of goals as an attachment in the &ldquo;Academic Goals and Research  Interests&rdquo; section</li>
     <li>Insert  names of references into &ldquo;References&rdquo; section (Note: we only require two. Put  N/A in third line)</li>
     <li>Submit  resume as an attachment </li>
     <li>Complete  residency information to be reviewed for in-state tuition rates</li>
     </ul>
   <li style="list-style-type:decimal">Request official  transcripts from all post-secondary institutions to be sent to UMBC Graduate  School, 1000 Hilltop Circle, Baltimore, MD 21250 or emailed to&nbsp;<a href="mailto:gradschool@umbc.edu">gradschool@umbc.edu</a>.</li>
   <li style="list-style-type:decimal">Request  for test scores to be sent by ETS to UMBC (code 5835)</li>
 </ul>
 <p><strong>All documents should be mailed to:</strong></p>
 <p>UMBC Graduate School, MPS: Data Science<br>
    1000 Hilltop Circle<br>
    Baltimore, MD 21250 U.S.A.</p>

 <p><strong>Your application packet should include the following documents:</strong></p>
 <ul>
   <li>Completed  application including residency form (Residency form determines tuition  classification)</li>
   <li>Two  letters of recommendation (if you choose to forego the online option, please  see the paper form <a href="http://gradschool.umbc.edu/students/forms/">here</a></li>
   <li>Personal  statement outlining the student&rsquo;s goals and  expectations of the program. Statement should include applicant&rsquo;s background,  qualifications, career experience, and any additional pertinent information. </li>
   <li><a href="documents/resume.docx" target="new">Industry formatted resume</a> (When applying online, upload your resume to the graduate school application.) </li>
   <li>Official  transcript(s) from each college &amp; university attended</li>
   <li>TOEFL or  IELTS scores &amp; GRE Scores</li>
   <ul>
     <li><em>Please  note</em>: All international students are required to submit Graduate  Record and TOEFL scores unless you attended a university in an English-speaking  country or hold an undergraduate degree from a U.S. institution.</li>
   </ul>
   <li>Application  fee ($50 online)</li>
   <ul>
     <li>If paying  by check, the check must be drawn on a bank with a U.S. location. The check  should be payable to UMBC. </li>
   </ul>
 </ul>
 <h4>Obtaining Immigration Documents</h4>
 <p>If you intend to apply for an F-1 visa and require an I-20 from UMBC, please include the following additional documents with your application.</p>
 <ul>
   <li>Copy of valid passport</li>
   <li>Certified bank statement or bank verification letter from financial sponsor showing a minimum of $29,000 (USD) in available funds.</li>
   <li>Letter from financial sponsor stating they will be responsible for all living expenses and school-related expenses for the duration of your stay at UMBC.</li>
 </ul>
 <p>Send these documents after receipt admission acceptance.  Note: I-20 is processed after formal acceptance into the program.</p>
 <p>All immigration related questions should be addressed to International Education Services by phone at (410) 455-2624 or by e-mail, <a href="mailto:ies@umbc.edu">ies@umbc.edu</a>.</p>
 
 <h4>Financial Aid/Tuition/Assistantships</h4>
 <p>Presently, the program is not offering any financial aid or graduate assistantships.</p>



