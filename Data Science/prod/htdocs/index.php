    <h3>UMBC Data Science Graduate Programs</h3>


       <p>The Data Science Master’s program at UMBC prepares students from a wide range of disciplinary backgrounds for careers in data science. In the core courses, students will gain a thorough understanding of data science through classes that highlight: Machine learning, Data analysis, Data management, Ethical and legal considerations, and more. </p>

<p>Students will develop an in-depth understanding of the basic computing principles behind data science, to include but not limited to data ingestion, curation and cleaning and the 4Vs of data science: Volume, Variety, Velocity, Veracity, as well as the implicit 5th V -- Value. Through applying principles of data science to the analysis of problems within specific domains expressed through the program pathways, students will gain practical, real world industry relevant experience. </p>

<p>The MPS in Data Science is an industry-recognized credential and the program prepares students with the technical and management skills that they need to succeed in the workplace.</p>

<h3>Why Data Science?</h3>
<ul>
<li>Organizations have a growing need for employees who are experts in the management and interpretation of big data</li>
<li>Our classes are taught by industry experts who combine their professional experience with theory to provide a rigorous classroom experience</li>
<li>Our small classes are taught with a mix of in-person and online instruction, providing students the best of an in-classroom experience while allowing for work-school life balance</li>
</ul>

<h3>Why UMBC?</h3>
<p>The Data Science graduate programs at UMBC was designed to respond to the growing regional and national demand for professionals with data science knowledge, skills, and abilities. Bringing together faculty from a wide range of fields who have a deep understanding of the real-world applications of data analytics, UMBC’s Data Science programs prepare students for the workplace through hands-on experience, rigorous academics, and access to a robust network of knowledgeable industry professionals. UMBC's graduate programs in Data Science offers a wide variety of benefits...</p>
<ul>
<li><strong>Exceptional faculty.</strong> The Data Science curriculum brings together UMBC&rsquo;s Departments of Computer Science &amp; Electrical Engineering, Information Systems, Mathematics and Statistics, and several departments from the social sciences to provide students with a rigorous and thorough base of knowledge. Faculty have particular strengths in addressing critical social questions through the application of data science.</li>
<li><strong>Rigorous research.</strong> UMBC is classified by the Carnegie Foundation as a Research University (High Research Activity).</li>
<li><strong>National recognition.</strong> For six years running (2009-2014), UMBC was ranked #1 in the U.S. News and World Report's list of "national up-and-coming" universities</li>
<li><strong>Convenient classes.</strong> Classes are conveniently offered in the evening on UMBC's main campus, located just five minutes from BWI Airport, with easy access to I-95 and the 695 Beltway</li>
</ul>


