<p>Once final grades have been posted, you can order an  official transcript.</p>
<h4>How to check final grades</h4>
<ol>
  <li><a href="importantdates.php">Verify</a> when grades will be posted.</li>
  <li><a href="http://my.umbc.edu/">Log-in</a> to <em>my</em>UMBC to check final grades.</li>
  <li>Highlight the Topics menu.</li>
  <li>Select &quot;Classes &amp; Grades.&quot;</li>
  <li>Select &quot;Student Grades.&quot;</li>
  <li>Select the desired &quot;Term/Semester.&quot;</li>
  <li>Click Continue, and your grades will display on the  screen.</li>
</ol>
<h4>How to order transcripts</h4>
<ul>
  <li>Order official UMBC transcripts through <a href="http://registrar.umbc.edu/services/records/">Parchment</a>,  UMBC&rsquo;s online transcript partner.</li>
  <li>View a video on <a class="video" href="https://www.youtube.com/watch?v=kvfB5X7Ej1E">How to Order Your Transcript</a>.</li>
  <li>There is a fee associated with ordering an official  transcript.</li>
  <li>You can also download and print an unofficial transcript  through <em>my</em>UMBC.&nbsp;</li>
</ul>

