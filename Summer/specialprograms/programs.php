<div class="accordionheader" class="health">
  <h4><a href="#health" id="health"></a>Health & Social Inequalities<i class="fa fa-angle-down"></i></h4>
</div>

 <div class="accordionbody" class="health">
 
 <p><img src="../images/poverty.jpg" style="width:50%; padding:5px" align="left">Social dimensions of health address the conditions in which  people are born, grow, live, work, and age beyond the biological and genetic  level. It is increasingly clear that social, psychological, economic, and  institutional inequalities act as powerful predictors of both health outcomes  and quality of life. <em>Health &amp; Social Inequalities </em>courses identify  how important social factors influence individual health behaviors, health  conditions, longevity and mortality, and provide a framework for thinking about  health &ldquo;outside the traditional box&rdquo; of a medical model.</p>

<p>These courses are recommended for students pursuing careers  in the health professions, or for anyone seeking to understand the dramatic  impact of social factors on individual and community health, wellness, and  quality of life. </p>

