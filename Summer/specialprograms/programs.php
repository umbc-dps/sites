<p>Social justice advocates eliminating inequalities among people by actively promoting equality, fairness and dignity for all human beings. This summer, explore a variety of timely courses that focus on the theme of <em>Culture & Social Justice</em>.
</p>

<p>Courses address relevant social issues, while promoting critical thinking and writing skills. They offer a view of social issues and practices from various perspectives, including the intersectional analysis of race, class, and gender. Courses in <em>Culture & Social Justice</em> help to understand and analyze structural inequalities while challenging us to envision and actively pursue a more just and better future. 
</p>


