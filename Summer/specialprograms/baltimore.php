                	<p>The music known as jazz has been celebrated throughout the world. MUSC 333 examines the music itself as well as the role that race has played throughout jazz history. Examine the music from a historical perspective through the study of the music and lives of its creators and practitioners. Additional content will explore the contributions of Baltimore jazz artists including Eubie Blake, Chick Webb, and Cab Calloway. You’ll learn to make aesthetic judgments, identify various jazz styles and discuss their relevance to their time and to the present. </p>
