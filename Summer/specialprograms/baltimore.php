<h3><strong>Lion Brothers Building</strong></h3>
<p>Some  classes will regularly meet downtown in The Lion Brothers Building (UMBC&rsquo;s  new city classroom <a href="http://www.lionbrothersbaltimore.com/neighborhood/" target="_blank">near Hollins Market</a>) where you can actively learn and discover  first-hand what makes Baltimore such a unique and special city. The  Lion Brothers Building is located at <strong>875 Poppleton Street and Hollins</strong>; across the street from the James  McHenry Elementary School and Recreation Center. The building is also close to  Café Gourmet, Zella&rsquo;s Pizzeria, Cup&rsquo;s Coffee House, and a branch of Harbor  Bank. Watch our <a href="https://www.youtube.com/watch?v=JRDAEcCz-z8" class="video">video with Dr. Kate Drabinski</a> to learn more about Discover Baltimore classes, the city & The Lion Brothers Building.</p>

<h4>Transportation</h4>
<p>Just two blocks from Martin Luther King Jr. Boulevard (MLK), with easy access to I-295, I-95, and I-83, the Lion Brothers Building is accessible from campus by <a href="http://www.umbc.edu/transit/routes/downtown.php" target="_blank">UMBC’s Downtown Shuttle</a>, local bus lines, as well as the <a href="http://www.charmcitycirculator.com/route/orange" target="_blank">Charm City Circulator</a>.   There is also street parking (free and metered) along the residential roads around the Lion Brothers Building. Students may also park on the UMBC campus (with valid parking permit) and ride the Downtown Shuttle.</p>
