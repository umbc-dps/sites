<p>Students admitted for fall 2017 are eligible to attend summer session 2017.</p>
<p>Summer session is a great time for incoming freshmen and transfer students to <em>grit going</em> at UMBC. Lighten your fall class load, take required courses, and get acclimated to the campus.  Smaller classes and dedicated faculty will give you a jump start on the busy fall semester and help keep you on track to graduate on time or early!</p>


<div class="accordionheader">
  <h4>How To Change Start Date From Fall To Summer<i class="fa fa-angle-down"></i></h4>
 </div>

 <div class="accordionbody">
<p>No additional application is necessary. <a href="http://www.umbc.edu/undergraduate/quicklinks/contact.html" target="_blank">Contact</a> the Office of Undergraduate Admissions to change your semester start date from fall to summer 2017.</p>
</div>


<div class="accordionheader">
  <h4>What To Do After You Change Your Start Date<i class="fa fa-angle-down"></i></h4>
 </div>

 <div class="accordionbody">
<p>If you have not done so already, <a href="https://webadmin.umbc.edu/admin/User/Create" target="_blank">create</a> your <em>my</em>UMBC account with your UMBC campus ID number and birthdate.  Use <em>my</em>UMBC to <a href="registrationtips.php"target="_blank">register</a> for courses. Advising is highly recommended, but not required.</p>
<p> Once registered, you are automatically enrolled in Blackboard, UMBC’s online learning management system.  Log-in to your <a href="https://blackboard.umbc.edu/webapps/login/" target="_blank">Blackboard</a> account early to check for updates as faculty often post course information/materials before the session begins.  Check your UMBC <a href="http://my.umbc.edu/" target="_blank">e-mail account</a> on a regular basis for important information prior to the start of classes.</p>
<p> Be aware of important <a href="importantdates.php" target="_blank">academic</a> and <a href="financialdates.php" target="_blank">financial</a> dates/deadlines, including schedule adjustment dates, when to pay your bill, and refund dates. Review the <a href="faq.php" target="_blank">FAQ/Services</a> page for important information concerning parking permits, textbooks, student ID cards, transcripts, bill payment, campus text alerts, etc.</p>
 </div>


<div class="accordionheader">
  <h4>Summer Bridge Program For New Freshmen<i class="fa fa-angle-down"></i></h4>
 </div>

 <div class="accordionbody">
<p>All incoming freshmen must complete a math and English course during their first year at UMBC.  UMBC’s six-week <a href="http://fye.umbc.edu/programs/csi/" target="_blank">Summer Bridge Program</a> offers required math and writing courses along with an interactive seminar that helps ease the transition from high school to college.  Outside of class, dedicated Summer Bridge staff coordinate tutoring and study groups, offer individual advising support, and organize special summer events for new students. Residential and commuter options are available.  The Summer Bridge Program is a great way to build a strong academic foundation at UMBC while earning 4 to 8 required credits. Students who successfully complete the program will also receive a scholarship equal to one credit of fall tuition.</p>
<p>The program begins Sunday, <strong>July 9</strong> (space is limited).</p>
<p>Contact Laila Shishineh at <a href="mailto:lailams@umbc.edu" target="_blank">lailams@umbc.edu</a> or (410) 455-3737 for more information.</p>
</div>


