           <p><strong>May 26, 27 &amp; 28</strong> - <strong>Summer STEM at UMBC Welcome Week</strong><br>
          <em>10:30 am - 1:30 p.m., University Center Plaza</em><br>
          The Summer STEM at UMBC welcome week provides an opportunity for students and faculty on campus to be aware of the initiative and activities and to build a sense of community among those who are involved in coursework, research and programmatic activities.
          <p><strong>June 1 &amp; June 2 - Study Group Workshops</strong> - <a href="rsvp-studygroup1">RSVP requested</a><br>
            <em>12:00 noon - 1:00 p.m., University Center Room 115</em><br>
            Coordinated study groups can help you stay on top of coursework during the fast-paced summer session. Join a study group and experience the value of cooperative learning. Learn how to work together with other motivated students to increase your understanding and improve your grades.  </p>
          <p>This 45-minute workshop prepares students to form or join study groups and provides information on academic support resources available in summer session. Students can attend one or both workshops during each session. </p>
          <p><strong>June 9 - Discover Your Options: Undergraduate Research Opportunities</strong> - <a href="rsvp-workshop1">RSVP requested</a><br>
          <em>12:00 noon - 1:00 p.m., University Center Room 115</em><br>
            Undergraduate research opportunities are akin to internships in other fields. There are ample opportunities for students to apply to Research Experiences for Undergraduates (REU). These opportunities have specific application procedures and time lines that many undergraduate students are unaware of until deadlines have passed. This workshop provides an overview of REU options, time lines for application and tips for success when applying to REUs.</p>
          <p><strong>June 10</strong> - <strong>Resumes and Internships</strong> - <a href="rsvp-workshop1">RSVP requested</a><br>
            <em>12:00 noon - 1:00 p.m., University Center Room 115</em><br>
            Employers say &quot;experience&quot; is the number one thing  they seek from job seekers, yet many college students feel that they don't have  it.&nbsp; Join us for a session with the UMBC Career Center to learn how to  develop a resume that highlights the accomplishments you do have and how to use  that document to obtain an internship.</p>
          <p><strong>June 17</strong> - <strong>From Classroom to Career</strong> - <a href="rsvp-workshop1">RSVP requested</a><br>
            <em>12:00 noon - 1:00 p.m., University Center Room 115</em><br>
  Research shows that networking brings between 80-97% of jobs.  In this workshop you will gain knowledge of invaluable career management techniques  by identifying key elements of managing a career vs. just seeking employment.  Learn how to make connections, uncover job openings and get referrals.    </p>
