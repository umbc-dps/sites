<div class="accordionheader">
  <h4>Visiting student requirements<i class="fa fa-angle-down"></i></h4>
</div>

 <div class="accordionbody">
<ul>
  <li>Must apply for admission to UMBC as a visiting student by May 25 (Session I) or July 6 (Session II).</li>
  <li>Must be admitted before registering for courses.  The last day to register without a late fee is May 30 (Session I) or July 10 (Session II).</li>
  <li>Late registration for classes with open seats is June 3 (Session I) or July 14 (Session II).</li>
  <li>Must be in good academic standing at your home  institution and provide a current transcript to enroll in UMBC courses with  prerequisite requirements. </li>
  <li><strong>NOTE:Â  Visiting students will initially be blocked  from registering in any UMBC course with prerequisite requirements. You must  first obtain  <a href="coursesprerequisites">a prerequisite override</a> before you can  enroll.</strong></li>
  <li>If you attend another University of Maryland System  (USM) school, please be aware that USM inter-institutional enrollment only  applies to fall and spring semesters. Therefore, students from other USM  schools who wish to enroll in summer or winter session at UMBC must apply as  visiting students.</li>
</ul>
</div>

<div class="accordionheader">
  <h4>Confirm your status before you apply<i class="fa fa-angle-down"></i></h4>
</div>

 <div class="accordionbody">
<ul>
  <li>If you plan to enroll at UMBC over multiple consecutive  terms (including fall and spring), but do not intend to pursue a full degree  program or major, apply as a <a href="http://www.umbc.edu/undergraduate/apply/nondegree.html">non-degree student</a>.</li>
  <li>If you previously attended UMBC and left in good  standing, apply for <a href="http://www.umbc.edu/undergraduate/apply/readmit.html">readmission</a>.</li>
  <li>If you previously attended UMBC and did not leave in  good standing, apply for <a href="http://www.umbc.edu/undergraduate/apply/reinstate.html">reinstatement</a>.</li>
  <li>If you are currently a high school student and want to  earn college credits apply for <a href="http://www.umbc.edu/undergraduate/apply/early_enroll.html">early admission</a>.</li>
  <li>If you are an <a href="incomingstudents">incoming</a> new student (admitted to begin full- or part-time at UMBC in the fall or  spring) you do<strong> not</strong> need to apply for  admission to enroll in the preceding summer or winter session. <a href="http://www.umbc.edu/undergraduate/quicklinks/contact.html">Contact</a> the Office of Undergraduate Admissions  and ask to change your semester start date to the summer or winter term.</li>
</ul>
</div>

