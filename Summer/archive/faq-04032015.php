<h4>What is <em>my</em>UMBC?</h4>

<ul>
  <li>A web-based portal to various services and online community resources at
  UMBC.</li>

  <li>Use <a href="http://my.umbc.edu/" target=
  "_blank"><em>my</em>UMBC</a> to register for courses, view account
  balances, online bill payment, register for a parking permit, check
  university e-mail and check grades.
  </li>
</ul>

<h4>How do I create a <em>my</em>UMBC account?</h4>

<ul>
  <li>Create your account with your date of birth and UMBC Campus ID (which you
  will receive from the Admissions Office).</li>

  <li>Visit the <em><a href="https://webadmin.umbc.edu/admin" target="_blank">
    my</a></em><a href="https://webadmin.umbc.edu/admin">UMBC WebAdmin
    page</a>.
  </li>

  <li>Click on <a href="https://webadmin.umbc.edu/admin//User/Create" target=
  "_blank">Create my Account</a>.
  </li>
</ul>

<h4>How can I get help if I need it?</h4>

<ul>
  <li>Submit an online Help Ticket through <em>my</em>UMBC.  Use myUMBC Help Tickets for billing issues, computing/technology support, questions about your class schedule, grades,
  etc.</li>

  <li>
    Access the
    <a href="https://my.umbc.edu/go/help" target="_blank"><em>my</em>UMBC Help Ticket site</a> and login with
    your <em>my</em>UMBC user name and password.
  </li>
</ul>

<h4>What is Blackboard?</h4>

<ul>
  <li>UMBC uses Blackboard, an online learning management system, for online,
  hybrid, and many in-person courses.</li>

  <li>When you register for a class, you are automatically enrolled in
  Blackboard.</li>

  <li>Be sure to <a href="http://blackboard.umbc.edu/" target="_blank">log-in
  to your Blackboard account</a> early to check for updates - faculty often
  post course information/materials before the session begins.
  </li>

  <li>
    <a href="https://wiki.umbc.edu/pages/viewpage.action?pageId=31199856"
    target="_blank">How to log-in to Blackboard</a>
  </li>

  <li>
    <a href="https://wiki.umbc.edu/display/faq/Blackboard" target=
    "_blank">Learn more about Blackboard</a>
  </li>
</ul>

<h4>Where can I eat on campus during summer session?</h4>

<ul>
  <li>On-campus dining is available during summer session. <a href="http://www.dineoncampus.com/umbc/">UMBC Dining Services will post open</a> venues and hours closer to the start of the session. </li>
</ul>

<h4>Where can I find out about student events?</h4>

<ul>
  <li>Check with the <a href="http://www.seb.umbc.edu/" target="_blank">Student
  Events Board (seb)</a> to see what activities are being offered.</li>
</ul>

<h4>How can my parents see my schedule or grades?</h4>

<ul>
  <li>
    <p>You can grant permission  to let others see your schedule or grades through &ldquo;profile sharing&rdquo; in myUMBC.  This feature allows you to share some of your myUMBC information with  anyone who has a myUMBC, Google, or Facebook account. Learn  more about <a href="https://wiki.umbc.edu/display/faq/Profile+Sharing" target="_blank">Profile Sharing</a>. </p>
  </li>
</ul>
