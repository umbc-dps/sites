<p>Once you've registered for a summer course, be sure to have everything in place for a successful summer session. Use this checklist as your guide. </p>
<h4>General reminders</h4>

<ul>
  <li>Check important <a href="importantdates" target="_blank">dates and
  deadlines</a>.</li>

  <li>
    <a href=
    "https://csprd-web.psg.umbc.edu/psp/csprdg/EMPLOYEE/HRMS/c/COMMUNITY_ACCESS.CLASS_SEARCH.GBL?FolderPath=PORTAL_ROOT_OBJECT.CO_EMPLOYEE_SELF_SERVICE.HC_CLASS_SEARCH_GBL&IsFolder=false&IgnoreParamTempl=FolderPath%252cIsFolder"
    target="_blank">Verify</a> your class locations and meeting times.
  </li>

</ul>

<h4>UMBC parking permit</h4>

<ul>
  <li>Request a UMBC <a href="parking" target="_blank">parking permit</a>
  (if you don&rsquo;t already have a valid permit).</li>

  <li>Be sure to request your parking permit several weeks in advance to ensure
  it arrives before the start of classes<em>.</em></li>
</ul>


<h4>On-campus housing</h4>

<ul>
  <li>Check to see what <a href="housing" target="_blank">housing options</a>
  are available during summer/winter sessions.
  </li>
</ul>

<h4>Textbooks</h4>

<ul>
  <li>Purchase your <a href="textbooks" target="_blank">textbooks</a>&nbsp;and
  other course related materials.
  </li>
</ul>

<h4>Student ID Card</h4>

<ul>
  <li>Obtain a <a href="http://campuscard.umbc.edu/about/" target=
  "_blank">campus card</a> (student ID).
  </li>
</ul>

<h4><em>my</em>UMBC, UMBC e-mail, and <em>my</em>UMBC Help Tickets</h4>

<ul>
  <li>Make sure you have an activ<em>e <a href="faq" target=
  "_blank">my</a></em><a href="faq">UMBC account</a>.
  </li>

  <li>Check your UMBC e-mail account on a regular basis.</li>

  <li>View how to <a href=
  "https://wiki.umbc.edu/pages/viewpage.action?pageId=1867963" target="_blank">
    forward</a> your UMBC e-mails to an already existing e-mail account
    (Hotmail, Gmail, etc.).</li>

  <li>Submit a <em><a href="faq" target="_blank">my</a></em><a href=
  "faq">UMBC Help Ticket</a> for billing issues, computing/technology
  support, questions about your class schedule, grades, etc.
  </li>
</ul>

<h4>Blackboard</h4>

<ul>
  <li>Before the session begin, check your <a href="faq" target=
  "_blank">Blackboard account</a> to see course updates/assignments.
  </li>
</ul>



<h4>E2Campus (text alerts)</h4>

<ul>
  <li>
    <a href="https://apps-my.umbc.edu/alerts/" target="_blank">Sign-up for
    E2Campus</a>, an emergency alert text-messaging system that notifies
    subscribers of any campus-related emergency (such as a potential safety
    hazard or campus closure due to weather).
  </li>

  <li>
    <a href="../facultystaff/inclementweather.html" target="_blank">Check
    here</a> for more information about E2Campus alerts.
  </li>
</ul>

<h4>Get involved and connect with students outside the
classroom!</h4>

<ul>
  <li>The <a href="http://my.umbc.edu/groups/seb/events" target=
  "_blank">Student Events Board</a> (seb) hosts regular on-campus activities
  and some off campus trips.
  </li>

  <li class="style4">See what events are happening at the <a href=
  "http://my.umbc.edu/groups/recsports" target="_blank">Retriever Athletic
  Center</a> (RAC).
  </li>

  <li>All RAC users must present a valid UMBC campus card (student) ID to gain
  entry.</li>

  <li>Visit the <a href="http://artscalendar.umbc.edu/" target="_blank">Arts
  &amp; Culture Calendar</a> for information about dance, music, visual art,
  theatre, and other<br>
    humanities events.
  </li>
</ul>

