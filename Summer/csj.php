<h3>Sociology</h3>
<p> <em>Focuses on race and ethnic inequality from a sociological perspective by examining sociocultural, economic, and political factors that have shaped contemporary race and ethnic relations in the U.S. Issues addressed include power, privilege, racism, immigration, discrimination, and social change based on resistance and social movements.</em></p>

<div class="accordionheader">
 <h4>SOCY 201:  Social Problems in American Society<i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
<p>Examines the key social problems in contemporary U.S. society from a sociological perspective. Social issues include poverty, crime and violence, gender and race discrimination, and unequal access to societal resources, such as education and healthcare. Learn to think creatively about possible solutions to social problems by understanding who is involved in defining social problems and the roles played by movements in raising awareness about social issues.</p>

