  <h3> Anthropology</h3>
  
  <p> <em>Anthropology is the worldwide comparative study of humankind, present and past. UMBC’s Cultural Anthropology program promotes deeper understanding of the diversity of cultural worlds, human social organization and social structure, and social research methods. Anthropology courses help students understand the complexity of current events within the U.S. and internationally as well as the importance of recognizing and weighing equally cultural values and ideas from across the globe. </em></p>
  
    
<div class="accordionheader">
  <h4> ANTH 211: Cultural Anthropology <i class="fa fa-angle-down"></i></h4>
</div>

 <div class="accordionbody">
<p>An introduction to the central concepts and issues in cultural anthropology. Employs an ethnographic perspective to examine topics including the concept of culture and diversity; social organization, exchange systems, and power within and across societies; religion, rights, and other value systems; and the possibilities for agency and change.  </p>  

