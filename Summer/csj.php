<div class="accordionheader">
  <h4>AMST 352: American Culture in Global Perspective <i class="fa fa-angle-down"></i></h4>
</div>

 <div class="accordionbody">
<p> Emphasizes American consumer culture in the context of globalization. Illuminates the meaning and history of particular American social structures, cultural values and ideological themes by placing them in a global perspective. Explore topics like "McDonaldization" as well as the global labor and ecological issues surrounding the garment industry, and the representational politics of American mass-mediated entertainment as public diplomacy. </p>
 
