 <h5>Social Justice (so·cial jus·tice) noun. 1: a social philosophy advocating the removal of inequalities among people; 2: the idea of acting with equality, fairness and dignity to all human beings.</h5>  <br>

   
 <p>This summer, interdisciplinary departments in UMBC’s College of Arts, Humanities, and Social Sciences will offer general education courses focused on topics in culture and social justice.</p>
 
 <p>These courses address relevant social issues, while promoting critical thinking and writing skills. They offer a view of social issues and practices from various perspectives, as well as the intersectional analysis of race, class, and gender. Courses in culture & social justice invite students to understand and analyze structural inequalities while challenging them to envision and build a better future.</p>
  
 <h3>Africana Studies</h3>
 
 <p><em>Africana Studies provides the tools to understand, critically evaluate, analyze and interpret events and phenomena that structure the experiences, possibilities and dynamics of the people of African descent in the U.S., Africa and its Diasporas. Courses address the cultural, economic, historical, political, psychological and sociological issues that affect peoples of African descent.</em></p>
 
<div class="accordionheader">
  <h4>AFST 100: Introduction to the Black Experience<i class="fa fa-angle-down"></i></h4>
</div>

 <div class="accordionbody">
 <p>Introduces the black experience in the African Diaspora through a survey of historical and sociocultural ties that link people of African descent worldwide. Evaluates ideas about the different cultural, economic, psychological, sociological, and political challenges confronting people of African descent in the 21st century. Particular attention is paid to how to evaluate those challenges and proposed solutions in the aftermath of Obama’s presidency.</p>

<p><strong>GEP: (AH)<br>
4W1 – ONLINE<br>
Tyson King-Meadows</strong></p>
 </div>  
  
<div class="accordionheader">
  <h4>AFST 213: Africa: Culture & Development<i class="fa fa-angle-down"></i></h4>
</div>

 <div class="accordionbody">
<p>An introduction to the history, peoples, cultures, languages, and societies of the African continent. While addressing the diversity of African cultural history and development, other themes include the colonial and post-colonial period, African nationalism, neo-colonialism, and African culture and development today.</p>

<p>Also listed as MLL 210.</p>

<p>
<strong>GEP: (AH) or (C)<br>
