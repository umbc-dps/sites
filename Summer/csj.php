<div class="accordionheader">
 <h4> GWST 338/POLI 338: Women, Gender and Law <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
<p>Examines ways in which gender affects rights within the American civil and criminal legal systems. It explores the interrelationship between traditional attitudes and stereotypes concerning women's roles in society and the historical development of women's legal rights. Topics include discrimination in employment and education, reproduction rights, and criminal law issues affecting women, including domestic violence, rape and prostitution. </p>


