<div class="accordionheader">
 <h4> ANTH 326: American Indian Cultures <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
<p>Drawing on contemporary ethnographic studies, ANTH 326 explores the cultural diversity of groups indigenous to the Americas. It examines issues of representation and identity in contemporary North American Indian communities including the role of anthropological interpretation and its impact on perceptions of Native America. Emphasizes the economic and political adaptations made by Indigenous people through the early 21st century in response to social change and challenges to social justice. </p>

