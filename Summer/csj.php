 <h3>American Studies</h3>
 <p> <em>A focus on issues of race, ethnicity, class, gender, and social justice has always been integral to the American Studies Department at UMBC. These courses provide skills to critically analyze issues of place, identity, and community through a comparative and transnational lens. </em></p>
 
<div class="accordionheader">
  <h4>AMST 320: TV and American Culture <i class="fa fa-angle-down"></i></h4>
</div>

 <div class="accordionbody">
<p>Examines the economic, social and artistic implications of television in the United States. It explores how TV’s history interacts with class, gender, ethnicity, and sexuality in terms of social hegemony. Explore new ways people consume media and how television is now available on many kinds of platforms and what that means for the industry as it tries to survive in the 21st century. </p>  

<p> <strong> GEP: (AH)<br>
