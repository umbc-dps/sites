<h3> Gender & Women’s Studies</h3>
<p> <em>Gender and Women’s Studies courses provide skills to critically analyze how gender and sexuality intersect with differences of race, class, nationality, ability status, and more to make our world. Courses help demonstrate how these axes of difference structure unequal access to resources, distribute life chances differently, and both produce and challenge justice in local and global settings. GWST courses help students think critically, not just about the history and origins of injustice, but also the ways people continue to strive to achieve social justice.</em></p>

<div class="accordionheader">
 <h4> GWST 210: Introduction to Critical Sexuality Studies<i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
<p>Examines the history and roots of inequality with a particular focus on sexual and gender minorities both locally and globally. It also looks at the intersections of sexuality with other differences such as gender, race, class, and ability. Students learn about social justice movements to challenge these inequalities. Also listed as AMST 210. </p>

<p> <strong>GGEP: (SS) or (C) <br>
6W1 - Wed 1-4:10 pm HYBRID <br>
Kate Drabinski</strong></p>
</div>

<div class="accordionheader">
 <h4> GWST 310: Gender and Inequality in America<i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
<p>Examines the roots of gender, class, and race-based inequalities through case studies of gender, labor,and globalization and the growth of the U.S. prison system. Students are encouraged not just to understand these issues, but also learn to evaluate and propose policy solutions and actions for social 
change. Also listed as AMST 310. </p>

<p> <strong>GEP: (AH) <br>
