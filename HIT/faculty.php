<div class="accordionheader">  
<h4> Maansi Raswnat, JD | Graduate Faculty <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
 <p><img src="images/faculty/umbc-faculty-Maansi-Raswnat.jpg" alt="UMBC Faculty Maansi Raswnat" align="left" width="200">
 B.A. Interdisciplinary Studies UMBC<br>
   JD Seton Hall University School of Law</p>
 <p>Maansi Raswant is a senior regulatory and policy advisor at The Hilltop  Institute. Currently, she is responsible for policy development and analysis to  support Maryland in the implementation of the Affordable Care Act (ACA). Ms.  Raswant has drafted regulations and legislation for the Maryland Health Benefit  Exchange. In addition, she leads efforts in summarizing federal guidance for  state agencies. Prior to this, she was a policy analyst at Hilltop.</p>
 <p>Previously, Ms. Raswant gained administrative law experience as a law  clerk at the Department of Health and Human Services, Office of General Counsel  (New York). In this position, she negotiated Medicare Secondary Payer claims  with personal injury attorneys, researched Freedom of Information Act (FOIA)  regulations to assist in responding to FOIA requests, and assisted agency  counsel with research for motions and briefs. Ms. Raswant was also a health law  and policy intern for the Asian and Pacific Islander American Health Forum,  where she analyzed sections of the ACA to determine its impact on foreign-born  populations living with HIV/AIDS. In addition, she drafted a legal memorandum  and policy paper on immigrant health benefits access, used by national advocacy  organizations in furthering legal action and framing community forum discussion  points. Ms. Raswant also served as a research assistant at the Center for  Health and Pharmaceutical Law and Policy at Seton Hall University School of  Law, where she researched several aspects of clinical trial subject recruitment  and physician compensation policy for a clinical trial conflict of interest  white paper, released in November 2009. Prior to that, she was a research  assistant at Hilltop, providing support for several different projects  addressing the efficacy of Maryland Medicaid programs.</p>
 
 <p>Ms. Raswant earned her law degree from Seton Hall University School of  Law, with a certificate in health law. While in law school, Ms. Raswant served as  executive editor of <em>Health Law Outlook</em>, a biannual student publication. She  earned her BA in interdisciplinary studies with a concentration in health care  ethics and public policy from UMBC, completing a capstone thesis on pandemic  resource allocation protocols in Maryland. Ms. Raswant is admitted to practice law  in Maryland. She is a member of the Maryland State Bar Association, American  Bar Association, and American Health Lawyers Association. </p>
 
 <p>Ms. Raswant will be teaching Health IT Law and Ethics. This course  provides a comprehensive overview of important legal principles affecting health  information technology and management, with a focus on the intersection of  these legal principles with business ethics.</p>
</div>

