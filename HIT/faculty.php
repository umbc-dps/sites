<h4><a id="tsai" href="#tsai"></a>Terry Tsai, MSPH, PhD | Graduate Faculty <i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
 <p><img src="images/faculty/umbc-faculty-Terry-Tsai.jpg" alt="UMBC Faculty Terry Tsai" align="left" width="200">PhD, Biomedical and Health Informatics, University of  Washington<br>
   MSPH, Public Health Informatics, Emory University<br>
   BBA, Information Technology, College of William and Mary</p>
   
<p>Dr. Terry Tsai has an extensive and diverse background in  biomedical and health informatics, in the domains of clinical and translational  medicine, with a particular emphasis on patient safety and population  genetics.  Her research interests include:  data integration, text mining, decision analysis, genetic epidemiology, patient  safety, and patient advocacy.</p>

<p>Dr. Tsai was previously a faculty member at the  University of Maryland Baltimore, working on numerous projects including a  research grant related to heart disease in rural hospitals as well as another grant  dealing with microbiota and its impact on pre-term birth.  She has also been a faculty member at the  Armstrong Institute for Patient Safety and Quality, working on various patient  quality initiatives: including the Surgical Unit-based Safety Program (SUSP) to  Reduce Surgical Site Infections (SSI) and Other Surgical Complications as well  as the Comprehensive Unit-Based Safety Program (CUSP) for Ventilator Associated  Pneumonia (VAP) Preventions initiative.  She  is currently a Faculty Research Associate at Johns Hopkins University, within  the Division of Health Sciences Informatics, serving as the PI on an AHRQ R03  grant focused on decision making for hospital decision makers when prioritizing  patient safety initiatives.</p>

<p>Dr. Tsai holds a Ph.D. in Biomedical and Health  Informatics from the University of Washington in Seattle as well as master&rsquo;s  degree in Public Health Informatics from Emory University in Atlanta. She completed her postdoctoral fellowship at  Johns Hopkins University.</p>
</div>

