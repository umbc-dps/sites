<h4><a id="evatt" href="#evatt"></a> 
Angela Evatt, M.A., M.P.P. | Graduate Faculty<i class="fa fa-angle-down"></i></h4>
</div>

<div class="accordionbody">
 <p><img src="images/faculty/umbc-faculty-Angela-Evatt.jpg" alt="UMBC Faculty Angela Evatt" align="left" width="200">
 B.A.,  Psychology, University of Louisiana at Lafayette<br>
   A.M., Social Service Administration, University  of Chicago, School of Social Service Administration<br>
   M.P.P., Public Policy, University of Chicago,  Harris School of Public Policy </p>
   
<p>Angela  Evatt is Division Chief for Health Information Exchange at the Maryland Health  Care Commission&rsquo;s (MHCC) Center for Health Information Technology and  Innovative Care Delivery.  Ms. Evatt  manages the 45 member health information exchange (HIE) Policy Board and played  an integral part in drafting the State&rsquo;s HIE Privacy and Security  regulations.  In her position, Ms. Evatt coordinates  various health information technology regulatory and research initiatives of  the MHCC to advance secure health information exchange and widespread adoption and  use of electronic health records.  Prior  to joining the MHCC, Angela served as Provider Training Coordinator and Analyst  for Automated Health Systems as they implemented a new Medicaid hybrid managed care program for the state of  Illinois.</p>

<p>Ms. Evatt completed  her dual masters from the University of Chicago, in Social Service  Administration and Public Policy.  During her time at University of  Chicago, Ms. Evatt held several community internship positions with local  not-for-profits and State agencies, including the Office of the Director at the  Illinois Department of Healthcare and Family Services, Jane Addams Hull House,  and the Illinois Facilities Fund.  She  earned her BA in Psychology with a minor in Cognitive Science from the  University of Louisiana at Lafayette. Ms. Evatt currently serves on several  workgroups and advisory panels in health information technology.</p>
</div>

