<h3>Faculty</h3>
<p>Courses   are taught by instructors prominent in Health IT industry as well as   faculty in the Information Systems department. These members conduct   cutting edge research in Health IT for some of Health IT's biggest   government and commercial organizations. The program is infused with   their unique combination of real-world experience in industry and   research. </p>
<div class="accordionheader">
  <h4>Krystl Haerian, M.D. | Program Director <i class="fa fa-angle-down"></i></h4>
</div>

 <div class="accordionbody">
  <p><img align="left" alt="UMBC Faculty Krystl Haerian" src="images/faculty/umbc-faculty-Krystl-Haerian.jpg">
 B.S.,  Biological Sciences, UMBC<br>
   M.S.,  Biotechnology, Johns Hopkins University<br>
   M.S.,  Biomedical Informatics, Columbia University<br>
   MD,  University of Maryland School of Medicine
   </p>
 <p>Dr. Haerian has over 12 years of medical, informatics, and  public health experience in both academia and government. As a Commissioner&rsquo;s  Fellow at the Food and Drug Administration&rsquo;s (FDA) Center for Tobacco Products,  she employed informatics methods to design, conduct, and analyze studies that  support the development of policies and regulations intended to decrease  morbidity and mortality from tobacco products.</p>
 <p>
   As a Postdoctoral Fellow at Columbia University&rsquo;s Department  of Biomedical Informatics, her research was focused on developing and applying  methodologies for pharmacovigilance and drug repurposing, utilizing electronic  health record data and natural language processing. During her fellowship she  was awarded Best Presentation at the NLM Training Conference and First Place  Poster Presentation at the DIA Annual Meeting; she was also honored to be  invited to present to the National Library of Medicine Board of Regents. </p>
 <p>Dr. Haerian completed a Postdoctoral Fellowship at the  National Institute of Health Laboratory for Informatics Development. Her  research included the development and application of clinical decision support  and alerts to prevent medication errors and to improve collection of clinical  research data. She also served on the JAMIA Student Editorial Board and the NIH  Fellows Editorial Board.</p>
   <p>As a Congressional Legislative Fellow in the US House of Representatives,  Dr. Haerian served as a scientific advisor with particular expertise in public  health and clinical medicine for Congresswoman Roybal-Allard and her staff. Her  responsibilities included meeting with lobbyists and special interest groups,  drafting bills and legislation, writing speeches and floor statements, and supporting  the Congresswoman&rsquo;s agenda as Chair of the Congressional Hispanic Caucus Health  Taskforce to advance policies that reduce health disparities across all  minority communities.  </p>
 <p>Dr. Haerian received her medical degree from the University  of Maryland School of Medicine. Additionally, she holds Masters&rsquo; degrees in  Biotechnology from Johns Hopkins University and in Biomedical Informatics from  Columbia University. She completed her undergraduate studies at UMBC with a B.S  in biological sciences and a minor in writing. She was a member of the UMBC  Crew Team in college. Dr. Haerian is a mother of 3. In her spare time, she  enjoys coaching her daughter&rsquo;s tyker lacrosse team and teaching STEM concepts  to elementary school children as coach of her son&rsquo;s Junior First Lego League.</p>
</p>
</div>

<div class="accordionheader">
  <h4>Craig Behm, MBA | Graduate Faculty<i class="fa fa-angle-down"></i></h4>
</div>
  
   <div class="accordionbody">
 <p><img src="images/faculty/umbc-faculty-Craig-Behm.jpg" alt="UMBC Faculty Craig Behm" align="left" width="200">BA University of Maryland,  College Park<br>
   MBA Loyola University  Sellinger School of Business</p>
<p>Craig Behm is the Executive Director of MedChi Network Services  (MNS), a firm that offers practice services and Accountable Care Organization  (ACO) management as a subsidiary of MedChi, the Maryland State Medical Society.  He created the initial formation strategy for MNS and executed on those plans to  form two distinct service lines advancing health care delivery. MNS is a state-designated management services  organization and a co-recipient of federal funding through the Maryland  Regional Extension Center, the Chesapeake Regional Information System for our  Patients (CRISP). Through that partnership, MNS is developing secure methods  for the effective utilization of electronic health records. CRISP is also the  state-designated Health Information Exchange, through which MNS is supporting  the real-time transfer of health information. MNS assisted hundreds of small  practices to reach Meaningful Use of certified EHRs, and continues to help with  implementation and optimization of IT systems.</p>

  <p>Mr. Behm is the Executive  Director of the three ACOs owned and operated by MNS. He performed a vital  role in the ACO formation process and leads clinical integration and quality  improvement actions.  All three ACOs were selected for the  competitive Advance Payment program (out of 33 total nationwide). Through those  groups, MNS is coordinating health care needs for over 25,000 Medicare  beneficiaries through a network of nearly 100 independent primary care  providers. Mr. Behm continues to facilitate the expansion of ACOs and  related services to enhance the private practice of medicine.</p>
  <p>
  Prior to joining MNS, Mr. Behm worked as a health care  business consultant for a small firm in the Baltimore region. He also worked at  Erickson Living, a large-scale developer and operator of continuing care  retirement communities. Mr. Behm holds a Bachelors of Arts from the University  of Maryland, College Park and a Masters of Business Administration from the  Loyola University Sellinger School of Business. </p>
  
  <p>
   Mr. Behm is teaching Process and  Quality Improvement with Health IT, which will provide an overview of quality measurement and process improvement as they  relate specifically to the health care industry. The purpose  of the class is to examine tools, techniques, and resources available to  health care professionals through effective use of health IT  and – most importantly – to successfully implement  them in a variety of health care settings.</p>
   </div>

   
