   <p>
   Dr. Robinson, an adjunct professor with UMBC since 2014, began his medical research  training working with a team at the Johns Hopkins Quality and Safety Research  Group (QSRG) on their international, <strong><em>On the CUSP: STOP BSI</em></strong> campaign. Since that time, Dr. Robinson has authored several manuscripts and  book chapters around health care quality improvement and patient safety. </p>
 <p>Dr. Robinson pursued a degree in clinical Investigation,  Biostatistics and Epidemiology at the Bloomberg School of Public Health where  he completed a thesis study on &ldquo;<strong><em>Airway Management by a Difficult Airway  Response Team&rdquo;. </em></strong>Dr. Robinson then became interested in the business of  healthcare and quality improvement, so he took time out to pursue a business  degree at the Johns Hopkins Carey Business School. Since 2012, Dr. Robinson has  applied his medical and research training along with his public health training  in the field of Telemedicine at Philips Healthcare. He is now a senior manager  in the research and development department working to extend Telemedicine  across the healthcare continuum while retaining his research interests in QI.</p>
 <p>Dr. Robinson will be teaching Health Informatics I. This  course touches people and organizational aspects of health information systems  as well as technology. Some of the topics covered in this course include  electronic health records, practice management, health information exchange,  data standards consumer health informatics and mobile health.</p>
